<h1>Algorithmen und Programmierung I und II </h1>
<div class="zeile">
<div class="float dreifach last">
<p>Pflichtfach<br />15 ECTS-P<br />12 SWS<br />j&auml;hrlich angeboten </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Verantwortlich </h2>
<div class="float dreifach last">
<p>Prof. Dr. Frank Victor, Prof. Dr. Erich Ehses </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Ziele </h2>
<div class="float dreifach last">
<p>Die Studierende sollen die Prinzipien der Objektorientierung verstanden haben und einfache Sofwareeinheiten unter Beachtung dieser Prinzipien erstellen k&ouml;nnen. Sie sollen die wichtigsten Eigenschaften der Programmiersprachen C und Java sowie der Java-Entwicklungsumgebung kennen. Sie sollen einfache Algorithmen beurteilen, vorhandene Bibliotheksl&ouml;sungen angemessen einsetzen und einfache Algorithmen selbst&auml;ndig entwickeln k&ouml;nnen. </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrinhalte </h2>
<div class="float dreifach last">
<ul><li>Prozedurale Programmierung am Beispiel von C </li><li>Objektorientierte Programmierung am Beispiel von Java</li><li>Kontrollstrukturen</li><li>Modularisierungskonzepte</li><li>Typkonzept</li><li>Schnittstellen</li><li>Robustheit und Ausnahmenbehandlung</li><li>statischer und dynamischer Typ</li><li>Polymorphie und Vererbung</li><li>Grundmuster der objektorientierten Programmierung</li><li>Algorithmenbegriff</li><li>Korrektheit</li><li>Laufzeitverhalten von Algorithmen</li><li>Sequenzielle Datenstrukturen</li><li>Bin&auml;rb&auml;ume</li><li>Algorithmen zum Suchen und Sortieren</li><li>Assoziativer Speicher </li></ul></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrmethoden </h2>
<div class="float dreifach last">
<p>Im Rahmen von Vorlesung, &Uuml;bung, Praktikum: Vermittlung der Prinzipien und Mechanismen der prozeduralen und objektorientierten Programmierung. Ein&uuml;ben der F&auml;higkeit, kleinere Anwendungen und Module selbstst&auml;ndig zu erstellen. Vermittlung der Kenntnis der grundlegenden Datenstrukturen und der grunds&auml;tzlichen Merkmale von Algorithmen. Beurteilung und Entwicklung einfacher Algorithmen. </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Vorraussetzungen </h2>
<div class="float dreifach last">
<p>Keine &uuml;ber die Zulassungsvorraussetzungen hinausgehenden Vorraussetzungen </p></div><br style="clear:both" /></div>
