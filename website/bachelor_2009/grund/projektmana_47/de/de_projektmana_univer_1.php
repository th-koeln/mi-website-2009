<h1>Projektmanagement </h1>
<div class="zeile">
<div class="float dreifach last">
<p>Pflichtfach<br />5 ECTS-P<br />4 SWS<br />j&auml;hrlich angeboten </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Verantwortlich </h2>
<div class="float dreifach last">
<p>Prof. Dr. Holger G&uuml;nther </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Ziele </h2>
<div class="float dreifach last">
<p>Erfolgreiches Projektmanagement umfasst organisatorische, planerische und kontrollierende Aktivit&auml;ten. Projekte im Bereich der interaktiven Medien stellen hierbei eine besondere Herausforderung dar: neue Gesch&auml;ftsprozesse, z.B. durch neue Vertriebswege, treffen auf neue Technologien im Umfeld Internet. Zusammengesetzte Projektteams aus den Fachkompetenzen Informationstechnologie (IT), Design und Kommunikation/Marketing m&uuml;ssen in Projekten vereint werden, die bisher Dom&auml;ne der IT-Spezialisten waren. Soziologische Aspekte sind von erfolgskritischer Bedeutung.<br /><br />Dies stellt besondere Anforderungen an den Projektleiter und das Projektteam: Methodisches und zielgerichtetes Vorgehen, gute Kommunikation im Team und mit dem Kunden und das Finden einer gemeinsamen fachlichen, sprachlichen und auch kulturellen Ebene sind die notwendigen Mittel f&uuml;r ein erfolgreiches und effektives Projektmanagement.<br /><br />Die vielseitigen Aufgaben eines Projektleiters sollen erarbeitet und alle notwendigen Methoden und auch Methodiken (dies sind im Ggstz. zu Methoden die handwerklichen Grundlagen und administrativen Basisaufgaben (="Administrivialit&auml;ten" nach Tom DeMarco) soweit erforderlich vermittelt werden.<br /><br />Die Studierenden sollen in die Lage versetzt werden, Organisation und Leitung eines Projektes in die spezifische Unternehmenshierarchie einzubetten, dabei u.a. mit Hilfe von Werkzeugen eigenst&auml;ndig Projektpl&auml;ne zu erstellen und in allen Disziplinen proaktive Planung und Kontrolle durchf&uuml;hren zu k&ouml;nnen.<br /><br />Angestrebte Lehrergebnisse sind u.a.: <li>Beherrschung der gundlegenden Aufgaben des Projektmanagements, insb. in DV-Projekten.</li><li>Beherrschung der Projektmanagement-Disziplinen und - Methoden und die F&auml;higkeit zur selbst&auml;ndigen und proaktiven Anwendung erforderlicher Ma&szlig;nahmen und Methodiken. </li><br /></p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrinhalte </h2>
<div class="float dreifach last">
<p>Organisation des Projektes<br />Organisationsformen, Projektgr&uuml;ndung, Methoden&Techniken<br /><br />Planung des Projektes u.a. Integrations-, &Auml;nderungs-, Scope­(Inhalt/Umfang) und Zeit-Management<br /><br />Kontrolle des Projektes u.a. Qualit&auml;ts-, Kosten-, Human Resource-, Risiko-und Beschaffungs-Management<br /><br />Erarbeitung praktischer Aspekte, begleitet durch eine entsprechende Fallstudie<br /><br />Spezielle methodische und inhaltliche Aspekte: <li>Change Management / Release Management</li><li>Risk Management</li><li>Quality Management</li><li>Cost Management	</li><li>Communications-und Human Resource-Management 	</li><li>Perfomance Reporting und - Kontrolle / Earned Value Management </li><br /></p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrmethoden </h2>
<div class="float dreifach last">
<p>Vorlesung, Praktikum, Seminar </p></div><br style="clear:both" /></div>
