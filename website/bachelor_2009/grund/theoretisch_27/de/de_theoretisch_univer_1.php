<h1>Theoretische Informatik </h1>
<div class="zeile">
<div class="float dreifach last">
<p>Pflichtfach<br />10 ECTS-P<br />8 SWS<br />j&auml;hrlich angeboten </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Verantwortlich </h2>
<div class="float dreifach last">
<p>Prof. Dr. Erwin Holland-Moritz </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Ziele </h2>
<div class="float dreifach last">
<ul><li>Die Studierenden sollen den in der Vorlesung dargestellten theoretischen Stoff durch erg&auml;nzende selbst&auml;ndige Literaturarbeit und an anderen Beispielen aufarbeiten. </li><li>Aufgaben zu den Lehrinhalten(s.u.) werden in kleinen Gruppen (Teamarbeit) selbst&auml;ndig gel&ouml;st. Die L&ouml;sungen sollen in den &Uuml;bungsstunden vorgetragen erden und der L&ouml;sungsweg den Kommilitonen hierbei erl&auml;utert werden.</li><li>Zum Abschluss muss jeder Studierende zum erfolgreichen Abschluss des Faches in der Lage sein, entsprechende Problemstellungen selbstst&auml;ndig zu l&ouml;sen. </li></ul></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrinhalte </h2>
<div class="float dreifach last">
<p>Dieses Grundlagenfach zur Informatik ersreckt sich &uuml;ber zwei Semester. Der theoretische Inhalt gliedert sich in zwei Teile:</p><ul><li>kurzer Abriss theoretischer Grundlagen der Digitaltechnik </li><li>ausf&uuml;hrliche Einf&uuml;hrung in die theoretischen Grundlagen zur Softwaretechnik (Beginn Mitte des 1.Semesters) </li></ul><p>1. Polyadisches Zahlensystem<br />2. Fehlererkennung und -korrektur<br />3. Boolsche Algebra<br />4. Pr&auml;dikatenlogik<br />5. Boolsche Funktionen<br />6. Basiswissen zur Graphentheorie<br />7. Sprachklassen der Chomsky Sprachen-Hierarchie<br />8. Automaten<br />9. Grammatiken<br />10. Regul&auml;re Sprachen (Typ-3)<br />11. Minimierungsverfahren<br />12. Entscheidbarkeitsprobleme<br />13. Kontextfreie Sprachen (Typ-2)<br />14. Kellerautomaten<br />15. Anwendungsbeispiele<br />16. Compilerbau<br />17. Kontextsensitive Sprachen (Typ-1)<br />18. Rekursiv-aufz&auml;hlbare Grammatik (Typ-0)<br />19. Turingautomaten<br />20. Synchrone und Asynchrone Automatennetze<br />21. Theorie der Petri-Netze </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrmethoden </h2>
<div class="float dreifach last">
<p>Vorlesungen (2 SWS) und &Uuml;bungen (2 SWS) pro Semester </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Vorraussetzungen </h2>
<div class="float dreifach last">
<p>Keine &uuml;ber die Zulassungsvorraussetzungen hinausgehenden Vorraussetzungen </p></div><br style="clear:both" /></div>
