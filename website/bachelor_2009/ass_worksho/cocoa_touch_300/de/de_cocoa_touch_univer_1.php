<h2 class="float einfach">Ziel </h2>
<div class="float dreifach last">
<p>Ziel des Kurses ist es den Teilnehmern einen schnellen Einstieg in die Entwicklung von Anwendungen f&uuml;r das iPhone/iPod zu erm&ouml;glichen. Zu diesem Einstieg geh&ouml;ren au&szlig;erdem die Betrachtung der Entwicklungsumgebung Xcode, des Interface-Builders, des Analysewerkzeuges Instruments sowie die Benutzung der Dokumentation. Am Ende des Kurses werden die Teilnehmer mit den notwendigen Werkzeugen umzugehen wissen, sowie f&auml;hig sein die entsprechenden Quellen zur Entwicklung eigener Anwendungen heranzuziehen. Aufgrund der begrenzten Zeit werden nur die wichtigsten Konzepte, Frameworks und Werkzeuge vorgestellt werden. </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Teilnahmebedingung </h2>
<div class="float dreifach last">
<p>Der Kurs richtet sich sowohl an Bachelor- als auch an Master-Studenten. F&uuml;r Bachelorstudenten gilt: Neben dem Interesse am Programmieren m&uuml;ssen die Teilnehmerinnen und Teilnehmer das Grundstudium abgeschlossen haben. Ein Schein aus dem Grundstudium darf fehlen. Erfahrungen mit einer objektorientierten Programmiersprache sollten die Teilnehmer haben. Kenntnisse einer speziellen Sprache sind nicht notwendig. </p></div><br style="clear:both" /></div>
