<h1>Ruby on Rails </h1>
<div class="zeile">
<h2 class="float einfach">Betreuer </h2>
<div class="float dreifach last">
<p>Andreas Bade<br />Dirk Breuer </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Beschreibung </h2>
<div class="float dreifach last">
<p>
<object width="526" height="360"><param name="movie" value="http://www.youtube.com/v/s6o8V94S3Tw&hl=de_DE&fs=1&"></param><param name="allowFullScreen" value="true"></param><param name="allowscriptaccess" value="always"></param><embed src="http://www.youtube.com/v/s6o8V94S3Tw&hl=de_DE&fs=1&" type="application/x-shockwave-flash" allowscriptaccess="always" allowfullscreen="true" width="526" height="360"></embed></object><br /><br />Webbasierte Software-Systeme sind aus der heutigen IT-Landschaft nicht mehr wegzudenken. Neben Produktplattformen oder Online-Portalen etablieren sich auch gerade "Software as a Service" (SaaS) L&ouml;sungen immer mehr als Alternative zu nativen Desktop-Anwendungen. Die Vorteile hierbei liegen klar auf der Hand: st&auml;ndige Verf&uuml;gbarkeit der Software, wesentlich geringere Nutzungskosten, hohe Erreichbarkeit der Kunden, Minimierung der Entwicklungskosten durch relativ einheitliche Plattform, schnelle Auslieferung von Updates und noch viele mehr. All diese Vorteile stellen dabei entsprechende Anforderungen an die jeweilige Technologie dahinter. In allen g&auml;ngigen Hochsprachen haben sich daher in den letzten Jahren mehrere Rahmenwerke etabliert die es Entwicklern erm&ouml;glichen Webapplikationen effizient zu erstellen. Eines dieser Rahmenwerke, was in letzter Zeit immer wieder im Rampenlicht stand ist "Ruby on Rails". Es ist in Ruby geschrieben, einer dynamisch typisierten, vollst&auml;ndig objektorientierten Interpretersprache. Das Rahmenwerk liegt seit Februar in einer neuen Beta Version vor, bei der unter der Haube eine ma&szlig;gebliche Neu- und Weiterentwicklung statt gefunden hat. </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Ziel </h2>
<div class="float dreifach last">
<p>In diesem ASS-Kurs wollen wir euch zum einen in die Grundz&uuml;ge der Ruby-Entwicklung einf&uuml;hren und dar&uuml;ber hinaus mit euch gemeinsam eine einfache Webapplikation entwickeln. Ziel am Ende soll es sein, dass ihr die Konzepte hinter Ruby on Rails soweit verinnerlicht habt, dass ihr selbstst&auml;ndig Webanwendungen konzipieren und umsetzen k&ouml;nnt. Bisher konnten wir dieses Ziel immer ganz gut erreichen und sind guter Dinge, dass uns das auch bei diesem Kurs wieder gelingen wird. Wir freuen uns also auf zahlreiche Anmeldungen. </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Teilnahmebedingung </h2>
<div class="float dreifach last">
<p>Der Kurs richtet sich sowohl an Bachelor- als auch an Master-Studenten. F&uuml;r Bachelorstudenten gilt: Neben dem Interesse am Programmieren m&uuml;ssen die Teilnehmerinnen und Teilnehmer das Grundstudium abgeschlossen haben. Ein Schein aus dem Grundstudium darf fehlen. Erfahrungen mit einer objektorientierten Programmiersprache sollten die Teilnehmer haben. Kenntnisse einer speziellen Sprache sind nicht notwendig. </p></div><br style="clear:both" /></div>
