<h1>Maven Spring </h1>
<div class="zeile">
<h2 class="float einfach">Betreuer </h2>
<div class="float dreifach last">
<p>Matthias Richter<br />Lars Haferkamp </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Beschreibung </h2>
<div class="float dreifach last">
<p>Mit diesem Workshop werden Studenten an professionelles Build- und Konfigurationsmanagement in der Java-Welt herangef&uuml;hrt. Die F&auml;higkeit, Software strukturiert zu bauen und zu konfigurieren, ist die Grundlage f&uuml;r eine erfolgreiche Arbeit in gr&ouml;&szlig;eren Teams und agilen Prozessen. Das Management des Build-Prozesses stellt in der Softwareentwicklung eine fundamentale Aufgabe dar, die in jeder Projektart und -gr&ouml;&szlig;e von zentraler Bedeutung ist: ohne einen Software-Build gibt es schlicht keine funktionierende Software. In diesem Workshop wird mit Maven eines der verbreitetsten Build-Tools verwendet und mit dem Tool in praktischen Aufgaben gearbeitet.<br /><br />W&auml;hrend das Buildmanagement mit Aufgaben an der Software betraut ist, arbeitet das Konfigurationsmanagement in oder mit der Software. Anhand des Spring-Frameworks werden im Workshop Kenntnisse dar&uuml;ber vermittelt, wie man Software konfigurieren kann. </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Ziel </h2>
<div class="float dreifach last">
<p>Nach der Veranstaltung verf&uuml;gen Studenten &uuml;ber Kenntnisse, welche hohe Relevanz in der Praxis haben, zum grundlegenden Aufbau und Betrieb von Software. Vermittelt werden zum einen allgemein anwendbare, grundlegende Konzepte der Softwareentwicklung und zum anderen die praktische Anwendung mit Hilfe weit verbreiteter Tools.<br /><br />Am Ende der Veranstaltung sollen die Teilnehmer: <li> mit Maven selbst&auml;ndig und innerhalb von Minuten die technische Projektstruktur von verschiedenen Java-Applikationen aufbauen k&ouml;nnen und</li><li> diese Projektstruktur um Frameworks / Libraries erweitern k&ouml;nnen</li><li> den Maven Lifecycle f&uuml;r ihre Zwecke nutzen k&ouml;nnen</li><li> die Applikation in Pakete (JARs, WARs, EARs) packen und auf Server deployen k&ouml;nnen,</li><li> Spring in ihre Applikation integrieren k&ouml;nnen.</li><li> Mit Hilfe von Spring die Entkopplung von Applikationskomponenten erreichen,</li><li> durch Spring in der Lage sein, verschiedene Komponenten zu konfigurieren</li><li> typische Konzepte der agilen Entwicklung mit Maven und Spring umsetzen k&ouml;nnen. </li></ul>Das Wissen erm&ouml;glicht Teilnehmern unter anderem: <li> den einfachen Kick-off von neuen, eigenen Projekten</li><li> den schnellen Einstieg in kommerzielle Projekte</li><li> die Mitarbeit in Projekten der Open Source Welt </li><br /></p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Teilnahmebedingung </h2>
<div class="float dreifach last">
<p>Der Kurs richtet sich sowohl an Bachelor- als auch an Master-Studenten. F&uuml;r Bachelorstudenten gilt: Neben dem Interesse am Programmieren m&uuml;ssen die Teilnehmerinnen und Teilnehmer das Grundstudium abgeschlossen haben. Ein Schein aus dem Grundstudium darf fehlen. Erfahrungen mit einer objektorientierten Programmiersprache sollten die Teilnehmer haben. Kenntnisse einer speziellen Sprache sind nicht notwendig. </p></div><br style="clear:both" /></div>
