<h1>Akkreditierung Bachelor </h1>
<div class="zeile">
<h2 class="float einfach">Was genau ist das Niveau eines bestimmten Bachelor-Abschlusses? </h2>
<div class="float dreifach last">
<p>Diese Frage stellen sich mehr und mehr Studieninteressierte, Arbeitgeber oder Hochschulen, die &uuml;ber die Aufnahme in einen Master-Studiengang entscheiden.<br /><br />Die Antwort soll durch die Akkreditierung von Studieng&auml;ngen erm&ouml;glicht werden. Die erfolgt je nach Fachrichtung und Ausrichtung des Studiengangs durch verschiedene Akkreditierungsagenturen. Der Bachelorstudiengang - ebenso wie der Masterstudiengang Medieninformatik - ist durch die ASIIN akkreditiert. Dies ist eine renommierte Agentur, die Informatikstudieng&auml;nge ebenso wie ingenieurwissenschaftliche und naturwissenschaftliche Studieng&auml;nge an Universit&auml;ten oder Fachhochschulen akkreditiert. </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="zeile">
<div class="subzeile">
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<dl class="einfach bild"><dt><img src="images/akkreditier_univer_12_543_.jpg" alt="Akkreditierung Bachelor Universalthema " width="172" class="" /></dt>
<dd><a href="http://www.asiin.de">www.asiin.de</a> </dd></dl>
<div class="float dreifach">
<p>Bei der Akkreditierung wird durch eine unabh&auml;ngige Fachkommission u.a. gepr&uuml;ft, ob ein Studiengang nach Inhalt und Niveau bundesweit einheitlichen Vorgaben entspricht, die in der Regel von berufst&auml;ndischen Vereinigungen festgelegt werden. Bei dem Studiengang Medieninformatik ist das die Gesellschaft f&uuml;r Informatik. Ausserdem wird gepr&uuml;ft, ob die personelle Ausstattung, die Ger&auml;te in den Laboren und die R&auml;ume und Einrichtungen (z.B. Bibliothek oder Informationsverarbeitung) f&uuml;r ein effektives Studium ausreichen. Die Akkreditierung muss alle f&uuml;nf Jahre wiederholt werden. F&uuml;r diesen Studiengang Medieninformatik wurde sie 2004 ausgesprochen und gilt demnach zun&auml;chst bis 2009. </p></div>
<div class="clear">&nbsp;<br /></div></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach">Internationale Anerkennung </h2>
<div class="float dreifach last">
<p>Die internationale Anerkennung, zumindest ausserhalb von Europa, muss von jeher meist im Einzelfall gekl&auml;rt werden. Die ASIIN, die die Medieninformatik Studieng&auml;nge der FH K&ouml;ln akkreditiert hat, verfolgt hier mit dem sogenannten <a href="http://en.wikipedia.org/wiki/Washington_Accord">Washington Accord</a> den Ansatz, dass ein Netz von Agenturen aus den verschiedenen Kontinenten die jeweils von Ihnen akkreditierten Abschl&uuml;sse anerkennt. </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<div class="clear">&nbsp;<br /></div></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach">Zugang zum weiterf&uuml;hrenden Studium </h2>
<div class="float dreifach last">
<p>Die Gliederung des Studiums in die Abschnitte Bachelor und Master bietet die Chance, nach dem ersten Abschnitt neue Schwerpunkte zu setzen. So kann man sich nach dem Bachelor in Medieninformatik entscheiden - u.U. an einer anderen Hochschule -, das Studium mit einem Masterstudium in einem verwandten Fach etwa in Medienwissenschaften, oder Wirtschaftsinformatik oder einem anderen Schwerpunkt fortzusetzen. Hierf&uuml;r ist es erforderlich, dass der Bachelorabschluss als ausreichend qualifizierend f&uuml;r das Masterstudium betrachtet wird. Was in der Regel nur bei akkreditierten Studieng&auml;ngen der Fall ist. Im Bereich der Masterangebote von Universit&auml;ten bietet die Akkreditierung durch die ASIIN die besten Voraussetzungen f&uuml;r eine Anerkennung. </p></div>
