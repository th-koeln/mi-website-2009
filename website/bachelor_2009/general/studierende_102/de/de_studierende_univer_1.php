<h1>Studierende trainieren Studierende </h1>
<div class="zeile">
<h2 class="float einfach">Wof&uuml;r steht ASS? </h2>
<div class="float dreifach last">
<p>ASS steht f&uuml;r "Aktuelles produktbezogenes Wissen in der Informatik - Studierende trainieren Studierende".<br /><br />Ihnen werden Kurse angeboten, die praktisches Wissen im Umgang mit aktuellen Produkten und Softwarepaketen vermitteln, und zwar hands-on - minimale Theorie maximale Praxis. </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach">Aktuelles produktbezogenes Wissen in der Informatik </h2>
<div class="float dreifach last">
<p>Bei der schnellen Entwicklung neuer Technologien und Methoden in der Informatik liegt bei den Lehrveranstaltungen der Schwerpunkt auf der Vermittlung von grundlegenden und etablierten Konzepten und deren praktischer Vertiefung. Neue, noch nicht etablierte Technologien, die oft viel versprechend und reizvoll sind, werden von den Studentinnen und Studenten auf Basis dieses Wissens im Rahmen von Projekt- und Abschlussarbeiten selbstst&auml;ndig erschlossen und eingesetzt.<br /><br />Um dabei die Einarbeitung zu unterst&uuml;tzen werden von Masterstudenten oder auch durch unsere Alumni dreit&auml;gige workshops angeboten, in denen anhand von von Miniprojekten Kompetenzen im Umgang mit einzelnen neuen Technologien vermittelt werden. </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<div class="clear">&nbsp;<br /></div></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach">Termine und Anmeldung </h2>
<div class="float dreifach last">
<p>Die Workshops werden in der Regel als Blockveranstaltung w&auml;hrend der vorlesungsfreien Zeit angeboten. Entsprechende Ank&uuml;ndigungen finden Sie hier auf der Website oder im <a href="../../../../../website/general/infra/semantic_me_69/de/de_semantic_me_articl_1.php">Semantic MediaWiki</a> der Medieninformatik. Die Workshops sind meist auf 15 Teilnehmer begrenzt und stets sehr gut besucht. Sollten Sie sich also f&uuml;r ein Angebot interessieren, melden Sie sich m&ouml;glichst schnell an. </p></div>
