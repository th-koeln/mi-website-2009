<h1>IPv6 und IPsec </h1>
<div class="zeile">
<h2 class="float einfach">Stefan Karsch<br />Hans L. Stahl </h2>
<div class="float dreifach last">
<p>Seit vielen Jahren hat das Internet-Protokoll IPv4 seine vielseitige Tauglichkeit unter Beweis gestellt. Gleichzeitig sind die Schw&auml;chen dieses Protokolls offenbar geworden. Zu den gravierendsten und bekanntesten geh&ouml;rt die Knappheit bei den IPv4-Adressen. Diese beseitigt IPv6 auf absehbare Zeit. Dar&uuml;ber hinaus hat IPv6 noch einige weitere Verbesserungen gegen&uuml;ber IPv4 zu bieten, u. a. werden Sicherheitsmechanismen (IPsec) als integraler Bestandteil von IPv6 geboten, die unter IPv4 lediglich als Erweiterung zur Verf&uuml;gung standen. Da ein immer gr&ouml;&szlig;erer Anteil der am Markt verf&uuml;gbaren Betriebssystemimplementierungen, eine zunehmende Zahl von Anwendungen und vor allem auch fast alle neueren Router IPv6 unterst&uuml;tzen, steigt die Praxistauglichkeit von IPv6 rapide. Daher ist die Besch&auml;ftigung mit diesem "Next Generation Network" (IPv6 und IPsec) ein wichtiger Bestandteil der Informatik-Ausbildung. Im Rahmen des WPF werden die Besonderheiten der Version 6 des Protokolls IP (IPv6) herausgearbeitet und speziell die Verwendung von IPv6 mit dem Sicherheitsprotokoll IPsec n&auml;her untersucht. Anhand eines im KTDS-Labor betriebenen Testnetzes k&ouml;nnen aktuelle Forschungsfragen bearbeitet werden. </p></div>
