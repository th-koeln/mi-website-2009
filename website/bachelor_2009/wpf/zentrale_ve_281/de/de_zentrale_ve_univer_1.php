<h1>Zentrale Verzeichnissysteme und Identity Management </h1>
<div class="zeile">
<h2 class="float einfach">Horst Stenzel<br />Thomas Kriener </h2>
<div class="float dreifach last">
<p>Das Thema Identity Management besch&auml;ftigt sich mit der Verwaltung von Identit&auml;ten innerhalb einer oder mehrerer Organisationen. Diese Identit&auml;ten sind meistens Mitarbeiter die Zugriff auf eine Vielzahl von Daten und Anwendungen bekommen sollen. Hierbei steht jede gr&ouml;&szlig;ere Organisation vor der Herausforderung die verschiedenen anwendungsbezogenen Benutzerkonten der eigentlichen Identit&auml;t, dem Mitarbeiter, zuzuordnen. Dies wird h&auml;ufig auch dadurch erschwert, dass die Benutzerkonten unterschiedlichen Konventionen entsprechen m&uuml;ssen, z.B. f&uuml;r die Email-Adresse <a class="mailto" href="mailto:Vorname.Nachname@firma.de">Vorname.Nachname@firma.de</a> oder a1234 f&uuml;r die Unix-Kennung. Durch internationale Regelungen wie SOX und Basel II hat dieses Thema aktuell einen sehr hohen Stellenwert in der Wirtschaft, ohne das es Standard-L&ouml;sungen f&uuml;r Unternehmen gibt. Die Produkte entwickeln sich gerade von statischen Regelwerken hin zu dynamischen genehmigungsbasierten Systemen, deren Komplexit&auml;t leicht untersch&auml;tzt wird. Innerhalb des WPF werden wir uns mit den aktuellen organisatorischen und technischen M&ouml;glichkeiten zum Thema Identity Management auseinander setzen und ein Konzept f&uuml;r den Einsatz innerhalb der Fachhochschule erarbeiten. Dabei nehmen wir uns das Thema eLearning als Beispiel und erarbeiten ein Konzept, wie auf der Basis der Verf&uuml;gbaren Datenbest&auml;nde ein Zugang zu einem solchen Angebot gesteuert werden kann. Konkrete Fragestellungen und Aufgaben dabei : . Welche Datenquellen k&ouml;nnen f&uuml;r die Identit&auml;ten verwendet werden ? . Durch welchen Ablauf kann der Zugriff vom Dateneigner freigegeben werden ? . Kann der Zugriff durch Eigenschaften der Identit&auml;t gesteuert werden ? . Wie kann eine effiziente Autorisierung vorgenommen werden? . Wie kann eine L&ouml;sung im Umfeld „Federated Identity" aussehen (zwischen verschiedenen Organisationen) ? . Wie wird der Zugang zeitnah entzogen ? Weiterf&uuml;hrende Projekte vorbereiten. . &Uuml;berblick &uuml;ber Identity Management-L&ouml;sungen im Hochschulbereich. Pr&uuml;fung Seminarvortr&auml;ge und schriftliche Ausarbeitung &uuml;ber Teilprojekte Zuk&uuml;nftig soll Shibboleth genutzt werden. Dies setzt ein funktionierendes IdM voraus. Daraus ergeben sich folgende Projekte: . Verzeichnissynchronisation zwischen bestehendem OpenLDAP und neuem Directrory . NIS-LDAP-Migration f&uuml;r die UNIX-Umgebung . Aufbau eines Shibboleth-Frontend f&uuml;r das DFN-AAI . Workflows f&uuml;r Zusatzberechtigungen und Kennungen die in K&ouml;ln nicht bekannt sind (Lehrbeautragte, Senioren etc.) . Sicherer Betrieb des Directories (Hochverf&uuml;gbarkeit, Konfiguration, ACLs, Schema etc.) In allen Projekten sind entsprechende Tools und Produkte auszuw&auml;hlen. Ausserdem sind die erforderlichen Attribute f&uuml;r das Schema des Directories zu benennen, sowie deren Herkunft zu bestimmen. </p></div>
