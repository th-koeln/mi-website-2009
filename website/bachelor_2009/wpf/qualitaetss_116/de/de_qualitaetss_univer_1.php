<h1>Qualitaetssicherung </h1>
<div class="zeile">
<h2 class="float einfach">Juergen Knittel </h2>
<div class="float dreifach last">
<p>Wenngleich die Veranstaltung aus organisatorischen Gr&uuml;nden Qualit&auml;tssicherung genannt wurde, so ist der Aspekt der Sicherung doch von untergeordneter Bedeutung. Vielmehr werden in der Vorlesung die Themen: -Entwicklung und Aufbau von Qualit&auml;tsmanagementsystemen (ISO, EFQM, SPICE etc.), -Management der Qualit&auml;t im laufenden Betrieb (VQM, EFQM, BPM, CRM etc.) -Qualit&auml;t des Managements der Unternehmung (Managerial Skills, Riskmanagement, HRM etc.), -Qualit&auml;tsaspekte der Leistungserbringung in der virtuellen Welt des WWW (wie kommt ein Ochse durch eine 64 KB-Leitung?) bearbeitet. An Beispielen aus real existierenden Unternehmen verschiedener Branchen (Dienstleistung, Logistik, Elektronik) werden die negativen und positiven Wirkungen der Vorgehens- und Verhaltensweisen im Zusammenhang mit den zu bearbeitenden Qualit&auml;tsfragen auf die inneren und &auml;u&szlig;eren Strukturen des Unternehmens behandelt. Aus den Erkenntnissen werden jeweils die Bez&uuml;ge zu der basisbildenden Theorie hergestellt. Die H&ouml;rerInnen haben am Ende der Veranstaltung Kenntnis von: -den wichtigsten Qualit&auml;tsmanagementmodellen resp. &iquest;systemen, -den g&auml;ngigsten Methodologien des Qualit&auml;tsmanagements, -der Vielf&auml;ltigkeit der Qualit&auml;tsproblemkreise im betrieblichen Alltag, -erprobten Strategien zur L&ouml;sung von Qualit&auml;tsproblemen. Zur Erlangung einer Leistungsbeurteilung (Note) m&uuml;ssen die H&ouml;rerInnen vertiefende Studien durchf&uuml;hren. Es ist ein aus der betrieblichen Praxis entnommenes Qualit&auml;tsproblem mit Hilfe der erlernten Qualit&auml;tstheorie zu analysieren und eine m&ouml;gliche L&ouml;sungsstrategie zu erarbeiten. Das Beispiel kann von den Kandidaten selbst gew&auml;hlt oder vom Dozenten vorgegeben werden. In jedem Fall muss das Thema zur Erreichung der Pr&uuml;fungsrelevanz beim Dozenten angek&uuml;ndigt und von diesem genehmigt werden. Die Seminararbeit kann in dem auf das Vorlesungssemester folgenden Semester angefertigt werden. Sie muss sp&auml;testens zum Beginn des Pr&uuml;fungszeitraums des auf das Vorlesungssemester folgenden Semesters beim Dozenten abgegeben werden. Die Anmeldung zur Pr&uuml;fung und die Einhaltung der vom Pr&uuml;fungsamt vorgegebenen Regularien liegen in der Eigenverantwortung der Kandidaten. </p></div>
