<h1>Praxisprojekt </h1>
<div class="zeile">
<div class="float dreifach last">
<p>Pflichtfach<br />15 ECTS-P<br />6 SWS<br />jedes Semester </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Verantwortlich </h2>
<div class="float dreifach last">
<p><a title="Verweis auf Prof. Dr. Kristian Fischer." href="../../../../../website/mi_am_campu/prof/prof_dr_k_12/de/de_prof_dr_k_univer_1.php">Prof. Dr. Kristian Fischer</a> </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Ziele </h2>
<div class="float dreifach last">
<p>Die Studierenden sollen lernen, die Methoden und Techniken, die sie im Studium erlernt haben, in einem realit&auml;tsnahen Projekt weit gehend selbstst&auml;ndig anzuwenden.<br /><br />Anwendung der Modulinhalte des ersten bis f&uuml;nften Semesters anhand von realen Anforderungen. Dies kann entweder in einem Unternehmen oder in der Hochschule - dann eingebettet in Forschungsprojekte - erfolgen. </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrmethoden </h2>
<div class="float dreifach last">
<p>Begleitete Projektarbeit, Seminar: Die Studierenden werden in Gruppen von bis zu 10 Personen von Dozenten betreut. Es wird in der Regel w&ouml;chentlich ein die Projektarbeiten begleitendes Seminar durchgef&uuml;hrt, in dem die Studierenden &uuml;ber Ihre Projektarbeit berichten und in Form eines seminaristischen Unterrichts f&uuml;r die Projekterfolge als wichtig erachtete methodische oder technische Inhalte vermittelt werden. </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Vorraussetzungen </h2>
<div class="float dreifach last">
<p>alle Modulpr&uuml;fungen au&szlig;er Bachelorarbeit und Kolloquium bestanden </p></div><br style="clear:both" /></div>
