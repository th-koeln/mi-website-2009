<h1>Medieninformatik und Gesellschaft </h1>
<div class="zeile">
<div class="float dreifach last">
<p>Pflichtfach<br />5 ECTS-P<br />4 SWS<br />j&auml;hrlich angeboten </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Verantwortlich </h2>
<div class="float dreifach last">
<p><a title="Verweis auf Prof. Dr. Mario Winter." href="../../../../../website/mi_am_campu/prof/prof_dr_m_18/de/de_prof_dr_m_univer_1.php">Prof. Dr. Mario Winter</a>, <a title="Verweis auf Edwin Ferger." href="../../../../../website/mi_am_campu/lehrbea/edwin_ferge_22/de/de_edwin_ferge_univer_1.php">Edwin Ferger</a> </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Ziele </h2>
<div class="float dreifach last">
<p>Die Studierenden sollen <li>die Bedeutung der Kommunikation als Voraussetzung f&uuml;r das Entstehen von Gesellschaft erkennen und die wechselseitigen Einfl&uuml;sse von Medien und gesellschaftlichem Strukturwandel kennen</li><li>die verschiedenen Medien im Hinblick auf Struktur und Funktion sowie ihren Nutzen f&uuml;r die Gesellschaft analysieren und dabei zwischen Entstehungs-und Verwendungszusammenhang unterscheiden k&ouml;nnen</li><li>Netzwerke, die sich auf Informations-und Kommunikationstechnologien st&uuml;tzen, als zentrales Charakteristikum der Gesellschaftsstruktur im Informationszeitalter erkennen</li><li>die Stufen des e-Business analysieren und in Webauftritten f&uuml;hrender Anbieter in ihren Funktionen analysieren k&ouml;nnen</li><li>die Funktionen Neuer Medien in der Bildung erkennen k&ouml;nnen </li><br /></p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrinhalte </h2>
<div class="float dreifach last">
<p>Ausgangspunkt ist Struktur und Funktion der Medien f&uuml;r die jeweilige Gesellschaft von den H&ouml;hlenmalereien bis zum Internet. Daran schlie&szlig;t sich eine Einf&uuml;hrung in die strukturell-funktionale Analyse und das AGIL - Schema von Talcott Parsons an, um einen theoretischen Bezugsrahmen f&uuml;r die Analyse von gesellschaftlichem Wandel zu bekommen.<br /><br />Nach einer historischen Kritik an der Einsch&auml;tzung der Informatik (Karl Steinbuch) wird auf die Wissensgesellschaft und Medien des Wissens, die Nachrichtenwerttheorie und die Wirksamkeit Neuer Medien eingegangen.<br /><br />Als moderne Konkretionen der Mediennutzung werden e-Business und Proactive Web design behandelt.<br /><br />Zwei Exkurse illustrieren neue Handlungsmuster: "Computerethik - Praktische Ethik des "Informationszeitalters"? und "Defensive Selbstdarstellung - Der Ablauf politischer Skandale" (nach Astrid Sch&uuml;tz).<br /><br />Abschlie&szlig;end werden die Bausteine einer Theorie der Netzwerkgesellschaft (M. Castells) er&ouml;rtert. </p></div><br style="clear:both" /></div>
