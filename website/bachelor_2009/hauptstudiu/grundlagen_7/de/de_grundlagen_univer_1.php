<div class="zeile">
<h2 class="float einfach">Ziele </h2>
<div class="float dreifach last">
<p>Die visuelle Gestaltung von Medien wird im Wesentlichen durch die individuelle gestalterische Kreativit&auml;t bestimmt. Diese wird in der Veranstaltung durch Entwurfs&uuml;bungen entwickelt und gefestigt. Die theoretischen Hintergr&uuml;nde, sowie vorbildhafte Beispiele und Inspirationen werden in Impulsreferaten vermittelt und vorgestellt. </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrinhalte </h2>
<div class="float dreifach last">
<ul><li>Grundlagen der visuellen Wahrnehmung </li><li>Punkt, Linie, Fl&auml;che, Form</li><li>Figur-Grund</li><li>Farbe, Helligkeit, Struktur, Textur, Kontraste</li><li>Typographie, Satz</li><li>Layout, Gestaltungsraster</li><li>Prototypen, Designmodelle, Varianten </li></ul>Die Aufgaben werden in der Vorlesung/Praktikum bearbeitet und personenindividuell betreut und korrigiert. </div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrmethoden </h2>
<div class="float dreifach last">
<p>Vorlesung mit eingebetteten &Uuml;bungselementen. Begleitendes Praktikum. </p></div><br style="clear:both" /></div>
<div class="zeile">
