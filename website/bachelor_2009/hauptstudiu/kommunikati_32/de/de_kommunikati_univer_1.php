<h1>Kommunikationstechnik </h1>
<div class="zeile">
<div class="float dreifach last">
<p>Pflichtfach<br />5 ECTS-P<br />4 SWS<br />j&auml;hrlich angeboten </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Verantwortlich </h2>
<div class="float dreifach last">
<p>Prof. Dr. Ludwig Stahl </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Ziele </h2>
<div class="float dreifach last">
<p>Die Studierenden sollen: <li>Prinzipien und Grundlagen von Kommunikationstechnik kennen lernen und Protokolle als wesentliche Grundlage der KT im Detail verstehen (wichtigste Internet-Protokolle TCP/IP, wichtigste Multimedia-Protokolle (H.323 u. a.), wichtigste TK-Protokolle (ISDN))</li><li>Strukturen von Netzen verstehen (Computernetze (Rechnernetze, Datennetze), Telekommunikationsnetze (TK-Netze, Telefonie-Netze) </li><li>Dienste-Begriff verstehen: bei Protokollen, als Anwendungsdienste, anhand konkreter Beispiele Anwenden einfacher Netzanalyse-Werkzeuge und Methoden<br /> <br />Die Studierenden sollen in der Lage sein, die wichtigsten Anforderungen und Randbedingungen der betrachteten Netze zu beurteilen, Einsatzm&ouml;glichkeiten zu erkennen und zu bewerten und die betrachteten Technologien zielgerichtet einzusetzen. </li><br /></p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrinhalte </h2>
<div class="float dreifach last">
<p>Grundbegriffe und Grundlagen, Kommunikationssysteme (Modelle, Grundbegriffe), Protokolle, Schnittstellen, Dienste, OSI-Referenzmodell, Standardisierung, Die TCP-/IP-Familie als Grundlage des weltweiten Internet, TCP/IP-Protokollfamilie, Schichtenmodell und Protokolle im Detail, Klassifizierung von Netzen, Topologien, Technologien Wegewahl / Vermittlung / Routing, Routing-Verfahren und Protokolle, Telekommunikationsnetze, besonders ISDN, Multimedia-Netze, Dienstg&uuml;te, Internet-Telefonie, Realisierung von Multimedia-Netzen </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrmethoden </h2>
<div class="float dreifach last">
<p>Vorlesung, Praktikum </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Vorraussetzungen </h2>
<div class="float dreifach last">
<p>Grundkenntnisse aus den ersten beiden Semestern des Studiums </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Ort, Ressourcen </h2>
<div class="float dreifach last">
<p>Labor f&uuml;r Kommunikationstechnik und Datensicherheit; Netzsoftware (ping, traceroute, netstat, nslookup) E-Mail-Server und Clients </p></div><br style="clear:both" /></div>
