<h1>Softwaretechnik I </h1>
<div class="zeile">
<div class="float dreifach last">
<p>Pflichtfach<br />5 ECTS-P<br />4 SWS<br />j&auml;hrlich angeboten </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Verantwortlich </h2>
<div class="float dreifach last">
<p><a title="Verweis auf Prof. Dr. Mario Winter." href="../../../../../website/mi_am_campu/prof/prof_dr_m_18/de/de_prof_dr_m_univer_1.php">Prof. Dr. Mario Winter</a> </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Ziele </h2>
<div class="float dreifach last">
<p>&Uuml;berblick &uuml;ber das Gebiet Softwaretechnik. Kenntnis von Methoden, Techniken und Werkzeugen der objektorientierten Softwareentwicklung. Dar&uuml;ber hinaus sollen die Teilnehmenden die vorgestellten Methoden, Techniken zueinander in Bezug setzen. und in kleineren Projekten einsetzen k&ouml;nnen.<br /><br />Die Vorlesung gibt zun&auml;chst einen &Uuml;berblick &uuml;ber das Gesamtgebiet Softwaretechnik und behandelt dann ausschlie&szlig;lich grundlegende "Informatikaspekte" der objektorientierten Softwareentwicklung. Als wesentliche Grundlage werden die wichtigsten Elemente der Unified Modelling Language (UML) f&uuml;r die strukturelle Modellierung sowie die Funktions-, und Verhaltensmodellierung vorgestellt und anhand kleinerer Beispiele erl&auml;utert. Danach werden typische Aktivit&auml;ten der Anforderungsermittlung, der Analyse und des Entwurfs besprochen, wobei die UML als Modellierungssprache benutzt wird. Im Praktikum werden die Anwendung der Modellierungselemente und die Durchf&uuml;hrung der Aktivit&auml;ten in Gruppenarbeit vertieft. </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrinhalte </h2>
<div class="float dreifach last">
<p>Softwaretechnik im &Uuml;berblick; Strukturelle Modellierung (Objekt-und Klassendiagramme); Funktionsmodellierung (Anwendungsfalldiagramme); Verhaltensmodellierung (Sequenzdiagramme, Kollaborationsdiagramme, Zustandsdiagramme).Vorgehensweisen und Prototyping. Anforderungsermittlung; Analyse und Architektur; Entwurfskonzepte und Grobentwurf; Feinentwurf; Qualit&auml;tssicherung. </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrmethoden </h2>
<div class="float dreifach last">
<p>Vorlesung, &Uuml;bung/Praktikum </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Vorraussetzungen </h2>
<div class="float dreifach last">
<p>Grundkenntnisse Algorithmen und (objektorientierte) Programmierung </p></div><br style="clear:both" /></div>
