<h1>Multimedia Anwendungen </h1>
<div class="zeile">
<div class="float dreifach last">
<p>Pflichtfach<br />5 ECTS-P<br />4 SWS<br />j&auml;hrlich angeboten </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Verantwortlich </h2>
<div class="float dreifach last">
<p><a title="Verweis auf Prof. Dr. Kristian Fischer." href="../../../../../website/mi_am_campu/prof/prof_dr_k_12/de/de_prof_dr_k_univer_1.php">Prof. Dr. Kristian Fischer</a> </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Ziele </h2>
<div class="float dreifach last">
<p>Die Studierenden sollen <li>Protokolle f&uuml;r multimediale Anwendungen wie streaming Protokolle, Messaging Protokolle sowie RPC und RMI Protokolle kennen und ihre Eigenschaften erkl&auml;ren k&ouml;nnen,</li><li>Architekturen f&uuml;r verteilte multimediale Anwendungen wie CORBA, J2EE und Web Services kennen und erkl&auml;ren k&ouml;nnen</li><li>des Themas recherchieren und in schriftlicher und m&uuml;ndlicher Form wiedergeben k&ouml;nnen. </li><br /></p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrinhalte </h2>
<div class="float dreifach last">
<p>In der Vorlesung werden zun&auml;chst wesentliche Aspekte verteilter Systeme mit besonderer Ber&uuml;cksichtigung multimedialer Anwendungen dargestellt. Als Beispiel wird besonders auf RMI, CORBA und JMS eingegangen. Die Besonderheiten von streaming Protokollen werden am Beispiel von RTP, RSVP und RSCP erl&auml;utert. <br />Die verschiedenen Ans&auml;tze f&uuml;r die Anwendungsintegration basierend auf Web Technologien werden diskutiert und es wird n&auml;her auf J2EE eingegangen. Die Entwicklung von Web Services auf Basis der eingef&uuml;hrten Technologien wird an einem Beispiel dargestellt. </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrmethoden </h2>
<div class="float dreifach last">
<p>Vorlesung, &Uuml;bung </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Vorraussetzungen </h2>
<div class="float dreifach last">
<p>Datenbanken I, Softwaretechnik I, Grundlagen von Multimedia Systemen, Grundkenntnisse der objektorientierten Programmierung </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Ort, Ressourcen </h2>
<div class="float dreifach last">
<p>MACROMEDIA DIRECTOR und FLASH, ADOBE PHOTOSHOP, Final Cut PRO, Emagic Logic Audio Gold, Soundforge, Corel XmetaL, Apache, Tomcat, J2EE 1.4, MySQL, Eclipse </p></div><br style="clear:both" /></div>
