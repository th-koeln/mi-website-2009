<h1>Datenbanken </h1>
<div class="zeile">
<div class="float dreifach last">
<p>Pflichtfach<br />10 ECTS-P<br />8 SWS<br />j&auml;hrlich angeboten </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Verantwortlich </h2>
<div class="float dreifach last">
<p><a href="../../../../../website/mi_am_campu/prof/prof_dr_f_9/de/de_prof_dr_f_univer_1.php">Prof. Dr. Faeskorn-Woyke</a>, Prof. Dr. Birgit Bertelsmeier </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Ziele </h2>
<div class="float dreifach last">
<p>Die Studierenden sollen <li>&uuml;ber ein einheitliches konsistentes Begriffsgeb&auml;ude bez&uuml;glich der Datenbankthematik verf&uuml;gen</li><li>die theoretischen Grundlagen von Datenbanksystemen am Beispiel relationaler und objekt-relationaler Datenbanksysteme verstanden haben</li><li>in der Lage sein, diese Erkenntnisse im Rahmen der Modellierung und Implementierung von Datenbankschemata praktisch anzuwenden</li><li>sowie auch komplexere Datenbankanfragen und Datendefinitionen und Daten&auml;nderungen &uuml;ber SQL programmieren zu k&ouml;nnen</li><li>mit dem Transaktionsbegriff in Datenbanken und Konzepten der Datensicherheit bei Datenbanken vertraut sein</li><li>Grundlagen und Methoden der Datenbankanwendungsprogrammierung verstanden haben</li><li>Wissen, was man unter aktiven Datenbanken versteht und welche Zwecke mit diesen Konzepten verfolgt werden </li><li>In der Lage sein, Datenbanktrigger und aktive Datenbankkomponenten in PL/SQL zu programmieren</li><li>Eine Datenbankanbindung mittels JDBC verstehen und programmieren k&ouml;nnen </li><li>Gespeicherte Datenbankprozeduren unter PL/SQL oder SQLJ erstellen k&ouml;nnen	</li><li>Unterschiede zwischen relationalen Datenbanken und objektorientierten Datenbanken verstanden haben</li><li>Konzepte der objektrelationalen Datenbanken kennen</li><li>Objektrelationale Erweiterungen verstehen und anwenden k&ouml;nnen	</li><li>Mit den Speicherstrukturen , die bei Datenbanken verwendet werden, grunds&auml;tzlich vertraut sein </li><br /></p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrinhalte </h2>
<div class="float dreifach last">
<p>Grundlagen: hierarchische und Netzwerk-Datenbanksystemen<br /><br />ANSI-3-Ebenen-Modell<br /><br />Grundlagen des relationalen Modells: Relationale Algebra, Anfrageoptimierung, Normalisierung<br /><br />Datenmodellierung (Entity Relationship Modell) und Implementierung am Beispiel eines relationalen Datenbanksystems<br /><br />Datenbanksprache SQL: DDL, (auch Multi-Media-Objekte in der Datenbank ), DML, DAL, Constraints<br /><br />Transaktionskonzepte, Datensicherheit<br /><br />Datenbankanwendungsprogrammierung <li>Einf&uuml;hrung in die Datenbankanwendungsprogrammierung </li><li>Die Datenbanksprache PL/SQL</li><li>Datenbanktrigger und Aktive Datenbanken 	</li><li>JDBC: ein Standard Zur Verbindung Von SQL-Datenbanken und JAVA </li><li>SQLJ und Java Stored Procedures </li></ul>Einf&uuml;hrung in objektorientierte Datenbanken<br /> <br />Objektrelationale Datenbanken<br /><br />Physische Speicherstrukturen bei Datenbanken </p></div><br style="clear:both" /></div>
