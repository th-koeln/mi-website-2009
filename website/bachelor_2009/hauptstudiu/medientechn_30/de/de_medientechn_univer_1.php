<h1>Medientechnik und -produktion </h1>
<div class="zeile">
<div class="float dreifach last">
<p>Pflichtfach<br />10 ECTS-P<br />8 SWS<br />j&auml;hrlich angeboten </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Verantwortlich </h2>
<div class="float dreifach last">
<p><a title="Verweis auf Prof. Hans Kornacher." href="../../../../../website/mi_am_campu/prof/prof_hans_11/de/de_prof_hans_univer_1.php">Prof. Hans Kornacher</a> </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Ziele </h2>
<div class="float dreifach last">
<p>Die Studierenden sollen die wesentlichen Technologien von audiovisuellen Medien kennen und bzgl. ihrer prozess- und qualit&auml;tsrelevanten Einbettung in Medienproduktionszusammenh&auml;ngen anwenden und kritisch bewerten k&ouml;nnen. In zwei eigenst&auml;ndigen Video-Produktionen soll das Wissen fundiert werden. </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrinhalte </h2>
<div class="float dreifach last">
<p>Zun&auml;chst werden grundlegende Eigenschaften analoger Videosignale vorgestellt, anschlie&szlig;end werden die wesentlichen analogen Fernsehnormen dargestellt. Darauf aufbauend werden digitale Video- und Audio-Signale diskutiert (z.B. SNR). Nach einem Einschug bzgl. W&auml;rmestrahlung und Lichtemission(Farbtemperatur etc.) werden die Funktionsprinzipien und Bauformen elektronischer Bildwandler vorgestellt. Die optischen und elektronischen Systeme sowie Automatik- und bildstabilisierende Funktionen von Camcordern werden besprochen. Ein &Uuml;berblick &uuml;ber Farbmetrik schlie&szlig;t den ersten Teil ab.<br /><br />Im zweiten Teil werden Medienproduktionsaspekte vermittelt; ausgehend von Drehbuch werden &uuml;ber Storyboard, Mis en Sc&egrave;ne, Postproduktion, Authoring und Finishing die wesentlichen Arbeitsschritte einer Bewegtbild-Produktion dargestellt und kritisch diskutiert. Montagetechniken und Gestaltungsaspekte werden vorgestellt.<br /><br />In einem dritten Teil stehen wieder medientechnische Inhalte im Fokus. Zun&auml;chst werden magnetische Speicher in ihren physikalischen Auspr&auml;gungen vorgestellt und verschiedene Technologien der Speicherung zun&auml;chst analoger Signale und anschlie&szlig;end digitaler Signale vorgestellt und vergleichend diskutiert. Die Diskussion magneto-optischer Speicher leiten dann zu optischen Speichern &uuml;ber (CD, DVD), verschiedene Quellen- und Kanalcodierungen werden vorgestellt. In einem weiteren Themenkomplex werden die verschiedenen elektronischen Bildwiedergabesysteme dargestellt und kritisch diskutiert. </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrmethoden </h2>
<div class="float dreifach last">
<p>Vorlesung, Einweisungen (z.B. Handhabung Camcorder, Lichtsetzen, Nonlineare Schnittsysteme), Teamarbeit, Seminar (Diskussion von Storyboard-Entw&uuml;rfen und Rohschnitten), Projekt (je ein dokumentarisches und narratives Filmprojekt) </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Vorraussetzungen </h2>
<div class="float dreifach last">
<p>Mathematisch/analytische F&auml;higkeiten, Bef&auml;higung zur Teamarbeit </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Ort, Ressourcen </h2>
<div class="float dreifach last">
<p>HD-Camcorder, Lichtkoffer, Audioequipment<br />Schnittsysteme (Final Cut, After Effects, Shake, Cinema 4D) </p></div><br style="clear:both" /></div>
<div class="zeile">
