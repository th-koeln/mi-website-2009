<h1>Querschnittsqualifikationen </h1>
<div class="zeile">
<div class="float dreifach last">
<p>Pflichtfach<br />5 ECTS-P<br />4 SWS<br />j&auml;hrlich angeboten </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Verantwortlich </h2>
<div class="float dreifach last">
<p>Prof. Dr. Kristian Fischer </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Ziele </h2>
<div class="float dreifach last">
<p>Es werden Veranstaltungen aus den Bereichen Pr&auml;sentation, Moderation, Teamarbeit angeboten. In dem ausgew&auml;hlten Bereich sollen die Studierenden Grundlagenwissen erwerben und praktische Erfahrungen erlangen. Zus&auml;tzlich wird begleitend durch die Studierenden ein Projekt durchgef&uuml;hrt.<br /><br />Sehr fr&uuml;hzeitig sollen den Studierenden Kreativit&auml;t, Vorgehensweisen und Erfahrungen in realen Projekten, Teamarbeit und &Uuml;bernahme von Verantwortung vermittelt werden. Dieses Modul ist im Gesamtzusammenhang der Vermittlung von Querschnittsqualifikationen sowie zur Vorbereitung auf die F&auml;cher Projektmanagement und (Wahlpflicht) Projekt im Hauptstudium zu sehen. </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrinhalte </h2>
<div class="float dreifach last">
<p>Die Lehrinhalte werden f&uuml;r den Bereich des Projekts nicht von au&szlig;en vorgeben, sondern m&uuml;ssen fallspezifisch von den Studierenden in Abstimmung mit dem Mentor selbst definiert und erarbeitet werden. Die Projektthemen k&ouml;nnen von den Studierenden sowie internen und externen Auftraggebern benannt werden; sie m&uuml;ssen im weiteren Sinne einen akademischen Bezug aufweisen. Im Vorlesungs-bzw. Seminarteil richtet sich der Inhalt nach dem gew&auml;hlten Thema. </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrmethoden </h2>
<div class="float dreifach last">
<p>Vorlesung, Seminar, Praxisorientierte Projektarbeit unter Verantwortung der Studierenden mit Coaching durch den Mentor </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Vorraussetzungen </h2>
<div class="float dreifach last">
<p>Es werden keine &uuml;ber die allgemeinen Zulassungsvoraussetzungen hinausgehenden fachlichen Voraussetzungen gefordert, jedoch sollten Engagement, Motivation, Toleranz, Bereitschaft zur Teamarbeit mitgebracht werden. </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Ort, Ressourcen </h2>
<div class="float dreifach last">
<p>Es stehen nach Absprache alle nutzbaren Infrastrukturkomponenten der Hochschule zur Verf&uuml;gung. Bei Finanzierungsbedarf bem&uuml;ht sich das Institut um entsprechende Budgets. <br />Personell werden die Projekte durch Mentoren aus dem Lehrk&ouml;rper begleitet, die bei der Projektdefinition, bei der Projektabnahme und bei eventueller Krisenintervention t&auml;tig werden. Es wird bewusst auf zu gro&szlig;e Einwirkung der Hochschule auf die Projektgestaltung verzichtet. </p></div><br style="clear:both" /></div>
