<div class="subzeile">
<h2 class="float einfach">Grundlagen der visuellen Kommunikation<br />Prof. Noss </h2>
<div class="float dreifach last">
<p>&Uuml;berp&uuml;nktlich sind wir dann zum Grundlagen der visuellen Komminkation Praktikum gegangen. Wir mussten noch die Aufgabe von letzter Vorlesung machen. Fest definierte Objekte sollen wir so anordnen, dass sich eine interessante und spannungsvolle Kompositions ergibt, diese auch bewerten und nat&uuml;rlich alles in Flash.<br />Als wir gerade richtig loslegen wollten, h&ouml;rten wir ein Gr&auml;usch, &auml;hnlich wie eine Kinderlaserpistole. Keiner wusste so recht etwas damit anzufangen, bis dem Tutor klar wurde : FEUERALARM!!!<br />Wir verlie&szlig;en das Geb&auml;ude und alle sammelten sich vor der FH. Die Feuerwehr kam gerade an, als sich in der FH ein beschlagenes Fenster &ouml;ffnete. Man munkelt, dass der Wasserkocher an all dem Schuld war, da es ein Fehlalarm war.<br /><br />Um 16 Uhr dann fand die Vorlesung statt. Es ging zun&auml;chst um Fachsprache f&uuml;r den Bereich des Inhaltes, z.B. bei B&uuml;cher. Um einige Beispiele zu nennen: Pagina bedeutet Seitenzahl, Footer meint die Fu&szlig;zeile etc.<br />Dannach schauten wir uns ziemlich viele Beispiele f&uuml;r Raster an. Prof. Noss hatte Raster &uuml;ber verschiedene Homepages gelegt und uns damit zeigen wollen, wie man ein Raster entwickelt.<br />Zum Schlu&szlig; gab es eine gro&szlig;e Miniaufgabe. Wir bekamen Bl&auml;tter auf denen vorgefertigte Raster drauf waren. Er nannte uns 4 Themen und Elemente die auf dem Raster enthalten sein sollten. Diese sollten wir nun anordnen, zu jedem Thema 2 Varianten. Diese sollen wir einscannen und auf unsere Homepage stellen. Die neue Aufgabe besteht darin, dies mit Flash zu realisieren. </p></div>
