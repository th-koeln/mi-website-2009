<h1>05. November 2009 </h1>
<div class="zeile">
<div class="float dreifach last">
<p>Morgen m&uuml;ssen wir das CGA Praktikum abgeben. Also trafen wir uns heute um 10 Uhr in der FH um auf den letzten Dr&uuml;cker flei&szlig;ig zu sein. Erstaunlicher Weise hatten wir heute kaum Probleme die Aufgabe zu beenden. Bisher scheint es sogar so als h&auml;tten wir zuviel gemacht. Angeblich sollten wir nur die Bilder erscheinen lassen, ohne Klickfunktion etc... Mal sehen was Prof. Stenzel morgen haben will. </p></div>
<div class="clear">&nbsp;<br /></div></div>
