<h1>16. November 2009 </h1>
<div class="zeile">
<div class="float dreifach last">
<p>Den Endspurt im <strong>KT-Praktikum</strong> begannen wir heute um <strong>08.45 Uhr</strong>. Die letzten Aufgaben waren einfacher als erwartet und so wurden wir schon um ca 10 Uhr fertig. Uns fehlt jetzt nur noch eine Aufgabe, da der ben&ouml;tigt Server offline war, aber dies wird eine Sache von 2min sein...<br /><br />Dannach wollten wir das <strong>Datenbankenpraktikum</strong> zu ende machen. Also flitzen wir in den <strong>WI-Pool</strong>. Aber... dort war es so laut, dass wir nur eine Aufgabe geschafft haben. Es war wirklich nicht auszuhalten. </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach">Medientechnik und Produktion<br />Prof. Kornacher </h2>
<div class="float dreifach last">
<p>Bis zum <strong>Praktikum</strong> um <strong>13 Uhr</strong> vertr&ouml;delten wir die Zeit. Im Praktikum selber hat uns <strong>Prof. Kornacher</strong> <strong>Storyboards</strong> gezeigt. Anders als beim Drehbuch gibt es hier keine festen Regeln. Hier hat man k&uuml;nstlerische Freiheit. Er zeigte uns also etliche Beispiele. Unter anderem sogar einige Storyboards von <strong>Alfred Hitchcock</strong>. F&uuml;r Hitchcock selber, waren Storyboards sehr wichtig. Er hatte immer schon zuerst den gesamten Film vor Augen. <br />Zum Schluss aber zeigte <strong>Prof. Kornacher</strong> uns ein Storyboard mit dem Filmausschnitt dazu, von einer <strong>Pro Sieben Produktion</strong> an der er selbst mitgearbeitet hat. Dies war ein Beispiel daf&uuml;r, dass ein Storyboard sehr ausschlaggebend sein kann, aber nicht muss!<br /><br />In der <strong>Vorlesung</strong> um <strong>16 Uhr</strong> ging es um <strong>Plasmabildschirme</strong>. Wir haben ihre Funktionsweise besprochen und die direkten Unterschiede zum LCD-Bildschirm zusammengetragen.<br />Am Ende ging es dann noch um Beamer. Hier gibt es 4 unterschiedliche Prinzipien. Eins davon kommt immer wieder gerne in der Klausur vor, genau wie die Unterschiede zwischen Plasma und LCD-Bildschirmen. </p></div>
