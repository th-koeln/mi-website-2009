<h2 class="float einfach">Computergraphik und Animation<br />Prof. Stenzel </h2>
<div class="float dreifach last">
<p><strong>Computergraphik und Animation</strong> fand um <strong>8:15 Uhr</strong> statt, allerdings kam es einigen von uns viel fr&uuml;her vor... Es ging haupts&auml;chlich um die <strong>Rasterung</strong> von grafischen Elementen. Um z.B. ein Rechteck zuf&uuml;llen, tastet man die Y-Achse von unten nach oben ab. Bei einem Polygon hingegen gibt es eine rekursive F&uuml;llfunktion. Hierzu gibt es Beispiele f&uuml;r uns im <strong>ILIAS</strong>. <br />Zum Schluss ging es noch ums <strong>Clippen</strong> von 2D Objekten. <br /><br />Die <strong>Praktikumsaufgabe</strong> die wir heute abgeben mussten, h&auml;tte ich eigentlich erst um <strong>16 Uhr</strong> abgeben sollen. Jedoch habe ich es erfolgreich geschafft mich in die Gruppe direkt nach der Vorlesung zu schleusen und durfte dann am Ende abgeben. Nat&uuml;rlich hat sich all der Aufwand der letzten Tage gelohnt und ich habe bestanden! </p></div>
