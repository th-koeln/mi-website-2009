<h1>16. Oktober 2009 </h1>
<div class="zeile">
<h2 class="float einfach">Computergrafik und Animation<br />Prof. Stenzel </h2>
<div class="float dreifach last">
<p>Um <strong>8:15</strong> begann heute der Freitag. Wieder ging es um die Einf&uuml;hrung in das Thema <strong>Computergrafik und Animation</strong>, allerdings wurden uns die Themen vorgestellt, die wir behandeln werden.<br />Dannach hatte ich 6 Freistunden...Juhu.. Motiviert, wie wir sind, wollten wir zun&auml;chst die Aufgaben f&uuml;r das <strong>Datenbankenpraktikum</strong> erledigen. Doch alle R&auml;ume in denen ERwin auf den PCs installiert sind, waren vollst&auml;ndig besetzt. Also wollten wir die Aufgabe f&uuml;r <strong>Grundlagen der visuellen Kommunikation</strong> machen. Doch der Raum war verschlossen, weit und breit niemand der ihn &ouml;ffnen konnte. Nach gut 40min hatten wir dann den Schl&uuml;ssel ergattern k&ouml;nnen und legen los... <br />Anschlie&szlig;end, als die R&auml;ume frei wurden in denen ERwin vorhanden ist, setzten wir uns an Datenbanken. <br />Als wir dann endlich die Zeit totgeschlagen hatten bis zum <strong>Computergrafik und Animations Praktikum</strong> waren wir mit 2 von 3 Aufgaben fertig. Ein recht guter Schnitt.<br />Im <strong>Praktikum</strong> dann mussten wir ein PDF durchlesen und die Aufgaben dazu machen. Es wurde gefordert, dass wir auf unserem Acc Eclipse einrichten und 3 Programme &ouml;ffnen.. Dann konnten wir gehen. Anstatt 3 Stunden dauerte dieses Praktikum heute nur 1 Stunde. <br />Alles in allem, war dieser Freitag sehr lang...Das Wocheende ist jetzt wirklich n&ouml;tig. </p></div>
