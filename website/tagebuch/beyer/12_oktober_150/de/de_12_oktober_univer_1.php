<h1>12. Oktober 2009 </h1>
<div class="zeile">
<h2 class="float einfach">Praktikum: Kommunikationstechnik<br />Prof. Stahl </h2>
<div class="float dreifach last">
<p>Wie an jedem Montagmorgen habe ich mich um viertel vor 6 aus dem Bett gequ&auml;lt, um p&uuml;nktlich um 7:45 zum <strong>Kommunikationstechnik Praktikum</strong> in der FH zu sein. Beim Praktikum dann, war es ziemlich leer. Mit mir waren 7 Studenten dort, von 47! Also haben wir noch ein wenig gewartet. Aber als nach einer halben Stunde immernoch keiner auftauchte, gab es ausser ratlosen Gesichtern der Praktikumsbeauftragen, nur eine sehr kleine Einf&uuml;hrung. Es hatte noch keine Vorlesung stattgefunden und der Professor selber war nicht anwesend. Also wurden wir entlassen.<br />Toll.<br />Was nun?<br />Eine Stunde von drei Prkatikastunden war rum. Dannach hatte ich noch 3 weitere Freistunden. Ich h&auml;tte ca. 6 Stunden l&auml;nger schlafen k&ouml;nnen.... </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="zeile">
<div class="subzeile">
<div class="float dreifach last">
<p>Aber dann fiel uns ein, dass wir ja schon eine Prakikumsaufgabe im Fach <strong>Grundlagen der visuellen Kommunikation</strong> bekommen hatten. Also setzten wir uns in den MiPool und machten unsere erste Abgabe in <strong>Flash</strong>. Leider ist uns <strong>Flash Cs4</strong> kurz bevor wir fetrig waren abgeschmiert. Von jetzt an, waren wir bewaffnet mit einen USB Stick und speicherten jeden einzelnen Schritt! <br />Und tada, wir waren fertig und hatte noch ein wenig Zeit zum <strong>Essen in der Mensa</strong>. </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<div class="clear">&nbsp;<br /></div></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach">Medientechnik und Produktion<br />Prof. Kornacher </h2>
<div class="float dreifach last">
<p>Endlich gab es dann um 13 Uhr ein weiteres <strong>Praktikum</strong>, dieses fand auch planm&auml;&szlig;ig statt. Es gab einen kleinen Vortrag welche Termien Pflicht sind und welche nicht. Im letzten Semester haben wir <strong>Dokumentarfilme</strong> gedreht. Leider waren nicht alle zum vorgeschriebenen Abgabetermin fertig. Diese schauten wir uns heute an. Einige waren wirklich lustig. Eine Gruppe z.B. hatte einen Selbstversuch gemacht. 2 Wochen ohne jegliche Drogen. Von vier schaffte es nur einer. Bitter.<br />Dannach ging die <strong>Vorlesung</strong> los.<br />Es ging haupts&auml;chlich um analoge und digitale Aufzeichnungen. <br />Und so endete um 18 Uhr ein laaaaaaaaaaaaaaaaaaanger Tag in Gummersbach. </p></div>
