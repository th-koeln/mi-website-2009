<h2 class="float einfach">Grundlagen der visullen Kommunikation<br />Prof. Noss </h2>
<div class="float dreifach last">
<p>Herrlich. Wir hatten beschlossen, diese Woche keinerlei Praktika zu erledigen. Also mussten wir erst um <strong>14 Uhr</strong> anwesend sein. <strong>Prof. Noss</strong> schaute sich im <strong>Praktikum</strong> heute unsere bisherigen Aufgaben an. Er gab kleine Tipps was er anders machen w&uuml;rde, jedoch keine genaue EInsch&auml;tzung. Leider. :(<br /><br />Um <strong>16 Uhr</strong> fand dann die Vorlesung statt. Die meiste Zeit ging es um Schriften. Man sollte unter 12px nie kursive Schriften verwenden, da diese nur schwer zu lesen sind. Wenn m&ouml;glich, sollte man ausserdem die Kantengl&auml;ttung benutzen. Ausserdem gibt es Schrfiten, die extra f&uuml;r eine Gr&ouml;&szlig;e gedacht sind. Schriften die man allerdingss skalieren kann sind proportionale Schriften.<br />Dann ging es noch um <strong>Raster</strong>. Bei Zeitungspapier und Siebdruck hat man 50-80 lpi (lines per inches), bei Kunstdruckpapier hingegen schon 130-180lpi.<br />Zum Schluss wurde die n&auml;chste Aufgabe f&uuml;r das Praktikum nochmal deutlich erkl&auml;rt, da es einige Unklarheiten gab. Wir sollen ein IPhone App entwerfen. zun&auml;chst nur als Raster. Es handelt sich um eine Gallerie in der Bilder und Texte angezeigt werden k&ouml;nnen. </p></div>
