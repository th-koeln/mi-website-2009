<h1>02. November 2009 </h1>
<div class="zeile">
<div class="float dreifach last">
<p>Um <strong>12 Uhr</strong> haben wir uns heute getroffen, um die erste praktische Aufgabe, in diesem Semester f&uuml;r <strong>MTP</strong> zu drehen. In 'Der Schneemann' geht es um einen Mann der in einem Caf&eacute; sitzt. Als er bezahlt hat und gehen will, nimmt ihn die Polizei mit. <br />Mit dem Storyboard finden wir an, da dies aber sehr aufwendig ist, haben wir lieber den Ort skizziert und Kamerabewegungen eingef&uuml;gt. So wussten wir, wo in etwa unsere Schauspieler stehen w&uuml;rden und wie die Kamera sich zu bewegen hat. Gesagt, getan. WIr fingen mit dem Drehen an und kamen beachtlich schnell vorran. Zwischenzeitlich schien es so, als w&uuml;rde uns das Wetter einen Strich durch die Rechnung machen, jedoch blieb es trocken, bis wir fertig waren. Nun fehlt nur noch der Schnitt. </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach">Medientechnik und Produktion<br />Prof. Kornacher </h2>
<div class="float dreifach last">
<p>Um <strong>16 Uhr</strong> fand die Vorlesung statt. Erstaunlicher Weise war es heute erschreckend leer. Das lag wohl daran, dass heute kein Pflichttermin im Praktikum war.<br />Jedenfalls haben wir uns sehr ausf&uuml;hrlich mit <strong>Mikrofonen</strong> besch&auml;ftigt. Welche <strong>unterschiedlichen Bauformen</strong> und welche <strong>Richtcharakteristiken</strong> gibt es? Bei den Bauformen unterscheidet man z.B. das dynamische Mikrofon und das Kondensatormirkofon voneinander. Das erste ist robuster gebaut und ben&ouml;tigt keine Stromversorgung, das zweite jedoch ist empfindlicher und die &Uuml;bertragungsqualit&auml;t ist besser. Bei den Richtcharakteristiken unterscheidet man z.B. die Kugel, die Acht, die Niere und die Superniere voneinander.<br />Ausserdem haben wir uns noch mit der <strong>Signalf&uuml;hrung</strong> besch&auml;ftigt. Im Profibereich z.B. hat man immer symmetrische Signale.<br />Zum Schluss haben wir noch die Einstellungen vom Mischpult besprochen, unter anderen den SIgnalrausch-Abstand. </p></div>
