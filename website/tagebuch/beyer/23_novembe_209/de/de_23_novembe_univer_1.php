<h2 class="float einfach">Medientechnik und Produktion<br />Prof. Kornacher </h2>
<div class="float dreifach last">
<p>Um <strong>16 Uhr</strong> war dann der Endspurt eingel&auml;utet. In der Vorlesung ging es heute um <strong>Lichttechnik und Beleuchtungssysteme</strong>. Leider muss ich zugeben, dass ich zu der Uhrzeit nicht mehr ganz auf der H&ouml;he war, zumal neben mir Datenbanken gemacht wurden. Aber wichtig schien die <strong>Beleuchtungsst&auml;rke</strong>. Diese nimmt mit dem Quadrat der Entfernung ab. Hinzukommend machte die <strong>Leuchtdichte</strong>, der <strong>Aufbau der Netzhaut</strong> und die <strong>Wellenl&auml;nge des Lichts</strong> einen wichtigen Eindruck.<br />Um <strong>17:45</strong> war die Vorlesung dann zuende und ich war schlie&szlig;lich um 19 Uhr zu Hause. <br />Super Start in die Woche, es folgen noch 3 Abgaben! </p></div>
