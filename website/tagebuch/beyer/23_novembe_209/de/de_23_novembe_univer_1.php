<h1>23. November 2009 </h1>
<div class="zeile">
<h2 class="float einfach">Kommunikationstechnik<br />Prof. Stahl </h2>
<div class="float dreifach last">
<p>Dieser Montag begann charakteristisch f&uuml;r einen Montag. Besonders typisch: Schlaflosigkeit. Nach 2 Stunden Schlaf trommelte mich gnadenlos mein Wecker um 05:45 wach. Das <strong>KT-Praktikum</strong> begann um 07:45. Heute war es soweit, die <strong>Abgabe</strong> stand an, inklusive <strong>Test</strong>. F&uuml;r den Test hatten wir 20 Minuten Zeit, diese brauchten wir jedoch nicht. Bei den Fragen handelete es sich um Themen, die in den Aufgaben behandelt wurden. Doch tats&auml;chlich gab es 3 Spezialisten, die ihn nicht bestanden haben und einen Nachholtermin machen m&uuml;ssen. W&auml;hrend wir den Test schrieben, korrigierte <strong>Herr Bujack</strong> die Ausarbeitungen. DIe Fehler wurden auf dem Deckblatt notiert. Doch zu unserer Freude stand bei uns: Gut! :D<br />Die Arbeit hat sich also gelohnt! </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="zeile">
<div class="subzeile">
<div class="float dreifach last">
<p>Im Anschluss setzten wir uns hoch erfreut &uuml;ber das Bestehen des KT-Praktikums in den WI-Pool um die letzten <strong>Datenbankaufgaben</strong> zu machen. Allerdings waren wir schon nach 10 Minuten fertig. Die Freude wich der M&uuml;digkeit.<br /><br />Nun hatten wir nur noch die Aufgaben f&uuml;r <strong>GdvK</strong> zu erledigen. Bis Donnerstag sollen alle bisherigen Aufgaben online sein. Uns fehlten noch 2 1/2. Wir werkelten bis <strong>16 Uhr</strong> mehr oder weniger erfolgreich an den Aufgaben. Den Rest werden wir morgen erledigen. </p></div>
<div class="clear">&nbsp;<br /></div></div>
