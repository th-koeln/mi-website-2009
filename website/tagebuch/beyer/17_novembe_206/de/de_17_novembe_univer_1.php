<h1>17. November 2009 </h1>
<div class="zeile">
<h2 class="float einfach">Kommunikationstechnik<br />Prof. Stahl </h2>
<div class="float dreifach last">
<p><strong>Prof. Stahl</strong> machte <strong>TCP/UDP</strong> und <strong>Ports</strong> zu seinen heutigen Themen. Ports lassen sich in 3 Gruppen einteilen. Zum einen gibt es die <strong>Well Known Ports</strong> von 0-1023, die <strong>Registered Ports</strong> von 1024-45151 und die <strong>Dynamic/Private Ports</strong> von 45152-65535. Bei der Adresszuweisung gibt es keine Regeln, nur Normen. Will man einige Dienste verstecken, so nimmt man einach einen anderen Port. Einige Standard-Ports sind z.B.: 80 = HTTP, 25=SMPT, 110=POP3. Auch diese Adresszuordnung war Teil der Praktikumsaufgabe, die wir jedoch schon fertig haben. </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach">Datenbanken<br />Prof. Bertelsmeier </h2>
<div class="float dreifach last">
<p>Die Datenbankenvorlesung fand heute ausnamsweise mit <strong>Prof. Bertelsmeier</strong> statt. Sie h&auml;lt die Datenbankvorlesungen sonst im Sommer. Doch da <strong>Prof. Faeskorn-Woyke</strong> heute verhindert war, vertrat Prof. Bertelsmeier. Zun&auml;chst haben wir die ganzen <strong>Join-Operationen</strong> wiederholt. Dazu machten wir viele ausf&uuml;hrliche Beispiele. Dann giing es langsam in Richtung <strong>SQL</strong>.<br /><br />In der <strong>&Uuml;bungstunde</strong> heute, die sonst am Mittwoch stattfindet, haben wir die <strong>Normalformen</strong> wiederholt. </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<div class="float dreifach">
<p>Im Anschluss der Vorlesungen hatten wir noch einen kleinen Termin bei <strong>Prof. Kornacher</strong>. Wir besprachen unsere Idee f&uuml;r den Spielfilm mit ihm. Er gab uns gute Tipps und Ratsschl&auml;ge die wir noch ber&uuml;cksichtigen werden m&uuml;ssen.<br /><br />Dannach erledigten wir weiter die <strong>Praktikumsaufgaben in Datenbanken</strong>. Allerdings standen wir etwas auf dem Schlauch, als wir <strong>Operatorb&auml;ume</strong> zeichnen sollten. Also sind wir zu <strong>Prof. Bertelsmeier</strong> gegangen und haben uns nochmal erkundigt, wie genau das funktioniert. Sie war sehr hilfsbereit und freundlich. Sie nahm sich 40 Minuten Zeit f&uuml;r uns und ging Schritt f&uuml;r Schritt unsere Fragen durch.<br />Neuen Mutes und Wissens gingen wir wieder an die Arbeit. Die Aufgaben fielen uns jetzt viel einfacher. Das Gespr&auml;ch mit ihr, hat wirklich sehr geholfen! </p></div>
