<h2 class="float einfach">Praktikum: Medientechnik und Produktion<br />Prof. Kornacher </h2>
<div class="float dreifach last">
<p>Um 13 Uhr fand wieder eine <strong>Pflichtveranstalung</strong> im <strong>Praktikum Medientechnik und Produktion</strong> statt. Da wir selber bald einen <strong>Spielfilm</strong> drehen sollen bekommen wir im Praktikum das Wissen dazu vermittelt. Heute ging es um die ersten Schritte zur Filmprofuktion, d.h. um das <strong>Expose</strong>, das <strong>Drehbuch</strong> und das <strong>Treatment</strong>. Beim Expose wird die Geschichte des Films einfach kurz und knapp erz&auml;hlt, mit Aufl&ouml;sung. Es geht darum seine Geschichte gut zu pr&auml;sentieren und nicht darum Spannung zu erzeugen. In einem Drehbuch m&uuml;ssen z.B. die Namen der Protagonisten stets gro&szlig; geschrieben und mittig platziert werden, damit sich der Dialog abhebt. EIn Treatment werden wir nicht schreiben. Hier wird die Geschichte ohne DIaloge geschrieben, mit Au&szlig;nahme von einem, der beispielhaft aufgeschrieben wird. Hier erkennt man dann, ob man f&uuml;r das Drehbuch evtl. Fachleute zum Dialoge schreiben ben&ouml;tigt!. Denn ein FIlm soll auch durch Dialoge spannend sein! </p></div>
