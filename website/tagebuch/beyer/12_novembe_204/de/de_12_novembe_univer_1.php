<h1>12. November 2009 </h1>
<div class="zeile">
<h2 class="float einfach">Grundlagen der visuellen Kommunikations<br />Prof. Noss </h2>
<div class="float dreifach last">
<p><strong>Praktikum</strong> hatten wir heute eigentlich erst um <strong>14 Uhr</strong>. Allerdings haben wir uns schon um <strong>13 Uhr</strong> an die Flasharbeiten gesetzt, da wir etwas zur&uuml;ck h&auml;ngen. Im Praktkum selber, hat uns der Tutor erkl&auml;rt und gezeigt wie man Seiten so animiert, dass es so aussieht, als bl&auml;ttere man sie um.<br /><br />In der <strong>Vorlesung</strong> um <strong>16 Uhr</strong> wurden wir sofot gefordert. Wir sollten aus 3 Elementen, 16 verschiedene <strong>Layouts scribbeln</strong>. Nach dem 9ten gingen mir jedoch langsam die Ideen aus... Weiter ging es dann mit den sogenannten <strong>Moodboards</strong>. Hierbei handelt es sich quasi um Collagen zu einem bestimmten Thema. <strong>Prof. Noss</strong> zeigte uns viele Beispiele dazu. <br />Zum Schluss ging es noch um <strong>Typographie</strong>. Dies l&auml;sst sich aufteilen in Typos= Muster, Gestalt und Graphein=Schreiben. Wir kamen &uuml;ber die Hieroglyphen bis hin zum Buchdruck zu dem Ergebnis, dass wir heute nich da w&auml;ren wo wir sind, g&auml;be es die Schrift nicht. Dank schriftlichen &Uuml;berlieferungen. Die ersten Alphabete stammen von 800 v. Christus, von den Griechen. Im Laufe der Zeit haben sich zich verschiedene Schriften entwickelt und jede hat ihren eigenen Charakter. Die Fachbegriffe haben wir nur kurz angerissen. Jedoch wei&szlig; ich jetzt, was ein 'Hurenkind' ist. Mir war vorher nicht klar, dass es sich hierbei um einen kurzen Wort/Satz/Text handelt, der auf der n&auml;chsten Seite weitergeht und endet. Allerdings haben wir auch W&ouml;rter wie <strong>Majuskel</strong> (Gro&szlig;buchstaben) und <strong>Minuskel</strong> (Kleinbuchstaben) gekl&auml;rt.<br />Ganz zum Schluss haben wir uns noch ein lustiges Video auf Youtube angesehen. Hier ging es um die verschiedenen Schriftcharakteren in Form einer Modenschau. </p></div>
