<h1>14. - 21. Dezember </h1>
<div class="zeile">
<div class="float dreifach last">
<p>Diese Woche gingen wir wieder etwas langsamer an.<br /><strong>Dienstag</strong>x trafen wir uns um die Aufgaben f&uuml;r das <strong>KT-Praktuikum</strong> zu erledigen. Allerdings waren wir nicht mehr sehr motiviert und kamen nur schleichend vorran.<br />Der Rest der Woche war sehr ruhig. <br />Doch am <strong>Freitag</strong> und <strong>Samstag</strong> traf sich unsere Gruppe wieder zum <strong>Drehen</strong> des Spielfilms f&uuml;r <strong>Medientechnik und Produktion</strong>. Freitag drehten wir bis <strong>19 Uhr </strong> und Samstag bis <strong>20 Uhr</strong>. Nun fehlt nur noch der Schnitt. Hierzu haben wir jedoch bis <strong>Februar</strong> Zeit. Es hat wirklich sehr viel Spa&szlig; gemacht den Film zu drehen. :D<br />Am <strong>Montag</strong> trafen wir uns dann erneut in der FH um KT weiter zu machen. <br />Ausserdem hatten wir <strong>2 Frauen</strong> der Gruppe die nette Aufgabe, das ganze Filmmaterial (Kamera, Licht, Ton, etc...) abzugeben.<br /><br />Aber jetzt ist erstmal <strong>Ruhe</strong>! <br />Weihnachten steht vor der T&uuml;r und dann gehts n&auml;chstes Jahr weiter. :D </p></div>
