<h1>09. November 2009 </h1>
<div class="zeile">
<div class="float dreifach last">
<p>Um <strong>9 Uhr</strong> haben wir uns heute getroffen um 2 Praktika zu erledigen. Wir haben mit <strong>Kommunikationstechnik</strong> angefangen. <br />Wir hatten heute eigentlich garkein Praktikum aber durften uns dazu setzen. Daran sa&szlig;en wir dann bis 11 Uhr und sind erstaunlich weit gekommen. Allerdings muss man dazu sagen, dass die Betreuer auch wirklich hilfsbereit waren und eininge Sachen veranschaulicht haben, wenn man Fragen hatte.<br /><br />Um <strong>11 Uhr</strong> haben wir uns dann an <strong>Datenbanken</strong> gesetzt, da wir das morgen abgeben m&uuml;ssen. Freitag waren wir geistig nicht mehr dazu in der Lage gewesen. Diese Aufgaben hatten es jedoch wirklich in sich. Wir waren bis 15 Uhr damit besch&auml;ftigt die Aufgaben zu l&ouml;sen, da wir einiges nicht ganz verstanden hatten, musste nat&uuml;rlich auch Zeit zum recherchieren investiert werden.<br /><br />Um <strong>15 Uhr</strong> hatten wir einen Besprechungstermin mit <strong>Prof. Kornacher</strong>. Die Dreharbeiten von letzter Woche mussten vorgestellt werden. Er kritisierte genau die Stellen, bei denen es in der Gruppe Auseinandersetzungen gab. Aber ich hatte es genauso gesehen wir Prof. Kornacher, von daher war das f&uuml;r mich in Ordnung. Wir wissen jetzt, worauf wir beim Filmdrehen achten m&uuml;ssen. </p></div>
<div class="clear">&nbsp;<br /></div></div>
