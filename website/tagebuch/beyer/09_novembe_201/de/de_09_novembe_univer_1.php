<h2 class="float einfach">Medientechnik und Produktion<br />Prof. Kornacher </h2>
<div class="float dreifach last">
<p>In der Vorlesung um <strong>16 Uhr</strong> war es wieder leer, wir waren nur ca. 10 Mann. Aber nat&uuml;rlich schaffen es trotzdem immer 2 Unruhestifter die ganze Zeit zu quasseln. So fiel mir das Zuh&ouml;ren selbst in der ersten Reihe, doch recht schwer. Aber die Vorlesung heute war, wie ich finde sehr interessant. Es ging um <strong>Bildwiedergabesysteme</strong>. Ausserdem hat er heute viele <strong>Klausurhinweise</strong> gegeben, z.b. wird in jeder Klausur ein Bildwiedergabesystem abgefragt. D.h. in jeder Klausur kommt entweder der Aufbau der Braunische R&ouml;hre, des Plasmabildschirms oder des LCD-Bildschirm vor. <br />Zum Schluss ging es noch um die <strong>Farbdarstellung</strong>. Hierzu erkl&auml;rte er uns das Prinzip des <strong>Liquid Crystal</strong>. </p></div>
