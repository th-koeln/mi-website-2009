<h2 class="float einfach">Medientechnik und Produktion<br />Prof. Kornacher </h2>
<div class="float dreifach last">
<p><strong>Willkommen im 3ten Semester! :D</strong><br /><br />Heute ging es recht gem&uuml;tlich los, denn die Vorlesung zu Medientechnik und Produktion fing erst um 16 Uhr an. Zur &Uuml;berraschung von Prof. Kornacher waren so ziemlich ALLE Studenten da. Mit so einem gro&szlig;en Ansturm hatte er nicht gerechnet. (Getreu nach: Neues Spiel, neues Gl&uuml;ck!) Er begann mit einer kleinen Einf&uuml;hrung zum dazugeh&ouml;rigen Praktikum. In diesem werden wir einen Spielfilm produzieren. In der Vorlesung selber, kn&uuml;pfte er dann bei den „Grundlagen der magnetischen Signalaufzeichnung" an. Dieses Thema hatten wir vor den Ferien angerissen, daher konnten wir gut mitarbeiten.<br />Haupts&auml;chlich ging es um die „Hysteresekurve". Er gab sich alle M&uuml;he es bildlich darzustellen. (Ich bin jedes Mal aufs Neue fasziniert, wie gerade er den Strich ziehen kann.) Die Hysteresekurve zeigt die maximale Magnetisierung, die Remanenz, die Koerzitivfeldst&auml;rke und die Neukurve. (Sie ist &uuml;brigens auch pr&uuml;fungsrelevant!)<br /><br />So ging auch schon der erste Tag in Gummersbach zu Ende und ich war schlie&szlig;lich um 20:15 zu Hause. </p></div>
