<h1>20. Oktober 2009 </h1>
<div class="zeile">
<h2 class="float einfach">Kommunikationstechnik<br />Prof. Stahl </h2>
<div class="float dreifach last">
<p>Um <strong>8:45</strong> begann heute die <strong>Vorlesung Kommunikationstechnik</strong> bei <strong>Prof. Stahl</strong>. Wir haben uns langsam an das Thema heran getastet. Zun&auml;chst haben wir uns &uuml;berlegt was Kommunikations eigentlich ist. Daraus haben wir dann das <strong>2-Ebenen-Kommunikationsmodell</strong> abgeleitet. Ausserdem haben wir Grundbegriffe definiert wie z.B.: Wort, Zeichen, Protokoll, Information, Nachricht, ... etc. <br />Hinzukommend haben wir uns dann mit der <strong>Geschichte der Kommunikationstechnik</strong> und mit der <strong>Entwicklung von Kommunikationsnetzen</strong> befasst . </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach">Datenbanken<br />Prof. Feaskorn-Woyke </h2>
<div class="float dreifach last">
<p>In der <strong>Vorlesung</strong> zu <strong>Datenbanken</strong> haben wir uns heute vorallem damit besch&auml;ftigt, was sich mit dem <strong>ER-Model nicht</strong> darstellen l&auml;sst. Ausserdem haben wir verschiedene <strong>Beziehungsarten</strong> untersucht und uns &uuml;berlegt wie wir an einen <strong>Subtypen</strong> oder einen <strong>Supertypen</strong> kommen. Im Anschlu&szlig; haben wir uns dann noch mit Attributen befasst, z.B. mit mehrwertigen und zusammengesetzten Attributen. Zu guter Letzt haben wir <strong>Schl&uuml;sselbegriffe</strong> untersucht, es gibt Fremdschl&uuml;ssel, Prim&auml;rschl&uuml;ssel, etc. .. </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<div class="float dreifach">
<p>Ab <strong>13 Uhr</strong> dann waren die Vorlesungen alle abgehakt und wir machten uns an die <strong>Praktikumsaufgaben</strong>. Am Freitag m&uuml;ssen wir <strong>CGA</strong> abgeben. Unser Start war recht kl&auml;glich. wir wussten zun&auml;chst nichts mit der Aufgabe anzufangen. DIe <strong>Aufgabe</strong> ist es, unseren Namen in <strong>Java 2 D</strong> so zu programmieren, dass er die Gestalt eines Tieres oder Menschens annnimmt. Wie soll das bitte gehen? Wir googlten und googlten.. doch es half alles nichts. WIr fragten <strong>Prof. Stenzel</strong> um Rat und er half uns auf die Spr&uuml;nge. Da wir so etwas noch nie gemacht hatten, brauchten auch kleine Erfolge Zeit. Um <strong>18 Uhr</strong> hatten wir dann ca. 1/3 des Praktikums geschafft und sind nach Hause gefahren. Am Donnerstag gehts weiter... </p></div>
