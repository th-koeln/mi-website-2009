<h1>07 - 12. Dezember 2009 </h1>
<div class="zeile">
<div class="float dreifach last">
<p>Diese Woche verlief wie sooft, stressig. <strong>Montag</strong> sa&szlig;en wir volle 7 Stunden an der <strong>Datenbankpraktikumsaufgabe</strong>. Es war die erste Aufgabe mit <strong>SQL</strong>. Da wir in der Vorlesung gerade erst damit angefangen haben, waren wir alle ein wenig &uuml;berfordert damit.<br /><strong>Dienstag</strong> bei der Abgabe hatten wir dann erfahren, dass unsere Aufgaben wohl doch nicht so falsch waren, wie wir bef&uuml;rchtet hatten. All das, was am Montag leider nicht funktionierte, lief am Dienstag einwandfrei. Klingt komisch, ist aber so. Somit hatten wir keine Probleme bei der Abgabe. Hinzukommend waren die Ergebnisse des <strong>Datenbankentests</strong> online und sehr erfreulich. :D<br />Am <strong>Donnerstag</strong> haben wir die alten <strong>Flashaufgaben</strong> &uuml;berarbeitet. Dies lief dann auf 4 Stunden 'Pixelschubsen' hinaus. <br />In der <strong>Vorlesung</strong> zu <strong>Grundlagen der visuellen Kommunikation</strong> ging es um Farben. Wie wirken Farben? WIe kann man Farben kombinieren? Wie erzeugt man eine optische T&auml;uschung durch Zusammenspiel verschiedener Farben?<br /><br /><strong>Samstag</strong> hatten wir unseren ersten <strong>Drehtag</strong> f&uuml;r den Spielfilm in <strong>Medientechnik und Produktion</strong>. Wir trafen uns um <strong>11 Uhr</strong> in Marienheide. Bis wir anfingen zu drehen vergingen 4 Stunden. Der Aufbau des Lichts, des Sets, etc erwies sich m&uuml;hsamer als gedacht. Zwischendrin machten wir eine kleine Essenspausen beim Pizzamann im Dorf. Die Pizza war lecker aber m&auml;chtig. Dannach drehten wir weiter bis <strong>22 Uhr</strong>. Wir hatten bis dahin eine Perspektive gedreht... Doch dann fiel jemanden auf, dass wir einen gro&szlig;en Filmfehler gemacht haben. Dank der Essenspause fehlte die Krawatte beim Schauspieler... Fatal. Alles neu drehen. Schlussendlich drehten wir dannn auch noch die 2te Perspektive und machten uns um kurz nach <strong>2 Uhr</strong> endlich auf den Heimweg! Zwischenzeitlich hatte es sogar geschneit...<br /><br />Dies war eine aufregende und abwechslunsgreiche Woche! </p></div>
