<h1>09. Oktober 2009 </h1>
<div class="zeile">
<h2 class="float einfach">Computer Grafik und Animation<br />Prof. Stenzel </h2>
<div class="float dreifach last">
<p>Letzter Tag der Woche! :D<br />Mein Wecker klingelte um 6 Uhr und ich machte mich h&ouml;chst motiviert fertig und auf den Weg nach Gummersbach. Um 8:15 fand dann eine weitere neue Vorlesung statt: <strong>Computer Grafik und Animation</strong>. Hier gab es nat&uuml;rlich auch eine kleine Einf&uuml;hrung. Wir werden mit Java 2D und 3D arbeiten, in 2er Teams. <strong>Prof. Stenzel</strong> hatte einige schlaue B&uuml;cher dabei, doch er verriet uns, dass die EInf&uuml;hrungen im Internet mitlerweile genau so gut seien. In den B&uuml;chern jedoch waren beeindruckende Bilder. W&auml;re wirklich toll, wenn wir so etwas nach dem <strong>Praktikum</strong> selber erschaffen k&ouml;nnten! Apropos Praktikum, leider wird er in der Vorlesung nicht darauf eingehen. Im ersten Moment fand ich das sehr merkw&uuml;rdig, aber ich warte erstmal das Praktikum n&auml;chste Woche ab.<br /><br />Dann begann er auch schon mit seiner <strong>Vorlesung</strong>. Heute war es nur oberfl&auml;chlich. Es ging um Grundlagen, z.B verdeutlichte er uns die Hierarchie des Grafik-Interface.<br />Um 10 Uhr war der Tag dann zu schon zu Ende, denn das Praktikum findet erstmals n&auml;chste Woche statt.<br />Also d&uuml;ste ich &uuml;ber die Autobahn und war schon um 11 Uhr zu Hause.<br /><strong>Sch&ouml;nes Wocheende allerseits! ;)</strong> </p></div>
