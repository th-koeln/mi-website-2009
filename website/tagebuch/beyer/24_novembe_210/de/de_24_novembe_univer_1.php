<h1>24. November 2009 </h1>
<div class="zeile">
<h2 class="float einfach">Kommunikationstechnik<br />Prof. Stahl </h2>
<div class="float dreifach last">
<p>Heute ging es nur um den Anwendungsdienst: <strong>DNS</strong>. Dies ist ein Verzeichnisdienst. Man sagta uch, es ist der wichtigste Basisdienst im Internet. Denn ohne ihn, w&uuml;rde das ganze Internet nicht funktionieren. Das <strong>D</strong>omain <strong>N</strong>ame <strong>S</strong>ystem l&ouml;st Namen in Ips auf. Es besteht in heutiger Form schon seit &uuml;ber 25 Jahren. Allerdings ist das Funktionieren an Bedingungen gekn&uuml;pft. Die Adressen m&uuml;ssen eindeutig sein! Seit ca 2005 wurde das DNS durch den <strong>IDN</strong> so erweitert, dass man auch <strong>Sonderzeichen</strong> benutzen kann. <br />Das DNS wird von <strong>13 Rootservern</strong> betrieben. Davon stehen 10 in den USA, 2 in Eurpoa und 1 in Japan. Allerdings hat jeder Rootserver mehrere Stellvertreter, falls einer mal ausf&auml;llt.<br /><br />F&uuml;r die Namen gibt es eine Baumstruktur.<br />Rootdomain <- Wurzel<br /><br />V<br />de   <-Top Leven Domain<br /><br />V<br />fh-koeln <- Second Leven Domain<br /><br />V<br />gm <-Subdomain<br /><br />V<br />www <- Ressource-Namen </p></div>
