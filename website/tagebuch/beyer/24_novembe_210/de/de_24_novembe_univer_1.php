<div class="float dreifach last">
<p>Langsam aber sicher geht es mit <strong>SQL</strong> richtig los. In SQL gibt es <strong>DDL</strong> und <strong>DAL</strong> Sprachen. <br />Wir haben einen &Uuml;berblick &uuml;ber die erlaubten <strong>ANSI-Datentypen</strong> erhalten. Es gibt z.B. INTEGER, FLOAT, DATE, TIME, CHARACTER, und NUMBER.<br />Ausserdem haben wir &uuml;ber <strong>Integrit&auml;tsbedinugungen</strong> gesprochen. In SQL lassen sich nur statische Integrit&auml;tsbedingen realisieren.<br /><br /><br />Im <strong>Praktikum</strong> war heute die Abgabe f&auml;llig. Da die Aufgaben teilweise komisch formuliert waren, hatten wir teilweise abweichende L&ouml;sungen. Bei den Operatorb&auml;umen z.B. hatten wir einiges falsch interpretiert. Aber im Gro&szlig;en und Ganzen war alles zufrieden stellend. </p></div>
