<h1>03. November 2009 </h1>
<div class="zeile">
<h2 class="float einfach">Kommunikationstechnik<br />Prof. Stahl </h2>
<div class="float dreifach last">
<p>In <strong>Kommunikationstechnik</strong> haben wir leider erst heute &uuml;ber Stoff geredet, den wir schon im <strong>Praktkum</strong> behandeln musste. Darunter fielen das Thema der <strong>&Uuml;bertragungsrate</strong> und der <strong>Antwortzeit</strong>, d.h. der Befehl <strong>ping</strong> wurde heute ausf&uuml;hrlich behandelt. Ausserdem ging es um <strong>IP-Adressen</strong> und darum, dass sie aus aus einem Host und einer Netzadresse bestehen. </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach">Datenbanken<br />Prof. Feaskorn-Woyke </h2>
<div class="float dreifach last">
<p>In der <strong>Vorlesung</strong> heute ging es eigentlich nur darum, wie man <strong>ER-Modelle</strong> auf ein <strong>Datenbankschema</strong> transformiert. Ausserdem haben wir ganz genau geguckt, was ist ein <strong>Datenmodell</strong> eigentlich? </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<div class="float dreifach">
<p>Um <strong>14 Uhr</strong> dann hatten wir <strong>Freizeit</strong>, diese mussten wir jedoch nutzen, mit den Praktika<br /> zu beginnen. Am <strong>Donnerstag</strong> muss die Aufgabe in <strong>GdvK</strong> online sein. Also suchten wir weiter im Internet nach Beispielen zu den <strong>Gestaltgesetzen</strong>. Anschlie&szlig;end fingen wir an eine <strong>Flashdatei</strong> zu erzeugen, mit diesen besagten Beispielen. Beim Klick auf den Button, soll das n&auml;chste Beispiel und Text dazu erscheinen. Der Text soll in Form einer <strong>MP3-Datei</strong> abrufbar sein..<br />Leider war das Beispiel von unserem Tutor, f&uuml;r die Soundeinbindung, ein klein wenig Buggy und wir hatten einige Probleme damit. <br />Um <strong>18 Uhr</strong> mussten wir dann das Feld r&auml;umen. DIeser Tag war wirklich nicht easy going und fertig sind wir leider immernoch nicht... </p></div>
