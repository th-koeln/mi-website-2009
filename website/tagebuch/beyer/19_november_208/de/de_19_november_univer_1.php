<h1>19.November 2009 </h1>
<div class="zeile">
<div class="float dreifach last">
<p>Der heutige Tag fing sehr chaotisch an. Zun&auml;chst hatten wir uns um <strong>12 Uhr</strong> zum <strong>GdvK-Praktikum</strong> erledigen getroffen. Allerdings fingen wir lieber ein Schw&auml;tzchen mit unserem Tutor Volker an. Eine Stunde sp&auml;ter, stellten wir fest, dass wir hiermit heute nicht weiter kommen werden. <br /><br />Also marschierten wir zum <strong>KT-Praktikumsraum</strong> und meisterten die uns noch fehlende Aufgabe. Da wir am Montag einen Test zu dieser Abgabe schreiben werden, lie&szlig;en wir <strong>Prof. Bongards</strong> &uuml;ber unsere PDF-Datei schauen. Er war sehr zufrieden damit und hoffte, dass wenigstens 1/3 der Ausarbeitungen so &auml;hnlich wie unsere seien. :)<br /><br />Nach einem weiteren Plausch, diesmal mit Prof. Bongards, setzten wr uns an die <strong>Datenbanken-Praktikumsaufgaben</strong>. Wir waren zwar so gut wie fertig, jedoch kontrollierten wir noch einiges, da noch etwas Zeit bis zur n&auml;chsten Vorlesung war. </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach">Grundlagen der visuellen Kommunikation<br />Prof. Noss </h2>
<div class="float dreifach last">
<p>Diese fand dann um <strong>16 Uhr</strong> statt. Heute ging es nur um <strong>Typographie</strong>. Wir wiederholten nocheinmal Fachw&ouml;rter und lernten neue kennen. Bei <strong>Ligaturen</strong> handelt es sich zum Beispiel um Buchstabenverbindungen. Ausserdem haben wir uns genauer angesehen, wie ein guter Text aussieht. Es gibt verschiedene <strong>Satzarten</strong>, z.B. Blocksatz, Rauhsatz, Formsatz, Flattersatz, symmetrischer Satz und freier Zeilenfall. Ein guter Blocksatz ist nur ein guter Blocksatz, insofern er als gleichm&auml;&szlig;ige Graufl&auml;che wirkt. Dies kann man &uuml;berpr&uuml;fen, indem die Augen etwas zusammen kniept. Hat ein Text wei&szlig;e Stellen, so wird dies als St&ouml;rung empfunden. Es f&auml;llt uns somit schwerer den Text zu lesen. So ist es auch beim Zeilumbruch. Stimmt der Abstand zwischen den Zeilen nicht, so f&auml;llt es uns schwer die n&auml;chste Zeile zu finden. Der ideale Zeilenabstand betr&auml;gt ca 120%~130% der Schriftgr&ouml;&szlig;e. <br />Ausserdem sollte der Abstand zwischen den Buchstaben, W&ouml;rtern etc. nach folgendem, logischen Prinzip aufgebaut sein:<br />Buchstabeabstand < W&ouml;rterabstand < Zeilenabstand < Spaltenabstand </p></div>
