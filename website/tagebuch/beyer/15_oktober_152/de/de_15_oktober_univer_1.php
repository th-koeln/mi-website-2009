<h2 class="float einfach">Grundlagen der visuellen Kommunikation<br />Prof. Noss </h2>
<div class="float dreifach last">
<p>Das <strong>Praktikum</strong> fand heute um 14 Uhr statt. Leider gab es noch keine neue Aufgabe. Die f&uuml;r heute hatte ich schon am Montag gemacht und sah deshalb keinen Sinn mehr zu bleiben und kam erst zur Vorlesung wieder.<br />W&auml;hrend der <strong>Vorlesung</strong> haben wir uns dann dem Thema <strong>Orientierung</strong> gewidmet. Um diese zu schaffen gibt es 3 Regeln: <strong>Reduzieren, Strukturieren</strong> und <strong>Hierarchisieren</strong>. Hier gilt: Unwichtiges weglassen, Muster bilden und Bez&uuml;ge herstellen! Um dies besser zu verstehen gab es viele Beispiele an verschiedenen Homepages. Die Frage ist jedes mal, wieviel Aufwand kostet es, diese Homepage warzunehmen? Um bessere Orientierung zu schaffen, m&uuml;ssen wir die <strong>3 Regeln</strong> beachten.<br />Anschlie&szlig;end haben wir uns einem neuen Gestaltunsgelemnt zu gewendet, der <strong>Linie</strong>. Hierzu gibt es auch eine <strong>Aufgabe</strong> f&uuml;r n&auml;chste Woche. Man soll verschiedene <strong>Rhyhtmen</strong> damit darstellen. Ich denke die Ergebnisse werden sehr spannend sein. </p></div>
