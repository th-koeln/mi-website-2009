<h1>10. November 2009 </h1>
<div class="zeile">
<h2 class="float einfach">Kommunikationstechnik<br />Prof. Stahl </h2>
<div class="float dreifach last">
<p>In Kommunikationstechnik ging es heut eum den <strong>Aufbau von IP- Datengrammen</strong>. Ausserdem um den <strong>Transport von Datengrammen</strong>. Hierzu werden die Pakete, falls sie zu gro&szlig; sind, fragmentiert. Dies kann &uuml;berall geschehen, die Reassemblierung jedoch, kann nur beim Empf&auml;nger geschehen.<br />Zum Schluss ging er noch auf einige <strong>Praktikumaufgaben</strong> ein. <strong>Ping</strong> und <strong>traceroute(bzw tracert)</strong> hat er ausf&uuml;hrlich erkl&auml;rt. Schade war nur, dass die meisten diese Aufgaben schon erledigt hatten. Mit dem Wissen aus der heutigen Vorlesung w&auml;re es sicher schneller und einfacher gewesen. </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach">Datenbanken<br />Prof. Feaskorn-Woyke </h2>
<div class="float dreifach last">
<p>Heute haben wir uns einem ziemlich wichtigen Thema gewidmet, den <strong>Join-Operationen</strong>. Hier l&auml;sst sich in Theta-Join, Equi-Join, Natural-Join, Verlustfreie Join-Operationen und Outer-Join unterteilen. <br /><br />Im <strong>Tutorium</strong> um <strong>13 Uhr</strong> wollten wir noch einige Fragen zum Praktikum stellen. Jedoch war es so voll, dass wir nicht dazu kamen.<br /><br />Im <strong>Praktikum</strong> um <strong>14 Uhr</strong> wurden unsere Aufgaben von <strong>Fr. Prof. Bertelsmeier</strong> abgenommen. Die Abnahme lief verunsichernd lange, aber wie sich herausstellte, war sie sehr zufrieden mit uns. Wir wurden gelobt und hatten bestanden.<br />Allerdings sind wir noch eine Stunde l&auml;nger geblieben um schon die n&auml;chsten Aufgaben anzufangen. Mit Aufgabe 1 sind wir komplett fetrig geworden. Bei Aufgabe 2 geht es um <strong>transitive Abh&auml;ngigkeiten</strong> Hier waren wir nicht 100% sicher und wollten uns erst schlau machen. </p></div>
