<h1>22. Oktober 2009 </h1>
<div class="zeile">
<div class="float dreifach last">
<p>Ich hatte heute zwar erst um <strong>14 Uhr Praktikum in Grundlagen der visuellen Kommunikation</strong>, aber ich war schon um 10 Uhr da. Da wir morgen die erste <strong>Aufgabe im CGA Praktikum</strong> abgeben m&uuml;ssen und wir noch nicht fertig waren, setzten wir uns heute erneut daran. Heute ging es wirklich gut vorran! Wir haben es heute fetrig bekommen und hatten sogar noch Zeit um mit dem <strong>Praktikum in Datenbanken</strong> weiter zu machen. Jedoch waren wir irgendwann so ausgelaugt, dass es beim Surfen endete. </p></div>
