<h2 class="float einfach">Grundlagen der visuellen Kommunikation<br />Prof. Noss </h2>
<div class="float dreifach last">
<p>Um <strong>14 Uhr</strong> fand dann das <strong>Praktikum zu GdvK</strong> statt. Hier haben wir die Aufgabe auch z&uuml;gig bearbeitet und hatten wieder etwas Zeit zum rumalbern.<br />Als dann aber um <strong>16 Uhr</strong> die <strong>Vorlesung</strong> los ging, waren wir wieder voll aufnahmef&auml;hig.<br />Es ging wieder um <strong>Wahrnehmung</strong>. Prof Noss zeigte uns ganz kurz Bilder und wir sollten aufschreiben was wir sehen, sp&auml;ter hat er die Zettel eingesammelt. Ich bin 'mal gespannt was das f&uuml;r eine Teststudie war. Ansonsten haben wir uns vorallem mit der Anordnung von Objekten besch&auml;ftigt. Wir haben auch gelernt, dass das Verh&auml;ltnis zwischen <strong>Positivraum und Negativraum</strong> ziemlich ausgewogen sein sollte! (Negativraum= ungenutzer Raum, Positivraum= genutzer Raum)<br />&Uuml;ber die verschiedenen Beispiele, die er uns zeigte, kam er dann zu den <strong>Gestaltgesetzen</strong>. Diese beziehen sich auf die Wirkung von Objekten. Sind z.B. einige Dinge n&auml;her beieinander als andere, so gruppieren wir sofort. Wir versuchen stets das einfachste in den Dingen zu sehen! </p></div>
