<h1>19. Oktober 2009 </h1>
<div class="zeile">
<h2 class="float einfach">Neu in der Medieninformatik </h2>
<div class="float dreifach last">
<p>Warum schimpfen eigentlich alle so sehr auf das Bachelor-Master-System? Mir kommt das nur zu Gute: Ich habe nach meinem Maschinenbau-Bachelor erst eine Zeit lang Physik auf Master studiert, um dann in Richtung Medieninformatik-Master umzuschwenken. Das w&auml;re mit dem guten alten Diplom nicht unbedingt m&ouml;glich gewesen.<br /><br />Nat&uuml;rlich bekommt man als Quereinsteiger Auflagen gestellt: Ich muss spezielle F&auml;cher im Umfang von 30 Credit-Points aus dem Bachelor-Studiengang Medieninformatik nachholen, was ich irgendwie versuche auf meine Regelstudienzeit zu verteilen. In diesem Semester geh&ouml;rt dazu auch das Fach <strong>Theoretische Informatik I</strong>, das die Bachelor schon im ersten Semester h&ouml;ren.<br /><br />Ansonsten sind die F&auml;cher im Master inhaltlich sehr breit aufgestellt. Hier ein &Uuml;berblick &uuml;ber die Veranstaltungen des Masters, die ich in diesem Semester besuchen werde:<br /><br /><strong>IT-Sicherheit</strong> (Herr Prof. Karsch)<br /><strong>Spezielle Gebiete der Mathematik</strong> (Herr Prof. Konen und Herr Prof. Stenzel)<br /><strong>Spezielle Gebiete der BWL</strong> (Frau Prof. Halfmann)<br /><strong>Medienrecht</strong> (Herr Wilde)<br /><strong>Ausgew&auml;hlte Anwendungen f&uuml;r multimediale Systeme</strong> (Herr Prof. Fischer)<br /><strong>Naturwissenschaftliche Grundlagen digitaler Medien</strong> (Herr Prof. Hartmann)<br /><strong>Interaction Design</strong> (Herr Prof. Hartmann)<br /><br />Im Einzelnen werde ich in den n&auml;chsten Wochen etwas zu den Veranstaltungen schreiben, wenn ich sie inhaltlich etwas besser kennen gelernt habe. Eines l&auml;sst sich bereits jetzt sagen: Es herrscht ein hoher seminaristischer Charakter im Master. In vielen F&auml;chern sind Ausarbeitungen in Form von Seminararbeiten, Referaten etc. anzufertigen. Teilweise freiwillig, teilweise als Kern des Faches. Auf jeden Fall ist viel zu tun. <br /></p></div>
