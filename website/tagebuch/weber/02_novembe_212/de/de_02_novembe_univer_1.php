<h1>02. November 2009 </h1>
<div class="zeile">
<h2 class="float einfach">Viel frei? Von wegen. </h2>
<div class="float dreifach last">
<p>Veranstaltungen in Gummersbach haben wir im Master nur an zwei Tagen pro Woche. In diesem Semester ist am Dienstag und Mittwoch. F&uuml;r diejenigen, die eher K&ouml;ln zugeneigt sind (und das sind die meisten), bedeutet das eine gro&szlig;e Erleichterung, da die 70-min&uuml;tige Zugfahrt K&ouml;ln-Gummersbach nicht mehr allzu oft angetreten werden muss. Der Bachelor Medieninformatik findet ja komplett in Gummersbach statt. <br /><br />Montags haben wir keine Veranstaltungen und gehen arbeiten oder schreiben an Seminararbeiten. Donnerstags und freitags sind wir in K&ouml;ln in der S&uuml;dstadt, Mainzer Stra&szlig;e 5, untergebracht - dort, wo der Fachbereich Sozialwissenschaften ans&auml;ssig ist. Ziemlich gegens&auml;tzlich aber nett. <br /><br />In den ersten Wochen des Semesters findet zus&auml;tzlich zum normalen Programm noch ein verpflichtender <strong>Mathe-Vorkurs</strong> statt. An mehreren Mittwoch-Terminen werden nochmal grundlegende Themen aus dem Bachelor wie Logik oder Statistik wiederholt, die f&uuml;r die Veranstaltung <strong>Spezielle Gebiete der Mathematik</strong> vonn&ouml;ten sein sollen. Die Veranstaltung l&auml;uft etwa bis Mitte November und ist dann abgeschlossen. Mittwochs bliebe dann nur noch die Veranstaltung <strong>Medienrecht</strong> bei Herrn Wilde, wenn sie nicht - Achtung, Freude! - ab Mitte November auf den Freitag in K&ouml;ln gelegt w&uuml;rde. Herr Wilde hat ja seine Kanzlei in K&ouml;ln, der d&uuml;rfte sich demnach auch nicht beklagen. Montags und demn&auml;chst auch mittwochs haben wir also frei, da f&auml;llt es schwer zu glauben, dass wir viel zu tun h&auml;tten. Haben wir aber. <br /><br />Ein Beispiel? Im Fach <strong>Naturwissenschaftliche Grundlagen Digitaler Medien</strong> hei&szlig;t es, dass eine Vor- und Nachbereitung der Veranstaltung von 10 Stunden pro Termin (jeden Freitag) angemessenes Minimum sei, um wirklich etwas von der Veranstaltung zu haben. Auch wenn manche das wahrscheinlich gerne leisten w&uuml;rden (ich find, das Fach ist ziemlich interessant), andere Dinge k&auml;men beinah zwangsl&auml;ufig zu kurz. Die ersten Meilensteine f&uuml;r die Seminararbeiten r&uuml;cken n&auml;her. Langsam f&auml;ngt man an zu rotieren&Ouml;<br /></p></div>
