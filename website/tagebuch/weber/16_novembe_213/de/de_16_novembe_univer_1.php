<h1>16. November 2009 </h1>
<div class="zeile">
<h2 class="float einfach">Referate, Seminare, Ausarbeitungen. </h2>
<div class="float dreifach last">
<p>Der erste Meilenstein liegt hinter uns. Im Fach <strong>IT-Sicherheit</strong> sollte das Inhaltsverzeichnis der Seminararbeit vorgezeigt werden. Keine schlechte Idee, fertigen doch viele das Inhaltsverzeichnis am liebsten zum Schluss an, wenn der &Uuml;berblick ohnehin da ist. Gerade aber bei solch umfassenden Themen wie "Digital Rights Management", "Biometrische Authentisierung" oder "Sicherheit webbasierter Anwendungen" wird ein fr&uuml;hes Feedback seitens der Studenten dankend entgegen genommen, damit die Ausarbeitung in die richtige Richtung geht.<br /><br />Daneben flie&szlig;t ein gro&szlig;er Teil des Studienaufwands derzeit in die Seminararbeit f&uuml;r das Fach <strong>Spezielle Gebiete der Mathematik</strong>. Hier konnte aus Themen wie z.B. "Geometrische Datenverarbeitung", "Video-Entfernungs-Messung" oder "Rekreative Mathematik, Back-Tracking" ausgew&auml;hlt werden. Die erste Version der Ausarbeitung muss relativ fr&uuml;h im Semester abgegeben werden, da sie anonymisiert an die anderen Studenten verschickt wird, die dann als Reviewer in Erscheinung treten. Man ist also Autor und Reviewer in einer Person. Mit den Reviews geht's dann in die n&auml;chste Runde, in der die Korrekturen eingebunden werden sollen. <br /><br />Auch in den F&auml;chern <strong>Spezielle Gebiete der BWL</strong> und <strong>Ausgew&auml;hlte Anwendungen f&uuml;r multimediale Systeme</strong> sollen Referate angefertigt und gehalten werden. Ich hab noch das Gl&uuml;ck, dass ich das Fach <strong>Projekt- und Informationsmanagement</strong> zugunsten der Auflagen auf n&auml;chstes Jahr geschoben habe - ich h&ouml;r nur immer von den anderen, dass denen da auch einiges abverlangt wird. Insgesamt muss aber festgehalten werden, dass durch die freie Themenwahl ein hohes Ma&szlig; an eigenem Interesse f&uuml;r die Thematiken besteht und gewiss auch ausreichend Motivation vorhanden ist. Nur die Zeit ist eben bei den meisten so die Sache... <br /></p></div>
