<p><span class="zweifach last abstand_einfach_links block">Ein wesentliches „neues" Medium unserer Zeit ist komplett digital: das Internet. Hier&uuml;ber kommunizieren wir via Chat, E-Mail, Twitter und mittlerweile auch per Audio/ Video-Konferenz. Wir informieren uns &uuml;ber Websites, Blogs und Portale. Wir h&ouml;ren Internetradio, schauen Videos im Web oder abonnieren RSS Feeds. <br /><br />Diese durchg&auml;ngige Digitalisierung textlicher, bildlicher und audiovisueller Medien im Zusammenspiel mit digitalen Kommunikations- und Distributionswegen f&uuml;hrt zu unendlich vielen Fragen, Problemen und M&ouml;glichkeiten. Auch f&uuml;r die Informatik, insbesondere die Medieninformatik. <br /><br />Absolventen der Medieninformatik analysieren, entwerfen, realisieren, adaptieren, betreiben und evaluieren informationstechnische, oft web-basierte Prozesse und Systeme zur Gestaltung, Produktion, Bearbeitung, Distribution und Nutzung medienbasierter Informationen. Das Studium der Medieninformatik zielt somit im Kern auf die Vermittlung entsprechender Methoden, Techniken und Denkweisen der Informatik. </span> </p></div>
