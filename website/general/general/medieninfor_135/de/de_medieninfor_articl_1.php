<h1>Medieninformatik </h1>
<div class="zeile">
<h2 class="float einfach">Was ist Medieninformatik? </h2>
<div class="float dreifach last">
<p>Weite Bereiche des privaten und beruflichen Alltags sind durchsetzt von digitalen Medien und Informationen: die morgendliche Tageszeitung wird durchgehend digital produziert, alle darin enthaltenen Texte auf dem Rechner erstellt, die Bilder mit Digitalkameras geschossen, via Handy und Internet zur Redaktion oder Bildagentur geschickt, im Digital-Asset-Management verschlagwortet und via World Wide Web weltweit zug&auml;nglich gemacht. Film und Fernsehen werden ebenfalls weitestgehend digital erzeugt, bearbeitet, korrigiert und verf&uuml;gbar gemacht. </p></div>
<div class="clear">&nbsp;<br /></div></div>
