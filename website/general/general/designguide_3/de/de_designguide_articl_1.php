<h1>Designguide </h1>
<div class="zeile">
<h2 class="float einfach">Schriftfamilie </h2>
<div class="float dreifach last">
<p>Bei Drucksachen und &uuml;berall dort, wo wir die Schrift kontrollieren k&ouml;nnen, wird die Myriad Pro eingesetzt. Weitere Infos dazu und die Schrift als Download finden Sie auf den Seiten des <a href="http://www.verwaltung.fh-koeln.de/organisation/dezernatesg/dezernat1/sg11/service/aussendarstellung/u/00279.php">Dezernat 1</a>.<br /><br />Im Web werden folgende Schriftarten in der abgebildeten Reihenfolge bevorzugt: Geneva, Arial, Tahoma, Verdana, sans-serif. </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach">Schriftschnitte und -lage </h2>
<div class="float dreifach last">
<p>F&uuml;r Mengentexte wird der Regular Schnitt in normaler Schriftlage verwendet werden. Kursive Schrift sollte nur in Ausnahmef&auml;llen und nicht in &Uuml;berschriften verwendet werden. F&uuml;r &Uuml;berschriften und Sublines sollten der Bold oder Semibold Schnitt verwendet werden. F&uuml;r Plakate, Flyer oder sonstige Publikationen in denen mit Typographie gespielt wird k&ouml;nnen auch andere Schnitte eingesetzt werden. </p></div>
