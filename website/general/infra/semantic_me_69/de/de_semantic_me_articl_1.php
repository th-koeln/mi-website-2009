<h2 class="float einfach">Technologie </h2>
<div class="float dreifach last">
<p>Anfang 2009 wurde das bisherige <a href="http://twiki.org">Twiki</a> durch das <a href="http://semantic-mediawiki.org">Semantic MediaWiki</a> ersetzt. Dieses ist eine Erweiterung des Mediawikis, wie es beispielsweise von Wikipedia eingesetzt wird, um den Informationen im Wiki eine Bedeutung geben zu k&ouml;nnen und eine Wiederverwendbarkeit der Informationen zu erm&ouml;glichen. </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<div class="clear">&nbsp;<br /></div></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach">Vorteile </h2>
<div class="float dreifach last">
<p>Ein semantisches Wiki bietet mehrere Vorteile gegen&uuml;ber einem "normalen" Wiki; so sind z.B. einzelne Informationen abfragbar und k&ouml;nnen an anderen Stellen im Wiki wiederverwendet werden. Ein Beispiel hierf&uuml;r k&ouml;nnte die Teilnahme an einer Veranstaltung sein. Mit dieser Information kann man f&uuml;r jeden Studenten eine &Uuml;bersicht seiner Veranstaltungen generieren.<br /><br />Doch nicht nur innerhalb des Wikis sind die Informationen verf&uuml;gbar, auch von au&szlig;en sind sie abfragbar, so bestehen Teile der Mi-Webseite (Aktuelle Neuigkeiten, Studentenliste,....) aus Informationen aus dem Wiki </p></div>
