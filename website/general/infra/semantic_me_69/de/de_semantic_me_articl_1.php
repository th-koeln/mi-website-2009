<h2 class="float einfach">Informieren, organisieren, dokumentieren </h2>
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<div class="float einfach marginalie">
<p><a href="http://www.medieninformatik.fh-koeln.de/w/">zum MI Wiki</a> </p></div>
<dl class="dreifach bild"><dt><img src="images/semantic_me_articl_11_482_.jpg" alt="Semantic MediaWiki Artikel " width="526" class="" /></dt></dl>
<dl class="einfach bild"><dt><img src="../../../../../struktur/images/spacer.gif" alt="Semantic MediaWiki Artikel " width="172" style="display:none;" class="" /></dt>
