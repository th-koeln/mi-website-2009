<div class="float dreifach">
<p>Wesentliche Teile der internen Organisation von Veranstaltungen und des Studienalltags werden &uuml;ber der MI Wiki abgebildet: Informationen zu Veranstaltung, das Buchen von Pr&uuml;fungsterminen, die Bereitstellung von Arbeitsmaterial und zur Ablage von Arbeitsergebnissen usw. Das Wiki wird daher von Studierenden und Dozenten gleicherma&szlig;en genutzt. </p></div>
