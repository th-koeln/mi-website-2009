<h2 class="float einfach">Wie k&ouml;nnen wir als Unternehmen mit dem Lehr- und Forschungsgebiet Medieninformatik kooperieren? </h2>
<div class="float dreifach last">
<p><span class="zweifach block">Es gibt verschiedene M&ouml;glichkeiten der Zusammenarbeit zwischen Unternehmen und dem Forschungs- und Lehrgebiet Medieninformatik. Hier finden Sie eine &Uuml;bersicht:</span> </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<div class="clear">&nbsp;<br /></div></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach">Bachelorarbeit im Unternehmen </h2>
<div class="float dreifach last">
<p>Die h&auml;ufigste Form der Kooperation ist die gemeinsam betreute Abschlussarbeit. Die angehenden Absolventen werden f&uuml;r die Zeit der <a href="../../../../../website/bachelor/studienseme/bachelorarb_49/de/de_bachelorarb_univer_1.php" rel="pkm49">Bachelorarbeit</a> und/oder des vorgelagerten <a href="../../../../../website/bachelor/studienseme/praxisproje_48/de/de_praxisproje_univer_1.php" rel="pkm48">Praxisprojekts</a> vom Unternehmen unter Vertrag genommen und absolvieren die Zeit &uuml;berwiegend beim Kooperationspartner. Das Thema oder Themenfeld kommt vom Unternehmen oder wird gemeinschaftlich erarbeitet. Im Regelfall wird die Arbeit von einem unserer Dozenten und einem Mitarbeiter des Unternehmens betreut. Unternehmen profitieren von der M&ouml;glichkeit, dass sich eine Person ohne Ablenkung vom "Tagesgesch&auml;ft" konzentriert mit einer Fragestellung auseinandersetzen und L&ouml;sungen auf dem aktuellen Niveau wissenschaftlicher Forschung erarbeiten kann. <br />Unsere Studierenden haben dadurch die M&ouml;glichkeit ein praxisnahes Problem im Unternehmenskontext zu l&ouml;sen und Kontakt zu potenziellen Arbeitgebern aufzubauen. Wir als Lehr- und Forschungsgebiet Medieninformatik profitieren von der Konfrontation mit konkreten praxisnahes Problemfeldern. <br /><br />Meist wird ein gemeinsames Projekt durch die Module Praxisprojekt und Abschlussarbeit abgedeckt. Dadurch k&ouml;nnen sich die Studentinnen und Studenten ein komplettes Semester auf ein Thema konzentrieren. </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<div class="float einfach marginalie">
<p><strong>Eckdaten Praxisprojekt:</strong><br />15 ECTS Punkte, entspricht 450 Stunden Workload<br />Studierende ab dem 5. Semester </p></div>
<div class="float dreifach">
<p><span class="marginalie block einfach"><strong>Eckdaten Bachelorarbeit:</strong><br />12 ECTS Punkte, entspricht 360 Stunden Workload<br />Studierende ab dem 6. Semester</span> </p></div>
<div class="clear">&nbsp;<br /></div></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach">Masterarbeit im Unternehmen </h2>
<div class="float dreifach last">
<p>Das viersemestrige Masterstudium schlie&szlig;t mit der Massenarbeit ab, f&uuml;r die ca. sechs Monate Arbeit vorgesehen sind. Der organisatorische Rahmen kann &auml;hnlich wie bei der Bachelorarbeit im Unternehmen sein. Der Anspruch und der Umfang der Arbeit m&uuml;ssen den Zielen des Masterstudiums entsprechen, insbesondere m&uuml;ssen die selbstst&auml;ndige Anwendung der theoretisch-analytischen F&auml;higkeiten und die selbstst&auml;ndige Urteilsfindung bei der Ausarbeitung im Vordergrund stehen. </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<div class="float einfach marginalie">
<p><br class="spacer" /> </p></div>
<div class="float dreifach">
<p><span class="marginalie block einfach"><strong>Eckdaten Masterarbeit:</strong><br />30 ECTS Punkte, entspricht 900 Stunden Workload<br />Studierende ab dem 4. Semester im Masterstudiengang </span> </p></div>
<div class="clear">&nbsp;<br /></div></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach">Projektarbeit als Wahlpflichfach </h2>
<div class="float dreifach last">
<p>Themen, die von einer Kleingruppe bearbeitet werden sollten, k&ouml;nnen bei uns im Rahmen eines Wahlpflichtfachs bearbeitet werden. Hier gibt es Angebote zu unterschiedlichen Themenfeldern, die abh&auml;ngig vom konkreten Thema von 5 bis 10 Studierenden besucht werden. Bei dieser Form der Zusammenarbeit sind wir zeitlich an die Vorlesungszeiten gebunden. Das Aufsetzen des Projekts erfolgt in der vorausgehenden vorlesungsfreien Zeit. Sollten sie an dieser Form der Kooperation interessiert sein, empfiehlt es sich uns bis sp&auml;testens Mitte Januar oder Juni zu kontaktieren um das Projekt im n&auml;chsten Sommer bzw. Wintersemester zu platzieren. </p></div>
<div class="clear">&nbsp;<br /></div></div>
