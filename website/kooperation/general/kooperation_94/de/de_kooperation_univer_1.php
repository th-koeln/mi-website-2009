<div class="float einfach marginalie">
<p><br class="spacer" /> </p></div>
<div class="float dreifach">
<p><span class="marginalie block einfach"><strong>Eckdaten Wahlpflichtfach:</strong><br />5 ECTS Punkte, entspricht 150 Stunden Workload<br />Studierende ab dem 3. Semester</span> </p></div>
<div class="clear">&nbsp;<br /></div></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach">Gastvortrag </h2>
<div class="float dreifach last">
<p>H&auml;ufig kommt der erste Kontakt f&uuml;r eine l&auml;ngerfristige Kooperation &uuml;ber einen Gastvortrag in einer unserer Veranstaltungen. Hier haben Unternehmensvertreter die M&ouml;glichkeit Problemfelder, L&ouml;sungen und Fallstudien aus ihrem Bereich vorzustellen und zu diskutieren. Sollten sie daran Interesse haben, schauen sie sich vorab unsere Veranstaltungs&uuml;bersicht an, um zu pr&uuml;fen in welches Gebiet ihr Thema passen k&ouml;nnte und kontaktieren sie am Besten den zugeh&ouml;rigen Verantwortlichen. </p></div>
