<h1>Aktuelles Forschungsprojekt: Virtuelle Exkursion zum Thema Rhein in K&ouml;ln </h1>
<p>In Zusammenarbeit mit dem Seminar f&uuml;r Geographie und ihre Didaktik der Universit&auml;t zu K&ouml;ln (Prof. Dr. Alexandra Budke) wird eine Web basierte virtuelle Exkursion zur Wasserproblematik in K&ouml;ln entwickelt, die im Rahmen des Geographieunterricht einsetzbar ist. Die virtuelle Exkursion soll die geographischen Kompetenzen der Sch&uuml;lerInnen erweitern, indem Inhalte und Handlungsm&ouml;glichkeiten wie sie bei einer "realen" Exkursion geboten sind geeignet in das Web transportiert werden. Am Beispiel der Wasserproblematik in K&ouml;ln k&ouml;nnen die Sch&uuml;lerInnen exemplarisch die Themenbereiche Wasserqualit&auml;t, Hochwasserproblematik, Ufernutzung und das &ouml;kologisches Gew&auml;ssermanagement behandeln. In einer zweiten Phase des Projektes soll die virtuelle Exkursion zu einem themenbezogenen Kooperationssystem ausgebaut werden, das es erlaubt verschiedene Klassen, aber auch Experten und andere Stakeholder in die Exkursion einzubeziehen. <br />Das Projekt f&uuml;r die Jahre 2012 und 2013 geplant und wird von der RheinEnergie Stiftung gef&ouml;rdert.<br /><br />Projektbeteiligte im Lehr- und Forschungsgebiet Medieninformatik:<br /><a href="../../../../../website/mi_am_campu/prof/prof_dr_k_12/de/de_prof_dr_k_univer_1.php" rel="pkm12">Prof. Dr. Kristian Fischer</a><br />Carsten Hoke, B.Sc. <br /></p></div>
