<h1>Akkreditierung Master </h1>
<div class="zeile">
<h2 class="float einfach">Zugang zum Promotionsstudium </h2>
<div class="float dreifach last">
<p>Mit der Einf&uuml;hrung des Konzepts der Bachelor- und Masterstudieng&auml;nge ist verbunden, dass der Masterabschluss grunds&auml;tzlich den Zugang zu einem weiterf&uuml;hrenden Promotionsstudium er&ouml;ffnet, unabh&auml;ngig davon, ob der Abschluss an einer Fachhochschule oder einer Universit&auml;t erworben wurde. Grundvorraussetzung ist in der Regel, dass der Studiengang durch eine fachlich anerkannte Agentur, wie es die ASIIN ist, akkreditiert ist. Einge der bisherigen Absolventen des Masterstudiengangs Medieninformatik konnten diesen Weg einschlagen und wurden von Universit&auml;ten in Deutschland oder im Ausland f&uuml;r das Promotionsstudium zugelassen </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="zeile">
<div class="subzeile">
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<dl class="einfach bild"><dt><img src="images/akkreditier_univer_12_572_.jpg" alt="Akkreditierung Master Universalthema " width="172" class="" /></dt>
<dd><a href="http://www.asiin.de">www.asiin.de</a> </dd></dl>
<div class="float dreifach">
<p>Bei der Akkreditierung wird durch eine unabh&auml;ngige Fachkommission u.a. gepr&uuml;ft, ob ein Studiengang nach Inhalt und Niveau bundesweit einheitlichen Vorgaben entspricht, die in der Regel von berufst&auml;ndischen Vereinigungen festgelegt werden. Bei dem Studiengang Medieninformatik ist das die Gesellschaft f&uuml;r Informatik. Ausserdem wird gepr&uuml;ft, ob die personelle Ausstattung, die Ger&auml;te in den Laboren und die R&auml;ume und Einrichtungen (z.B. Bibliothek oder Informationsverarbeitung) f&uuml;r ein effektives Studium ausreichen. Die Akkreditierung muss alle f&uuml;nf Jahre wiederholt werden. F&uuml;r diesen Studiengang Medieninformatik wurde sie 2004 ausgesprochen und gilt demnach zun&auml;chst bis 2009. Die Studieng&auml;nge Medieninformatik Bachelor und Medieninformatik Master sind in 2009 erfolgreich reakkreditiert worden. Die Reakkreditierung gilt bis 2011. </p></div>
<div class="clear">&nbsp;<br /></div></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach">Zugang zur Laufbahn des h&ouml;heren Dienstes </h2>
<div class="float dreifach last">
<p>Bisher war im Bereich des &ouml;ffentlichen Dienstes der Zugang zu der Laufbahn des h&ouml;heren Dienstes Absolventen von Universit&auml;ten vorbehalten. Diese Regelung widerspricht dem Konzept der Bachelor- und Masterstudieng&auml;nge. Die neue Regelung ist, dass erstens generell nur Masterabsolventen Zugang zu der Laufbahn des h&ouml;heren Dienstes haben und Absolventen von Fachhochschulen nur dann, wenn die Qualifizierung zur Laufbahn des h&ouml;heren Dienstes im Rahmen der Akkreditierung festgestellt wurde. Das ist diesem Studiengang erfolgt: Der Masterstudiengang Medieninformatik berechtigt zur Laufbahn des h&ouml;heren Dienstes. </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<div class="clear">&nbsp;<br /></div></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach">Einordnung als "st&auml;rker anwendungsorientiert" </h2>
<div class="float dreifach last">
<p>Masterstudieng&auml;nge werden bei der Akkredidierung verschiedenen Gruppen zugeordnet, um um die Charakteristik des Studiengangs und die Ausrichtung der Hochschule, die ihn anbietet auf ein Blick sichtbar zu machen. Entsprechend der Ausrichtung von Fachhochschulen ist der Studieng als "st&auml;rker anwendungsorientiert" eingeordnet. </p></div>
