<h1>Spezielle Gebiete der Mathematik </h1>
<div class="zeile">
<div class="float dreifach last">
<p>Pflichtfach<br />7 ECTS-P<br />4 SWS<br />i.d.R. Wintersemester </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Verantwortlich </h2>
<div class="float dreifach last">
<p><a href="../../../../../website/mi_am_campu/prof/prof_dr_h_81/de/de_prof_dr_h_univer_1.php" rel="pkm81">Prof. Dr. Horst Stenzel</a> </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Ziele </h2>
<div class="float dreifach last">
<p>Der Master-Studiengang hat zum Ziel, die Teilnehmer f&uuml;r weiterf&uuml;hrende wissenschaftliche Arbeiten zu qualifizieren. Mathematische Fertigkeiten sind eine unverzichtbare Basis wissenschaftlicher Arbeiten im Bereich der Medieninformatik. Diese Veranstaltung soll dazu verhelfen, gen&uuml;gend Sicherheit im Umgang mit mathematischen Methoden zu geben, um sich in diesem Feld selbst&auml;ndig bewegen zu k&ouml;nnen. </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrinhalte </h2>
<div class="float dreifach last">
<p>Exemplarische Fragestellungen der Medieninformatik mit wesentlichen mathematischen Inhalten. Themen des vergangenen Semesters: Deskriptive Statistik, Datenanalyse, Visualisierung; Schlie&szlig;ende Statistik, Trendanalyse; Wavelets, Bildverarbeitung; Kryptologie; Schlie&szlig;ende Statistik; 3D-Informationen aus Stereo-Bildpaaren; Color-Management </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrmethoden </h2>
<div class="float dreifach last">
<p>Grundlagenvorlesung zur Nivellierung der Eingangsniveauunterschiede, schriftliche Seminararbeit, Web­based Peer-Review (Jeder Studierende &uuml;bernimmt dabei, in Anlehnung an den etablierten Prozess des wissenschaftlichen Publizierens, die Funktion eines Autors und die eines anonymen Gutachters &uuml;ber die Ausarbeitungen seiner Kommilitonen. Durch die Auswahl der Themen und der zu begutachtenden Arbeiten kommt er in intensive Auseinandersetzung mit den verschiedenen Themengebieten der Lehrveranstaltung.) </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Voraussetzungen </h2>
<div class="float dreifach last">
<p>keine, die &uuml;ber die Eingangsvoraussetzungen zum Studium hinausgehen </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Ort, Ressourcen </h2>
