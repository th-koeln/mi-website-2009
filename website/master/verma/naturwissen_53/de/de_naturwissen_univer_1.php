<h1>Naturwissenschaftliche Grundlagen digitaler Medien </h1>
<div class="zeile">
<div class="float dreifach last">
<p>Pflichtfach<br />5 ECTS-P<br />4 SWS<br />i.d.R. Sommer- und Wintersemester </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Verantwortlich </h2>
<div class="float dreifach last">
<p><a href="../../../../../website/mi_am_campu/prof/prof_dr_g_17/de/de_prof_dr_g_univer_1.php" rel="pkm17">Prof. Dr. Gerhard Hartmann</a> </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Ziele </h2>
<div class="float dreifach last">
<p>Ziel ist die Vertiefung naturwissenschaftlicher Kenntnisse, die Elaboration der Methodenkompetenz bzgl. naturwissenschaftlicher Fragestellungen und die Weiterentwicklung analytischer F&auml;higkeiten im Anwendungsbezug auf Digitale Medien. </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrinhalte </h2>
<div class="float dreifach last">
<p><strong>Vorlesung:</strong><br />Zun&auml;chst werden spezielle Fragestellungen bzgl. hochaufl&ouml;sender (HD-) Akquisitiontech-nologien behandelt: Empfindlichkeit, Gradation und Aufl&ouml;sungsverm&ouml;gen CCD-basierter Bildwandlersystemen. Anschliessend werden Speichertechnologien (magnetische und optische) f&uuml;r Bewegtbild betrachtet. Physikalische Charakteristika der verf&uuml;gbaren Technologien (Omega-Gang, D&auml;mpfungseffekte, numerische Apertur) werden aufgezeigt, deren Auswirkungen kritisch diskutiert und Ausblicke auf avancierte Technologien gegeben (z.B. AOD). In einem zweiten Teil werden zun&auml;chst Bewegungsmodelle f&uuml;r die Bewegtbildanalyse dargestellt. Daran anschliessend werden Verfahren f&uuml;r die Segmentierung und (2-und 3-dimensionale) Bewegungssch&auml;tzung vorgestellt und kritisch diskutiert.<br /><br /><strong>Seminar:</strong><br />Ausgew&auml;hlte Algorithmen der Video-und Bildverarbeitung werden bearbeitet und deren Leistungsf&auml;higkeit kritisch diskutiert. </p></div><br style="clear:both" /></div>
