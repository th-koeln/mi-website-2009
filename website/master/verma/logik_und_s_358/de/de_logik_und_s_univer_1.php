<h1>Logik und semantische Modellierung </h1>
<div class="zeile">
<div class="float dreifach last">
<p>Pflichtfach<br />5 ECTS-P<br />4 SWS<br />i.d.R. Sommersemester </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Verantwortlich </h2>
<div class="float dreifach last">
<p><a href="../../../../../website/mi_am_campu/prof/prof_dr_k_12/de/de_prof_dr_k_univer_1.php" rel="pkm12">Prof. Dr. Kristian Fischer</a> </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Ziele </h2>
<div class="float dreifach last">
<p>Die Studierenden <li>kennen die zentralen Konzepte und Zusammenh&auml;nge der Aussagenlogik, der Pr&auml;dikatenlogik und der Beschreibungslogik und k&ouml;nnen in den Resolutionskalk&uuml;len einfache Beispiele rechnen.</li><li>kennen im Bereich der semantischen Modellierung die zentralen Sprachen, Techniken und Methoden, k&ouml;nnen diese in Bezug auf Anwendungen kritisch diskutieren.</li><li>k&ouml;nnen mit &uuml;blichen Editoren, Systemen und Rahmenwerken Implementierungen im Sinne eines proof-of-concept erstellen.<br /> </li><br /></p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrinhalte </h2>
<div class="float dreifach last">
<p>Im ersten Teil der Vorlesung wird eine Einf&uuml;hrung in die mathematische Logik gegeben. Ein Schwerpunkt liegt dabei in der Vermittlung von Konzepten wie „Modell", „Entscheidbarkeit" oder „Kalk&uuml;l" und deren Diskussion. Die Konzepte der Beschreibungslogik werden eingef&uuml;hrt und in Bezug auf Fragen der Entscheidbarkeit, Komplexit&auml;t und Unterst&uuml;tzung durch Werkzeuge diskutiert.<br /><br />Im zweiten Teil die zentralen Sprachen, Technologien, Methoden und Anwendungen im Bereich der semantischen Modellierung dargestellt. In der Anwendung wird ein Schwerpunkt auf Ontologien in den Bereichen Metadaten und audiovisuelle Inhalte gelegt.<br /><br />Im Seminarteil sollen Techniken, Kalk&uuml;le und Methoden durch die Studierenden angewendet und kritisch diskutiert werden. <br /></p></div><br style="clear:both" /></div>
<div class="zeile">
