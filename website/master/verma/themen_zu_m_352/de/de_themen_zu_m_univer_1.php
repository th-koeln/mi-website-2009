<h1>Themen zu Medien und Gesellschaft </h1>
<div class="zeile">
<div class="float dreifach last">
<p>Pflichtfach<br />2 ECTS-P<br />2 SWS<br />i.d.R. Sommersemester </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Verantwortlich </h2>
<div class="float dreifach last">
<p><a href="../../../../../website/mi_am_campu/prof/prof_dr_k_12/de/de_prof_dr_k_univer_1.php" rel="pkm12">Prof. Dr. Kristian Fischer</a> </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Ziele </h2>
<div class="float dreifach last">
<p>Die Studierenden sollen wesentliche Begriffe und Grundpositionen bez&uuml;glich ethischer und sozialer Fragen, die durch die mediale Kommunikation und den Einsatz von Informationssystemen aufgeworfen werden, kennen. Sie sollen weiterhin Positionen aus wissenschaftlichen Ver&ouml;ffentlichungen in diesem Bereich erarbeiten, vortragen und dazu Stellung beziehen k&ouml;nnen. </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrinhalte </h2>
<div class="float dreifach last">
<p>In der Veranstaltung werden ausgew&auml;hlte Themen aus dem Bereich „Medien und Gesellschaft" behandelt und orientiert sich an den sich etablierenden Fachgebieten „Computer Ethics" in der angloamerikanischen Literatur und „Informationsethik" in der deutschsprachigen Literatur. Grundbegriffe f&uuml;r den gesellschaftlichen Diskurs im Umfeld<br />digitalisierter Informationen, <br />weltweiter Vernetzung und <br />Handlungen in elektronischen R&auml;umen <br />werden in Form von kurzen Vorlesungen eingebracht. Auf dieser Basis werden aktuelle Themen wie Privatsph&auml;re, globale Informationsethik, professionelle Verantwortung, geistiges Eigentum und andere in Seminarform behandelt.<br /></p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrmethoden </h2>
<div class="float dreifach last">
<p>Seminar </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Voraussetzungen </h2>
<div class="float dreifach last">
<p>Keine &uuml;ber die Zulassungsbedingungen hinausgehenden Voraussetzungen </p></div><br style="clear:both" /></div>
<div class="zeile">
