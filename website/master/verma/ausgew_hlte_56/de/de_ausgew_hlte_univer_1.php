<h1>Ausgew&auml;hlte Anwendungsgebiete f&uuml;r multimediale Systeme </h1>
<div class="zeile">
<div class="float dreifach last">
<p>Pflichtfach<br />5 ECTS-P<br />4 SWS<br />j&auml;hrlich angeboten </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Verantwortlich </h2>
<div class="float dreifach last">
<p><a href="../../../../../website/mi_am_campu/prof/prof_dr_k_12/de/de_prof_dr_k_univer_1.php">Prof. Dr. Kristian Fischer</a> </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Ziele </h2>
<div class="float dreifach last">
<p>Die Studierenden sollen:	 <li>typische Funktionen von Systemen f&uuml;r rechnergest&uuml;tzte Gruppenarbeit kennen und in Bezug zu Gruppenprozessen, Aufgaben und der Organisation setzen k&ouml;nnen, 	</li><li>die wesentlichen Verfahren f&uuml;r synchrone und asynchrone Kooperation theoretisch fundiert erkl&auml;ren k&ouml;nnen,	</li><li>die wesentlichen Ans&auml;tze mediengest&uuml;tzten Lernens in ihrer historischen Entwicklung kennen und bewerten k&ouml;nnen</li><li>die wesentlichen Methoden zur Strukturierung von medialen Lernangeboten kennen und Methoden zum Einsatz von XML zur Strukturierung von Lernmedien kennen </li><br /></p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrinhalte </h2>
<div class="float dreifach last">
<p>Es werden exemplarisch zwei Anwendungsgebiete f&uuml;r multimediale Systeme behandelt: die rechnergest&uuml;tzte Gruppenarbeit und telemediale und multimediale Lernumgebungen.<br /><br />Bei der rechnergest&uuml;tzten Gruppenarbeit wird ausgehend vom Begriff der Gruppe auf das Zusammenspiel von Gruppe, Aufgabe, Organisation und Technik eingegangen. Im Anschluss wird eine Klassifizierung von Systemen f&uuml;r die rechnergest&uuml;tzte Gruppenarbeit eingef&uuml;hrt. Ausf&uuml;hrlicher wird dann auf Konzepte der synchronen und asynchronen Kooperation eingegangen, insbesondere auch auf Methoden der Nebenl&auml;ufigkeitskontrolle.<br /><br />Bei den telemedialen und multimedialen Lernumgebungen werden programmierte Unterweisung, kognitive Ans&auml;tze, tutorielle Systeme, sowie situiertes Lernen und Konstruktivismus eingef&uuml;hrt und diskutiert. Anschlie&szlig;end wird auf Methoden zur Strukturierung von Lernangeboten (Gagne, Merill) eingegangen.<br /><br />Anschlie&szlig;end wird auf Techniken f&uuml;r die Strukturierung von Lernmedien f&uuml;r den Konstruktivismus eingegangen und in diesem Zusammenhang der Einsatz von XML diskutiert. </p></div><br style="clear:both" /></div>
<div class="zeile">
