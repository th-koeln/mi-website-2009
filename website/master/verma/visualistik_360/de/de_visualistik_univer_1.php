<h1>Visualistik </h1>
<div class="zeile">
<div class="float dreifach last">
<p>Pflichtfach<br />5 ECTS-P<br />2 SWS<br />i.d.R. Sommersemester </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Verantwortlich </h2><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Ziele </h2>
<div class="float dreifach last">
<p>Die Studierenden sollen in die Lage versetzt werden, abstrakte, vorwiegend numerische Datenbest&auml;nde in 2- und 3-dimensionalen Repr&auml;sentationen zu visualisieren und mit interaktiven Methoden zu untersuchen, um Strukturen aufzudecken, Hypothesen abzuleiten und zu verifizieren sowie Ergebnisse zu kommunizieren. Die Studierenden sind in der Lage, die aus modernen Simulations- und Messmethoden resultierenden umfangreichere Datenvolumina durch Visualisierungsverfahren einer Analyse zuzuf&uuml;hren. Dies wird sowohl anhand von theoretischen Grundlagen, der Analyse und Pr&auml;sentation aktueller Forschungsergebnisse als auch der Verifikation der erworbenen Kenntnisse an eigenen Visualisierungen vermittelt. </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrinhalte </h2>
<div class="float dreifach last">
<p>Grundlagen der Visualistik und r&auml;umlichen Wahrnehmung, Datenstrukturierung und Analyse, aktuelle Visualisierungs- und Interaktionsverfahren. Dar&uuml;ber hinaus werden Grundlagen der statistischen Datenanalyse und der 3D-Computergrafik einbezogen. </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrmethoden </h2>
<div class="float dreifach last">
<p>Vorlesung, Seminar, Selbststudium </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Voraussetzungen </h2>
<div class="float dreifach last">
<p>Keine &uuml;ber die Zulassungsbedingungen hinausgehenden Voraussetzungen<br /><br />Empfohlen:<br />Grundfertigkeiten und Erfahrungen der Computer Graphik, der Mensch-Maschine-Interaktion<br /></p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Ort, Ressourcen </h2>
<div class="float dreifach last">
<ul><li>Beamer-gest&uuml;tzte Vorlesungen, Folien und andere Unterlagen in elektronischer Form auf der Lehrplattform </li><li>Vertiefende Materialien wie Literaturausz&uuml;ge und beispielhafte Analysen samt Daten und Software in elektronischer Form </li><li>praktische Vorf&uuml;hrungen und eigenen explorative Analsye zu &Uuml;bungszwecken in Gruppenarbeit </li></ul></div><br style="clear:both" /></div>
