<h1>Interaction Design </h1>
<div class="zeile">
<div class="float dreifach last">
<p>Pflichtfach<br />5 ECTS-P<br />2 SWS<br />i.d.R. Wintersemester </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Verantwortlich </h2>
<div class="float dreifach last">
<p><a href="../../../../../website/mi_am_campu/prof/prof_dr_g_17/de/de_prof_dr_g_univer_1.php" rel="pkm17">Prof. Dr. Gerhard Hartmann</a> </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Ziele </h2>
<div class="float dreifach last">
<p>Die Studierenden sollen Kenntnisse in ausgew&auml;hlten, vertiefenden Bereichen der Kognitions- und Motivationspsychologie erwerben, Methoden und Techniken zur Benutzer-, Nutzungs- und Kontext-Modellierung kennen und anwenden k&ouml;nnen, Modelle der Interaktion und interaktiver Systeme kennen, Entwurfskompetenz erwerben (prototyping, storyboarding etc.) und die Entw&uuml;rfe methodenkritisch evaluieren k&ouml;nnen.<br /><br />Ziel ist vor allem, die Benutzerperspektive im Entwicklungsprozess zu ber&uuml;cksichtigen, nicht von der Technologie sondern vom menschlichen Handeln auszugehen und eine der jeweiligen Pragmatik ad&auml;quate Interaktionsmodellierung und -gestaltung erreichen zu k&ouml;nnen. Dabei wird besonderer Wert auf den Auf- bzw. Ausbau von Entwurfskompetenz („reflection in action", „conversation with the material") gelegt, die das systematische Entwickeln von Entwurfsalternativen, deren Bewertung, der Synthese gefundener Qualit&auml;ten in koh&auml;renten und konsistenten Systementw&uuml;rfen und den systematischen, konstruktiven Umgang mit trade-offs und ein insgesamt iteratives Vorgehen beinhaltet. <br /><br />Ziel ist es auch, nicht die Artefakte in den Vordergrund zu r&uuml;cken (z.B.„Interface-Design"), sondern die Interaktion zwischen Menschen und technischen Systemen systemisch zu verstehen, zu beschreiben und sowohl aus der menschlichen als auch aus der technischen Perspektive heraus zu modellieren und in einen konsistenten Systementwurf zu &uuml;berf&uuml;hren („designing from both sides of the screen").<br /></p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrinhalte </h2>
<div class="float dreifach last">
<ul><li>Ausgew&auml;hlte Themen der Kognitionspsychologie und Motivationspsychologie, </li><li>Interaktions-Modelle von Norman, Abowd, Herczeg, u.a.,</li><li>Ergonomie-Gesetze und ihre Anwendungsbereiche,</li><li>Benutzer, Benutzungs- und Kontextmodellierung,</li><li>Paradigm- and metaphor engineering,</li><li>Affordances nach Gibson, Norman und Hartson,</li><li>HCI Design pattern,</li><li>Kreativit&auml;t und Kreativit&auml;tstechniken,</li><li>Techniken des Prototypings,</li><li>Bewertung, Testing und Evaluation von Artefakten (Methoden und Techniken),</li><li>Praxisorientierte (untereinander vernetze) Workshops zu einzelnen Punkten.<br /> </li></ul></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrmethoden </h2>
