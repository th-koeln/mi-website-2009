<h1>Projektmanagement </h1>
<div class="zeile">
<div class="float dreifach last">
<p>Pflichtfach<br />4 ECTS-P<br />3,5 SWS<br />i.d.R. Sommersemester </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Verantwortlich </h2>
<div class="float dreifach last">
<p>Prof. Dr. Holger G&uuml;nther </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Ziele </h2>
<div class="float dreifach last">
<p>Die Studierenden sollen alle Kenntnisse, Informationen, Theorie- und Faktenwissen sowie alle kognitiven und praktischen Fertigkeiten und Kompetenzen besitzen, um eigenverantwortlich und professionell Projekte im Umfeld der Medieninformatik organisieren, durchf&uuml;hren, kontrollieren und leiten zu k&ouml;nnen.<br />Sie sollen die nach PMI (Project Management Institute) wesentlichen Projektmanagement-Disziplinen und Methoden beherrschen und einordnen und die erforderlicher Ma&szlig;nahmen und Methodiken anwenden k&ouml;nnen.<br />Die Studierenden sollen Kennnisse, Fertigkeiten und soziale sowie methodische F&auml;higkeiten in Arbeits- und Projektsituationen integrieren k&ouml;nnen, und insbesondere sollen sie die Bedeutung soziologischer Aspekte einsch&auml;tzen k&ouml;nnen, mit dem Ziel einer menschengerechten und soziologisch fundierten Menschenf&uuml;hrung zur Erreichung einer effektiven und effizienten Produktivit&auml;t bei komplexen Projekten.<br /></p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrinhalte </h2>
<div class="float dreifach last">
<p>Erfolgreiches Projektmanagement umfasst organisatorische, planerische und kontrollierende Aktivit&auml;ten. Projekte im Medienumfeld stellen hierbei eine besondere Herausforderung dar: Prozessgestaltung und der Umgang mit personellen und fachlichen Kompetenzen in Medienprojekten erfordern informatische, betriebswirtschaftliche und soziologische Kompetenzen, die in den Projekten vereint werden m&uuml;ssen. Soziologische Aspekte sind von erfolgskritischer Bedeutung. Methodisches und zielgerichtetes Vorgehen, gute Kommunikation im Team und mit dem Kunden und das Finden einer gemeinsamen fachlichen, methodischen, sprachlichen und kulturellen Verst&auml;ndigungsebene sind notwendige Mittel f&uuml;r ein effektives Projektmanagement. <br /><br />Die vielseitigen Aufgaben eines Projektleiters sollen erarbeitet und alle notwendigen Methoden und „Methodiken" (dies sind im Gegensatz zu „Methoden" die handwerklichen Grundlagen und administrativen Basisaufgaben) soweit erforderlich vermittelt werden. Zur Erarbeitung insbes. der soziologischen Anforderungen an das Projektmanagement werden spezielle Aspekte behandelt, insb. Risikomanagement zur Sicherstellung der Business Continuity, andererseits z.B. Changemanagement und soziologische Aspekte.<br /></p></div><br style="clear:both" /></div>
