<h1>IT-Sicherheit </h1>
<div class="zeile">
<div class="float dreifach last">
<p>Pflichtfach<br />5 ECTS-P<br />4 SWS<br />i.d.R. Wintersemester </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Verantwortlich </h2>
<div class="float dreifach last">
<p>Prof Dr. Stefan Karsch </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Ziele </h2>
<div class="float dreifach last">
<p>F&auml;higkeit zur Analyse und Formulierung von Schutzzielen in Einsatzszenarien von IT <li>Kenntnis der aktuell in der Praxis eingesetzten kryptographischen Verfahren und ihrer Eigenschaften f&uuml;r die praktische Anwendung sowie F&auml;higkeit zur Auswahl geeigneter Sicherheitsmechanismen, um vorgegebene Sicherheitsziele zu erreichen </li><li>F&auml;higkeit zur Analyse von Systemen auf Sicherheitsschw&auml;chen und kritisches Hinterfragen von Aussagen zur Sicherheit </li><li>Kenntnis der Ursachen von Sicherheitsschw&auml;chen in TCP/IP-basierten Netzen und Diensten </li><li>F&auml;higkeit zu Einsatz und Analyse der Sicherheitsmechanismen in TCP/IP-basierten Netzen <br />Ein&uuml;ben der F&auml;higkeit zur Analyse der Sicherheitseigenschaften konkreter Werkzeuge mit Hilfe der Im Vorlesungsteil der Veranstaltung vorgestellten Begriffe </li><li>Einsch&auml;tzen der Grenzen von Sicherheitswerkzeugen anhand konkreter Beispiele </li><li>Vertieftes Verst&auml;ndnis technischer Zusammenh&auml;nge in der IT-Sicherheit </li><li>Einsch&auml;tzen des Schutzbedarfs anhand konkreter Angriffsm&ouml;glichkeiten </li><li>Schulung der analytischen F&auml;higkeiten mittels konkreter Einsatzszenarien die Basiskonzepte und Grundlagen der Betriebssysteme und der Rechnerarchitektur kennen und verstehen sowie</li><li>ein einheitliches konsistentes Begriffsgeb&auml;ude zu teilweise aus der pers&ouml;nlichen Praxis bekannten Sachverhalten der IT aufbauen<br /> </li><br /></p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrinhalte </h2>
<div class="float dreifach last">
<p>Teil I: <li>Einf&uuml;hrung in die Grundbegriffe der IT-Sicherheit (Was ist Sicherheit?, Schutzziele, Mechanismen, Beispielszenarien)</li><li>Einsatz aktueller kryptographischer Verfahren als Mechanismus zur Erreichung von Schutzzielen (symmetrische, asymmetrische Kryptographie, digitale Signaturen, Zertifikate, Angriffe)</li><li>Aktuelle kryptographische Standards (AES, RSA ,...)</li><li>Prinzipien und Mechanismen zur Authentisierung (Biometrie, Passw&ouml;rter, Chipkartensysteme, Single-Sign-On)</li><li>Sicherheit TCP/IP basierter Netze und Dienste (Schw&auml;chen, Angriffe, Beispiele)</li><li>Firewall-Systeme (Application-Level-Gateways, Paketfilter, Remote-Access) </li></ul>Teil II: Spezielle Werkzeuge der IT-Sicherheit in der Praxis: <li>Sicherheitswerkzeuge, </li><li>Werkzeuge zur Pr&uuml;fung der Sicherheit, </li><li>Angriffswerkzeuge und -methoden </li></ul>Aufgabe der Studierenden: <li>Beschreibung, </li><li>Test und Vorf&uuml;hrung der Funktion, </li><li>Analyse und Erl&auml;uterung der F&auml;higkeiten und Grenzen im Kontext von Teil I der Veranstaltung, </li><li>Darstellung typischer Einsatzszenarien, </li><li>Vergleich mit anderen Werkzeugen mit &auml;hnlichen F&auml;higkeiten oder Eigenschaften<br /> </li><br /></p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrmethoden </h2>
<div class="float dreifach last">
<p>Vorlesung, Seminar (Vortrag, schriftliche Ausarbeitung, Test und Vorf&uuml;hrung von Werkzeugen)<br /></p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Voraussetzungen </h2>
<div class="float dreifach last">
<ul><li>Grundlagen Rechnerarchitektur und Betriebssysteme </li><li>Tiefergehende Kenntnisse &uuml;ber Betriebssysteme und ihre Implementierung sowie praktische Erfahrung mit aktuell verbreiteten Betriebssystemen</li><li>Kenntnisse und Erfahrungen mit sicherheitskritischen IT-Einsatzszenarien </li><li>Kenntnis grundlegender Unternehmensprozesse <br />Protokollfamilie TCP/IP und darauf basierende Dienste<br /> </li></ul></div><br style="clear:both" /></div>
