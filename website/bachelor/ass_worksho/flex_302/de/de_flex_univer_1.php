<div class="zeile">
<h2 class="float einfach">Beschreibung </h2>
<div class="float dreifach last">
<p>Im Bereich der RIAs spielt das von Adobe entwickelte Flex Open-Source-Framework eine essentielle Rolle. Zahlreiche Komponenten und Erweiterungen erm&ouml;glichen es mit diesem Framework anspruchsvolle und komplexe Multimediaanwendungen zu erstellen.<br /><br />Flash ist erwachsen geworden. Im Bereich der Rich Internet Applications (RIAs) spielt das von Adobe entwickelte Flex Open-Source-Framework eine essentielle Rolle. Zahlreiche Komponenten und Erweiterungen erm&ouml;glichen es mit diesem Framework anspruchsvolle und komplexe Multimediaanwendungen zu erstellen. Flex-basierte RIAs k&ouml;nnen mit dem weit verbreiteten Adobe Flash&reg; Player im Browser oder mit Adobe AIR™ auf dem Desktop ausgef&uuml;hrt werden, wobei in s&auml;mtlichen g&auml;ngigen Browsern und auf allen f&uuml;hrenden Betriebssystemen die Konsistenz gewahrt bleibt. </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Ziel </h2>
<div class="float dreifach last">
<p>Ziel der Veranstaltung ist es, die Teilnehmer und Teilnehmerinnen in den Umgang mit dem Flex Builder und dem FLEX Framework einzuf&uuml;hren. </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Teilnahmebedingung </h2>
<div class="float dreifach last">
<p>Der Kurs richtet sich sowohl an Bachelor- als auch an Master-Studenten. F&uuml;r Bachelorstudenten gilt: Neben dem Interesse am Programmieren m&uuml;ssen die Teilnehmerinnen und Teilnehmer das Grundstudium abgeschlossen haben. Ein Schein aus dem Grundstudium darf fehlen. </p></div><br style="clear:both" /></div>
