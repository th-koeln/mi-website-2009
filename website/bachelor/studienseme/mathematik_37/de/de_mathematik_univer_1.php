<h1>Mathematik II </h1>
<div class="zeile">
<div class="float dreifach last">
<p>Pflichtfach<br />8 ECTS-P<br />7 SWS<br />j&auml;hrlich angeboten </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Verantwortlich </h2>
<div class="float dreifach last">
<p><a href="../../../../../website/mi_am_campu/prof/prof_dr_w_16/de/de_prof_dr_w_univer_1.php" rel="pkm16">Prof. Dr. Wolfgang Konen</a> </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Ziele </h2>
<div class="float dreifach last">
<p>Die Studierenden sollen <li>die F&auml;higkeiten zur Analyse realer oder geplanter Systeme entwickeln, indem sie praktische Aufgabenstellungen aus dem Informatik-Umfeld in mathematische Strukturen abstrahieren und</li><li>lernen, selbstst&auml;ndig die Modellfindung und die Ergebnisbeurteilung vorzunehmen. </li><li>Dabei sollen die Anwendungsbez&uuml;ge der Mathematik deutlich werden, z. B. die Beziehungen diskreter Strukturen wie der Graphen zu vielf&auml;ltigen grundlegenden Datenstrukturen, die Statistik zur Deskription und Beurteilung von Beobachtungen und die Analysis zur Verarbeitung von Signalen und zur L&ouml;sung von mathematischen Modellen..<br /> </li><br /></p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrinhalte </h2>
<div class="float dreifach last">
<ul><li>Analysis (mehrerer Ver&auml;nderlichen) </li><li>Graphentheorie</li><li>Statistik und Wahrscheinlichkeit</li><li>Komplexe Zahlen und</li><li>Fourierreihen.<br /> </li></ul></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrmethoden </h2>
<div class="float dreifach last">
<p>Im Rahmen von Vorlesung, &Uuml;bung Vermittlung der wichtigsten mathematischen Abstraktionstypen (Graphen, Funktionen, algebraischen Strukturen, Zufallsvariablen etc.), der anwendbaren Operationen (Differentiation, Integration, Erwartungswertbildung etc.) und deren praktisches Ein&uuml;ben. Neben den klassischen &Uuml;bungen werden rechner-unterst&uuml;tzte &Uuml;bungen (Praktika) mit Computeralgebrasystemen oder Mathematiksoftware-Paketen (z.B. Maple, Matlab) durchgef&uuml;hrt, in denen die Einbettung mathematischer Techniken in Algorithmen ge&uuml;bt wird (numerische Verfahren, Animationen etc.). Die Aufgabentexte beziehen sich auf Inhalte der Vorlesung und beinhalten Hinweise und L&ouml;sungsmuster, die die Studierenden in Pflichtpr&auml;senz unter tutorieller Betreuung zu einer kompletten L&ouml;sung f&uuml;hren. </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Zus&auml;tzliches </h2>
<div class="float dreifach last">
<p><strong>Begr&uuml;ndung f&uuml;r Aufteilung in klassische &Uuml;bung und rechnerunterst&uuml;tzte &Uuml;bungen:</strong><br /> <br />Eine praxisorientierte Fachhochschulausbildung sollte bei theoretisch gepr&auml;gten Inhalten wie der Mathematik auf eine praktische Ein&uuml;bung des Gelernten Wert legen. Gerade f&uuml;r die Medieninformatik tr&auml;gt die Visualisierung komplexer Zusammenh&auml;nge (Geometrie, Koordinatentransformation, Kinematik und Animation) sowie das interaktive Arbeiten (&Uuml;berpr&uuml;fen von Hypothesen) wesentlich zum Verst&auml;ndnis des Grundlagenstoffes aber auch zum Verst&auml;ndnis technischer Berechnungen in der Medieninformatik sowie der Signalverarbeitung (Bild-und Tonbearbeitung) bei. Hierf&uuml;r eignen sich Software-Pakete wie Maple und MATLAB in besonderer Weise. <br />Rechnerunterst&uuml;tzte &Uuml;bungen mit Pflichtpr&auml;senz erh&ouml;hen den Lernerfolg, da einerseits die Studenten zu vollst&auml;ndiger Teilnahme motiviert werden und andererseits seitens der Lehrenden bei Kleingruppen in optimaler Weise auf Lern- und Verst&auml;ndnisprobleme eingegangen werden kann. Sie bilden damit eine ideale Erg&auml;nzung der klassischen &Uuml;bung, indem sie auf den &Uuml;bungsaufgaben aufsetzen und deren Umsetzung in dem Anwendungsgebiet Medieninformatik praxisnah vermitteln. </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Voraussetzungen </h2>
<div class="float dreifach last">
<p>Keine &uuml;ber die Zulassungsvorausetzungen zum Studium hinausgehende Vorkenntnisse. Gegebenfalls werden die folgenden Inhalte &uuml;ber einen Mathematik-Vorkurs angeglichen: Reelle Zahlen, komplexe Zahlen, Grundbegriffe der Mengenlehre, Relationen und Abbildungen, Zahlenfolgen und -reihen, Kurvendiskussion. </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Ort, Ressourcen </h2>
<div class="float dreifach last">
<p>Rechnerarbeitspl&auml;tze, Computer-Algebra-Software, Internet-­Kommunikations-Plattform </p></div><br style="clear:both" /></div>
