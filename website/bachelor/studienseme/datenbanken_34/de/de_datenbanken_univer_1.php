<h1>Datenbanken </h1>
<div class="zeile">
<div class="float dreifach last">
<p>Pflichtfach<br />5 ECTS-P<br />4 SWS<br />j&auml;hrlich angeboten </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Verantwortlich </h2>
<div class="float dreifach last">
<p><a href="../../../../../website/mi_am_campu/prof/prof_dr_f_9/de/de_prof_dr_f_univer_1.php" rel="pkm9">Prof. Dr. Faeskorn-Woyke</a>, Prof. Dr. Birgit Bertelsmeier </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Ziele </h2>
<div class="float dreifach last">
<p>Die Studierenden sollen <li>&uuml;ber ein einheitliches konsistentes Begriffsgeb&auml;ude bez&uuml;glich der Datenbankthematik verf&uuml;gen</li><li>die theoretischen Grundlagen von Datenbanksystemen am Beispiel relationaler und objekt-relationaler Datenbanksysteme verstanden haben, insbesondere die relationale Algebra, die Normalisierung sowie funktionale Abh&auml;ngigkeiten</li><li>in der Lage sein, diese Erkenntnisse im Rahmen der Modellierung und Implementierung von Datenbankschemata praktisch anzuwenden</li><li>sowie auch komplexere Datenbankanfragen und Datendefinitionen und Daten&auml;nderungen &uuml;ber SQL programmieren zu k&ouml;nnen</li><li>mit dem Transaktionsbegriff, der Mehrbenutzersynchronisation und Verfahren zur Fehlererholung sowie zur Sicherung der Datenintegrit&auml;t vertraut sein </li><br /></p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrinhalte </h2>
<div class="float dreifach last">
<p>Grundbegriffe und Architektur von Datenbanken<br /><br />Ein Vorgehensmodell zur Erstellung eines Datenbanksystems<br /><br />Grundlagen des relationalen Modells: Relationale Algebra, Anfrageoptimierung, Funktionale Abh&auml;ngigkeiten, Datenintegrit&auml;t, Normalisierung<br /><br />Datenmodellierung (Entity Relationship Modell) und Implementierung am Beispiel eines relationalen Datenbanksystems<br /><br />Datenbanksprache SQL: DDL, (auch Multi-Media-Objekte in der Datenbank ), DML, DAL, Constraints<br /><br />Transaktionskonzepte, Mehrbenutzersynchronisation, Fehlererholung und Datensicherheit </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrmethoden </h2>
<div class="float dreifach last">
<p>Vorlesung und begleitendes Praktikum </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Voraussetzungen </h2>
<div class="float dreifach last">
