<div class="float dreifach last">
<p>Vorlesungsunterlagen: Foliensammlung, ausformuliertes Skript, Beispiell&ouml;sungen<br /> <br />Multiple Choice Test zum Inhalt der Vorlesung, ONLINE-SQL-Trainer als E-Learning-Tool, ONLINE-ER-Trainer als E-Learning-Tool, ONLINE-Zugang zur Datenbank ORACLE<br /><br />Case-Tool: ERwin; DB-Programmierung: Oracle-DBS, SQL-Developer<br /></p></div><br style="clear:both" /></div>
