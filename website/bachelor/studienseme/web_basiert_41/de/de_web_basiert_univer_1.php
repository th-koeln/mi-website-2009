<h1>Web-basierte Anwendungen 2: Verteilte Systeme </h1>
<div class="zeile">
<div class="float dreifach last">
<p>Pflichtfach<br />5 ECTS-P<br />4 SWS<br />j&auml;hrlich angeboten </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Verantwortlich </h2>
<div class="float dreifach last">
<p><a href="../../../../../website/mi_am_campu/prof/prof_dr_k_12/de/de_prof_dr_k_univer_1.php" rel="pkm12">Prof. Dr. Kristian Fischer</a> </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Ziele </h2>
<div class="float dreifach last">
<p>Die Studierenden sollen in der Lage sein, alternative Architekturen f&uuml;r Informationssysteme im Web zu identifizieren, deren Eigenschaften zu benennen und sie gegeneinander abzugrenzen. <br />Dazu sollen die Studierenden im Einzelnen: <li>auf der Basis der Kenntnis wesentlicher klassischer Middelewaresysteme die grundlegenden Web Services Protokolle benennen, einordnen und diskutieren k&ouml;nnen, </li><li>eine geeignete Vorgehensweise zum Entwurf eines dienstorientierten Informationssystems im Web w&auml;hlen k&ouml;nnen,</li><li>die praktischen Herausforderungen einer Umsetzung anhand eines eigenen Miniprojekts meistern k&ouml;nnen,</li><li>die Bedeutung der Modellierung von Daten in offenen Umgebungen benennen k&ouml;nnen und die Eigenschaften der Datenmodellierung in XML bzw. in RDF diskutieren k&ouml;nnen,</li><li>die relevanten Standards und Architekturen f&uuml;r stream-orientierte interpersonale Kommunikation im Web kennen und abgrenzen k&ouml;nnen.<br /> </li><br /></p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrinhalte </h2>
<div class="float dreifach last">
<ul><li>Modellierung von Daten f&uuml;r die Kommunikation im Web: Datenkommunikation in offenen Umgebungen; Prinzip von Auszeichnungssprachen; Datenmodellierung mit XML und XML Schemasprachen; Datenmodellierung mit RDF und RDFS </li><li>Infrastruktur und Standards: klassische Middlewaresysteme: RPC,CORBA, nachrichtenorientierte Middleware; Standards und Standardisierung im Internet; quelloffene vs. propriet&auml;re Software</li><li>Dienstorientierte Architektur: Basisstandards: SOAP, WSDL, UDDI; Dienstkomposition; Transaktionen; Entwurfsprinzipen: top-down vs. bottom-up, Koh&auml;sion, Granularit&auml;t</li><li>Kommunikationsarchitekturen: Streaming Protokolle: RTP, RSVP, RTCP; Dienstg&uuml;te; Signalisierungsprotokolle: SIP, H.323; Pr&auml;senzarchitekturen<br /> </li></ul></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrmethoden </h2>
<div class="float dreifach last">
<p>Vorlesung, &Uuml;bung </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Voraussetzungen </h2>
<div class="float dreifach last">
<p>Datenbanken, Softwaretechnik I, Grundlagen von Multimedia Systemen, Grundkenntnisse der objektorientierten Programmierung, Grundkenntnisse der Kommunikationstechnik, Netzbasierte Anwendungen </p></div><br style="clear:both" /></div>
