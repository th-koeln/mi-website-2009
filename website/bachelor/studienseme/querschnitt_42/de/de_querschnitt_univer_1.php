<h1>Querschnittsqualifikationen </h1>
<div class="zeile">
<div class="float dreifach last">
<p>Pflichtfach<br />5 ECTS-P<br />4 SWS<br />j&auml;hrlich angeboten </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Verantwortlich </h2>
<div class="float dreifach last">
<p><a href="../../../../../website/mi_am_campu/prof/prof_dr_k_12/de/de_prof_dr_k_univer_1.php" rel="pkm12">Prof. Dr. Kristian Fischer</a> </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lernziele / Kompetenzen </h2>
<div class="float dreifach last">
<p>Die Studentinnen und Studenten sollen Grundlagen f&uuml;r einen ausgew&auml;hlten Bereich querschnittlicher Qualifikationen durch eine entsprechende Veranstaltung in Bereichen wie Pr&auml;sentation, Moderation, Teamarbeit oder &Auml;hnlichem erwerben.<br /><br />Die Studentinnen und Studenten sollen erste Projekterfahrungen in einem durch sie selbst gesteuerten Projekt erworben haben. </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrinhalte </h2>
<div class="float dreifach last">
<p>Das Modul besteht aus zwei Veranstaltungen:<br /></p><ol><ul><li> Einer Lehrveranstaltung zu querschnittlichen Qualifikationen im Umfang von 2,5 CP. Diese Veranstaltung wird Bewertung und die Bewertung ergibt die Modulnote. Es werden Veranstaltungen aus den Bereichen Pr&auml;sentation, Moderation, Teamarbeit angeboten. In dem ausgew&auml;hlten Bereich sollen die Studierenden Grundlagenwissen erwerben und praktische Erfahrungen erlangen.</li><br /><ul><li> Einem durch die Studentinnen und Studenten in Teamarbeit selbstgesteuerten Projekt im Umfang von 2,5 CP. Dieses Projekt wird nicht benotet sondern vom betreuenden Dozenten oder der betreuenden Dozentin als „bestanden" oder "nicht-bestanden" gewertet.<br /><br />Sehr fr&uuml;hzeitig sollen den Studierenden Kreativit&auml;t, Vorgehensweisen und Erfahrungen in realen Projekten, Teamarbeit und &Uuml;bernahme von Verantwortung vermittelt werden. <br /><br />Die Lehrinhalte werden f&uuml;r den Bereich des Projekts nicht von au&szlig;en vorgegeben, sondern m&uuml;ssen fallspezifisch von den Studierenden in Abstimmung mit dem Mentor selbst definiert und erarbeitet werden. Die Projektthemen m&uuml;ssen keinen fachlichen Bezug haben, sondern k&ouml;nnen allgemeine Projektinhalte haben, wie etwa die Organisation einer Veranstaltung. Die Projekte sollten einen gemeinn&uuml;tzigen und nicht einen kommerziellen Charakter haben, indem sie einen Beitrag etwa f&uuml;r das akademische Leben an der Hochschule, oder f&uuml;r das Leben in anderen Bereichen, etwa an Schulen, Vereinen oder &Auml;hnlichem leistet.</li> </ol><p> </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrmethoden </h2>
<div class="float dreifach last">
<p>Vorlesung, Seminar, Praxisorientierte Projektarbeit unter Verantwortung der Studierenden mit Coaching durch den Mentor </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Voraussetzungen </h2>
<div class="float dreifach last">
<p>Es werden keine &uuml;ber die allgemeinen Zulassungsvoraussetzungen hinausgehenden fachlichen Voraussetzungen gefordert, jedoch sollten Engagement, Motivation, Toleranz und Bereitschaft zur Teamarbeit mitgebracht werden. </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Ort, Ressourcen </h2>
<div class="float dreifach last">
<p>Es stehen nach Absprache alle nutzbaren Infrastrukturkomponenten der Hochschule zur Verf&uuml;gung. Bei Finanzierungsbedarf bem&uuml;ht sich das Institut um entsprechende Budgets. <br />Personell werden die Projekte durch Mentoren aus dem Lehrk&ouml;rper begleitet, die bei der Projektdefinition, bei der Projektabnahme und bei eventueller Krisenintervention t&auml;tig werden. Es wird bewusst auf zu gro&szlig;e Einwirkung der Hochschule auf die Projektgestaltung verzichtet. </p></div><br style="clear:both" /></div>
