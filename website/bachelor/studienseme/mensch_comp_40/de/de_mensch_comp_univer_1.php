<h1>Mensch-Computer Interaktion </h1>
<div class="zeile">
<div class="float dreifach last">
<p>Pflichtfach<br />5 ECTS-P<br />4 SWS<br />j&auml;hrlich angeboten </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Verantwortlich </h2>
<div class="float dreifach last">
<p><a href="../../../../../website/mi_am_campu/prof/prof_dr_g_17/de/de_prof_dr_g_univer_1.php" rel="pkm17">Prof. Dr. Gerhard Hartmann</a> </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Ziele </h2>
<div class="float dreifach last">
<p>Die Studierenden sollen die wesentlichen Ans&auml;tze benutzerorientierter Analyse- und Entwicklungsmethoden kennen und kritisch reflektieren, sowie menschliche, soziale und organisatorische Faktoren ber&uuml;cksichtigen k&ouml;nnen. Dies hei&szlig;t im Einzelnen: theoretische und praktische F&auml;higkeiten in der Entwicklung user­-centered-design orientierter Mensch-Computer-Systeme, Methoden-Kenntnisse bzgl. des user-centered-design- Entwicklungsprozesses, Sensibilisierung f&uuml;r Problemstellungen im Zusammenhang mit situiertem Arbeiten, Ausbildung der F&auml;higkeit zu iterativer Optimierung eines Mensch-Computer-Systems in Projektform und die Bef&auml;higung, computerbasierte Systeme aus Sicht der Anwender zu sehen. </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrinhalte </h2>
<div class="float dreifach last">
<p>Zun&auml;chst werden die Zielsetzungen der Disziplin vorgestellt. Menschliche Faktoren (wie Kognition, Perzeption, mentale Modelle, Ergonomie etc.) und Technologien werden als Bestandteile der Fragestellung nebeneinander gestellt. Nach einem historischen Abriss der Entwicklung der Disziplin werden die wesentlichen Techniken und Methoden von MCI vermittelt. Dazu z&auml;hlen Grundlagen eines benutzerzentrierten Systemdesigns, Modelle des benutzerzentrierten Design-Prozesses, Aufgaben-Analyse, Sammeln von Systemanforderungen und Software-Entwicklungsmethoden. Anschlie&szlig;end werden Fragestellungen von Evaluation, Prototyping und usability-testing diskutiert. <br />Im zweiten Teil werden nationale und internationale Normen (DIN-Norm 66234, ISO-Norm 9241) vorgestellt; web-usability kritisch diskutiert und spezielle Themen der visuellen und auditiven Wahrnehmung vertieft. Spezielle Methoden und Techniken des user-centered-engineerings (Persona-Konzepte und Szenarien) werden. Dar&uuml;ber hinaus werden empirische Methoden der Datenerhebung und Messung empirischer Zusammenh&auml;nge (Operationalsierung, Indikatorwahl, Datenerhebungsverfahren etc.) detaillierter vorgestellt. Exemplarisch werden aktuelle Forschungsergebnisse vorgestellt und kritisch diskutiert. </p></div><br style="clear:both" /></div>
