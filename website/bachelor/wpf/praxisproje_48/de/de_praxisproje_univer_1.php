<h1>Praxisprojekt </h1>
<div class="zeile">
<div class="float dreifach last">
<p>Pflichtfach<br />15 ECTS-P<br />6 SWS<br />jedes Semester </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Verantwortlich </h2>
<div class="float dreifach last">
<p><a href="../../../../../website/mi_am_campu/prof/prof_dr_k_12/de/de_prof_dr_k_univer_1.php">Prof. Dr. Kristian Fischer</a> </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Ziele </h2>
<div class="float dreifach last">
<p>Die Studierenden <li>k&ouml;nnen Methoden und Techniken, die sie im Studium erlernt haben, in realit&auml;tsnahen Projekten weit gehend selbstst&auml;ndig anwenden, </li><li>kennen Techniken wissenschaftlichen Arbeitens,</li><li>haben erste Erfahrungen mit aktiver englischsprachiger Fachkommunikation gesammelt,</li><li>haben erste Erfahrungen mit der Selbststeuerung und proaktiven Kommunikation in einem Projekt mittlerer Gr&ouml;&szlig;e und der Einordnung von Projektarbeit in betriebliche, gesellschaftliche und rechtliche Rahmenbedingungen gesammelt und</li><li>k&ouml;nnen eigene Projektergebnisse vor einem Fachpublikum in Vortrag und Diskussion darstellen und verteidigen.<br /> </li><br /></p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrmethoden </h2>
<div class="float dreifach last">
<p>Das Modul besteht aus einem Projektteil und einem Seminarteil.<br /><br />Im Projektteil werden <li>Modulinhalte des ersten bis f&uuml;nften Semesters anhand von realen Anforderungen in einem praxisrelevanten Kontext angewendet und</li><li>Und der Studierende durch die Betreuung des Dozenten an eine selbstst&auml;ndige Projektdurchf&uuml;hrung und Kommunikation herangef&uuml;hrt. </li></ul>Der kann entweder in einem Unternehmen oder in der Hochschule - dann eingebettet in Forschungsprojekte - erfolgen.<br /><br /><br />Der Seminarteil besteht aus <li>Veranstaltungen in denen Techniken wissenschaftlichen Arbeitens vermittelt werden,</li><li>Vortr&auml;gen und Diskussionen der Studierenden in englischer Sprache, in denen der Schwerpunkt auf einer Sensibilisierung f&uuml;r englischsprachige Fachkommunikation liegt,</li><li>Fachvortr&auml;gen von Studierenden &uuml;ber Ihre Projektergebnisse.<br /> </li><br /></p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Vorraussetzungen </h2>
