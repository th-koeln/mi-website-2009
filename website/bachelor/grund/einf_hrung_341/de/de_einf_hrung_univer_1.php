<h1>Einf&uuml;hrung in die Medieninformatik </h1>
<div class="zeile">
<div class="float dreifach last">
<p>Pflichtfach<br />5 ECTS-P<br />4 SWS<br />j&auml;hrlich angeboten </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Verantwortlich </h2>
<div class="float dreifach last">
<p><a href="../../../../../website/mi_am_campu/prof/prof_dr_g_17/de/de_prof_dr_g_univer_1.php">Prof. Dr. Gerhard Pla&szlig;mann</a>, <a href="../../../../../website/mi_am_campu/prof/prof_dr_k_12/de/de_prof_dr_k_univer_1.php">Prof. Dr. Kristian Fischer</a>, <a href="../../../../../website/mi_am_campu/prof/prof_hans_11/de/de_prof_hans_univer_1.php">Prof. Hans Kornacher</a>, <a href="../../../../../website/mi_am_campu/prof/prof_chris_5/de/de_prof_chris_univer_1.php">Prof. Christian Noss</a> </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Ziele </h2>
<div class="float dreifach last">
<p>Die Studierenden k&ouml;nnen die inhaltlichen Ausrichtungen und die Zielsetzungen der Lehr- und Anwendungsdisziplin Medieninformatik benennen und gegen&uuml;ber verwandten oder &auml;hnlichen Disziplinen abgrenzen. Sie kennen Definitionen des Medienbegriffs, k&ouml;nnen verschiedene Medientheorien benennen, erl&auml;utern und kritisch diskutieren. Sie k&ouml;nnen einen historischen Abriss der medientechnischen Entwicklung geben und in Bezug auf gesellschaftliche, soziale und &ouml;konomische Fragestellungen diskutieren.<br /><br />Die Studierenden kennen Grundkonzepte der Informatik sowie audiovisueller und interaktiver Medientechnologien, kennen architekturelle Alternativen interaktiver Systeme und kennen Gestaltungsdimensionen f&uuml;r deren Informations-, Kommunikationsinhalte.<br /><br />Die Studierenden sind sensibilisiert f&uuml;r Modellierungs- und Entwicklungsaufgaben von medienbasierten Software-Systemen zur Unterst&uuml;tzung menschlichen Handelns in betrieblichen und privaten Kontexten.<br /></p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrinhalte </h2>
<div class="float dreifach last">
<p>Begriffsbestimmung f&uuml;r Informatik, Medien, Medieninformatik. Darstellung von Themen- und Anwendungsgebieten des Lehr- und Forschungsbereichs der Medieninformatik. Darstellung verschiedener Medientheorien. Historischer Abriss der Medientechnik im Kontext gesellschaftlich-&ouml;konomischer Perspektiven.<br /><br />Grundlagen audiovisueller Medientechnologien. Gestaltungsdimensionen medienbasierter Systeme und „g&auml;ngige" Auspr&auml;gungen. Grunds&auml;tze der Modellbildung und deren Anwendungsbezug zu Softwaresystemen, computergest&uuml;tzten menschlichen Handeln in relevanten Kontexten (&ouml;konomisch, sozial etc.). Repr&auml;sentation von „realen und virtuellen Welten" in interaktiven Systemen.<br /></p></div><br style="clear:both" /></div>
