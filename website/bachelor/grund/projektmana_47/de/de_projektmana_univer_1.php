<h1>Projektmanagement </h1>
<div class="zeile">
<div class="float dreifach last">
<p>Pflichtfach<br />5 ECTS-P<br />4 SWS<br />j&auml;hrlich angeboten </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Verantwortlich </h2>
<div class="float dreifach last">
<p>Prof. Dr. Holger G&uuml;nther </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Ziele </h2>
<div class="float dreifach last">
<p>Die Studierenden sollen bef&auml;higt werden, <li>die grundlegenden Aufgaben des Projektmanagements, insb. in IT-Projekten, zu charakterisieren und durchzuf&uuml;hren;</li><li>die Projektmanagement-Methoden, -Techniken und -Werkzeuge zielgerichtet einzusetzen;</li><li>die erforderlichen soziologischen und kommunikativen Aspekte zu ber&uuml;cksichtigen, insb. mit dem Ziel einer menschengerechten und soziologisch fundierten Menschenf&uuml;hrung zur Erreichung einer wirklichen und optimalen Produktivit&auml;t bei komplexen Projekten.<br /> </li><br /></p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrinhalte </h2>
<div class="float dreifach last">
<p>Das Modul befasst sich mit den Managementaspekten der professionellen Entwicklung gro&szlig;er Softwaresysteme. <br />Der Vorlesungsteil des Moduls gliedert sich in folgende Kapitel: <li>&Uuml;berblick - Warum Projektmanagement?;</li><li>Teamarbeit und Menschenf&uuml;hrung (Kommunikation und F&uuml;hrung);</li><li>Kosten/Nutzen-Analysen und Entscheidungstechniken;</li><li>Projektorganisation und Projektplanung (Aufbauorganisation, Ablauforganisation, Prozessmodellierung, Netzplantechnik);</li><li>Detaillierte Aufwandssch&auml;tzung und Projektcontrolling (Function Point Analysis, COCOMO, Risikomanagement, Projektpr&auml;sentationen);</li><li>Inhalte PM-BOK (Project Management - Body of Knowledge); </li><li>Zusammenfassung und Pr&uuml;fungsvorbereitung; </li></ul>Damit die Studierenden die vorgestellten Methoden und Techniken zum Management von Softwareprojekten anwenden sowie besser analysieren und bewerten k&ouml;nnen, werden im Praktikum die in der Vorlesung vermittelten Inhalte in Teams anhand eines Fallbeispiels eingesetzt. Dazu bilden die Teilnehmenden Teams zu jeweils 6 Studierenden. Im Praktikum werden folgende Bereiche vertieft: <li>Kosten- Nutzenrechnung, Entscheidungstechniken;</li><li>Aufbauorganisation;</li><li>Aufwandssch&auml;tzung (Function-Point-Analyse, COCOMO); </li><li>Ablauf- und Ressourcenplanung (Netzplantechnik, Einsatz von PM-Software wie z. B. MS-Project);</li><li>Risikomanagement.<br /> </li><br /></p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrmethoden </h2>
<div class="float dreifach last">
<p>Vorlesung, Praktikum, Seminar </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Vorraussetzungen </h2>
<div class="float dreifach last">
<p>Grundlegende Kenntnisse der Organisationsformen von Unternehmen<br /><br />Grundlegende EDV-Kenntnisse sowie Grundlagen-Kenntnisse &uuml;ber SW-Erstellungsprozesse </p></div><br style="clear:both" /></div>
