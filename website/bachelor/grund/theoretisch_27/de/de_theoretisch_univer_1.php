<h1>Theoretische Informatik I und II </h1>
<div class="zeile">
<div class="float dreifach last">
<p>Pflichtfach<br />10 ECTS-P<br />8 SWS<br />j&auml;hrlich angeboten </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Verantwortlich </h2>
<div class="float dreifach last">
<p><a href="../../../../../website/mi_am_campu/prof/prof_dr_m_594/de/de_prof_dr_m_univer_1.php" rel="pkm594">Prof. Dr. Martin Eisemann</a> </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Ziele </h2>
<div class="float dreifach last">
<ul><li>Die Studierenden sollen den in der Vorlesung dargestellten theoretischen Stoff durch erg&auml;nzende selbst&auml;ndige Literaturarbeit und an anderen Beispielen aufarbeiten. </li><li>Aufgaben zu den Lehrinhalten(s.u.) werden in kleinen Gruppen (Teamarbeit) selbst&auml;ndig gel&ouml;st. Die L&ouml;sungen sollen in den &Uuml;bungsstunden vorgetragen erden und der L&ouml;sungsweg den Kommilitonen hierbei erl&auml;utert werden.</li><li>Zum Abschluss muss jeder Studierende zum erfolgreichen Abschluss des Faches in der Lage sein, entsprechende Problemstellungen selbstst&auml;ndig zu l&ouml;sen. </li></ul></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrinhalte </h2>
<div class="float dreifach last">
<p>Dieses Grundlagenfach zur Informatik ersreckt sich &uuml;ber zwei Semester. </p><ul><li>Grundlagen: Mengen, Relationen, Graphen, Polynome; Zahlensysteme, Zahlendarstellung, Numerische Aspekte; Codierung, Informationstheorie </li><li>Logik und Boolesche Algebra: Aussagenlogik; Pr&auml;dikatenlogik; Boolesche Algebra, Schaltnetze und Schaltwerke</li><li>Regul&auml;re (Typ-3) Sprachen: Endliche Automaten, Regul&auml;re Ausdr&uuml;cke; Typ3-Grammatiken, Syntaxdiagramme; Chomsky-Hierarchie</li><li>Modellierung sequentieller und paralleler (Ausgabe-) Prozesse: Endliche Maschinen, Berechnungen; Automatennetze, Petri-Netze</li><li>Kontextfreie (Typ-2) Sprachen: Kontextfreie Grammatiken, Chomsky- und Greibach-Normalform; Kellerautomaten; Anwendungen (Ableitungs- und Syntaxb&auml;ume, Syntax von Programmiersprachen, Backus-Naur-Form)</li><li>Kotextsensitive (Typ-1) und rekursiv aufz&auml;hlende (Typ-0) Sprachen: Grammatiken, Monotonie, Normalform; Turingautomaten; Einf&uuml;hrung in die Begriffe: Berechenbarkeit, Entscheidbarkeit und Komplexit&auml;t </li></ul></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrmethoden </h2>
<div class="float dreifach last">
<p>Vorlesungen (2 SWS) und &Uuml;bungen (2 SWS) pro Semester </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Vorraussetzungen </h2>
<div class="float dreifach last">
<p>Keine &uuml;ber die Zulassungsvorraussetzungen hinausgehenden Vorraussetzungen </p></div><br style="clear:both" /></div>
