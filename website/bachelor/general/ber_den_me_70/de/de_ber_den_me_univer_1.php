<h1>&Uuml;ber den Medieninformatik Bachelorstudiengang </h1>
<div class="zeile">
<h2 class="float einfach">Was ist der Medieninformatik Bachelor? </h2>
<div class="float dreifach last">
<p>Der Bachelor Studiengang Medieninformatik f&uuml;hrt zum Abschluss Bachelor of Science und hat eine <strong>Regelstudienzeit</strong> von <strong>6 Semestern</strong>. Er gliedert sich in zwei Teile: <strong>Grund- und Hauptstudium</strong>, wobei das Hauptstudium aus einem Pflichtfachteil und einem Wahlpflicht- und Projektteil besteht. Eine Auflistung der einzelnen Module findet sich weiter unten auf dieser Seite. <br /><br />Im Bachelor-Studium der Medieninformatik werden drei Ziele verfolgt: </p><ul><li> der Aufbau von Kommunikations- und Methodenkompetenz </li><li> die Vermittlung eines umfassenden Technologieverst&auml;ndnisses</li><li> das Kennenlernen von Gesch&auml;ftsprozessen und Kernaktivit&auml;ten der Medienwirtschaft </li></ul></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach">Was lernt man im Studium? </h2>
<div class="float dreifach last">
<p><strong>Absolventinnen und Absolventen des Bachelorstudiengang ...</strong><br /><br />... arbeiten h&auml;ufig in Teams aus unterschiedlichen Fachgebieten, z.B. mit Betriebswirtschaftlern, Designern, Tontechnikern, Redakteuren usw. Im Studium werden Vokabular und Sichtweisen der verschiedenen Disziplinen genauso vermittelt, wie Vokabular und Methoden der Medieninformatik selbst, die in F&auml;chern wie z.B. Softwaretechnik, Interaction Design und Projektmanagement gelehrt werden. Die eigenst&auml;ndige Durchf&uuml;hrung von Projekten und die so genannten Querschnittsqualifikationen sind wesentlicher Bestandteil des Studiums. Letztere beinhalten Themen wie Teamarbeit, Rhetorik, Moderation und Pr&auml;sentation sowie Selbstentwicklungskompetenzen, F&auml;higkeiten, die neben der fachlichen Qualifikation f&uuml;r den beruflichen Erfolg unabdingbar sind.<br /><br />... k&ouml;nnen die Architektur von Systemen analysieren und gestalten und Methoden, Techniken und Tools verstehen und anwenden. Sie m&uuml;ssen die Grundlagen von Betriebssystemen, Kommunikationsnetzen, Web Anwendungen, von Mediengestaltung, -bearbeitung, -speicherung und -distribution beherrschen, wozu auch Grundlagenf&auml;cher wie Mathematik und Theoretische Informatik notwendig sind. Und sie m&uuml;ssen - wie alle Informatikerinnen und Informatiker - nat&uuml;rlich programmieren k&ouml;nnen und algorithmisch denken lernen.<br /><br />... verstehen das Gesch&auml;ft der Unternehmen, in denen sie arbeiten werden. Dazu erwerben sie Grundlagenwissen in Wirtschaftswissenschaften, m&uuml;ssen die Gesch&auml;ftsprozesse und die informatischen und kreativen Prozesse bei der Mediengestaltung, -produktion, -distribution und -nutzung verstehen und eignen sich Wissen &uuml;ber rechtliche und gesellschaftliche Aspekte des Gesch&auml;fts an. </p></div>
<div class="clear">&nbsp;<br /></div></div>
