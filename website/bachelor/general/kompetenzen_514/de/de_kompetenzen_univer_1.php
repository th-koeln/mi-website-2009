<h2 class="float einfach">Pr&auml;sentations- und Kommunikationskompetenz: </h2>
<div class="float dreifach last">
<p>Die Studierenden erlangen Wissen &uuml;ber Nutzen, Gefahren, Risiken, Grenzen und rechtliche Vorgaben &uuml;ber den Einsatz von Informationstechnologien, &Ouml;konomieverst&auml;ndnis, sowie die F&auml;higkeit zur effektiven und effizienten Kommunikation und zur Teamarbeit. Die Studierenden erwerben Wissen bzgl. kultureller Rahmenbedingungen menschlichen Handelns, kennen Konzepte der Ethik und k&ouml;nnen diese handlungsleitend integrieren.<br /></p></div>
