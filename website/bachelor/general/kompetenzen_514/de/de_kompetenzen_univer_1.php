<h1>Kompetenzen </h1>
<div class="zeile">
<h2 class="float einfach">Kompetenzen </h2>
<div class="float dreifach last">
<p>Unter Kompetenz verstehen wir das komplexe und zielorientierte Zusammenspiel von (Fach-)Wissen, F&auml;higkeiten, Fertigkeiten und (professionelle) Zust&auml;ndigkeiten, &uuml;ber die eine handelnde Person verf&uuml;gt um eine Problemstellung effizient zu l&ouml;sen. </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach">Formale, algorithmische, mathematische Kompetenzen: </h2>
<div class="float dreifach last">
<p>Die Studierenden besitzen profundes Wissen und Verst&auml;ndnis &uuml;ber die Prinzipien der Informatik und insbesondere der Medieninformatik; sie erlangen die F&auml;higkeit zum analytischen Denken, zur Abstraktion, zur Konzeption von Modellen, zur Beurteilung von Unterschieden zwischen Modell und Realit&auml;t sowie in formalen Welten, auch unter Auswahl und Anwendung geeigneter mathematischer Konstrukte zu operieren.<br /></p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<div class="clear">&nbsp;<br /></div></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach">Analyse-, Design-, Realisierungs- und Evaluierungskompetenz: </h2>
<div class="float dreifach last">
<p>Die Studierenden sind f&auml;hig, Problemstellungen aus neuen und in der Entwicklung begriffenen Bereichen der Medieninformatik zu analysieren, in Teilprobleme zu zerlegen, L&ouml;sungsans&auml;tze zu formulieren, zu formalisieren und unter Verwendung der Prinzipien, Konzepte, Methoden und Arbeitstechniken der Informatik zu l&ouml;sen. Sie erlangen die F&auml;higkeit, komplexe Systeme zu analysieren, im Team zu konzipieren, zu realisieren, zu testen und zu evaluieren.<br /></p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<div class="clear">&nbsp;<br /></div></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach">Medienkompetenz: </h2>
<div class="float dreifach last">
<p>Die Studierenden k&ouml;nnen eine Perspektive der Medienkonzeption einnehmen, haben eine mediengestalterische Grundkompetenz entwickelt und sind in der Lage, bzgl. der Kommunikationsziele eine geeignete Medienauswahl zu treffen.<br />Sie k&ouml;nnen organisationale, soziale und kulturelle Kontexte, Vorgaben und Regeln eigenst&auml;ndig erschlie&szlig;en, analysieren, definieren und unter Ber&uuml;cksichtigung weiterer fachlicher Perspektiven angemessene Gestaltungsziele formulieren. Sie kennen die Gestaltungsdimensionen von Medien und besitzen aktive Vokabularien zur Beschreibung und Realisierung angemessener Konzeptionen. Die Studierenden k&ouml;nnen die Realisationen bez&uuml;glich der Zielsetzungen kritisch diskutieren. </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<div class="clear">&nbsp;<br /></div></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach">Praktische und theoretische Methodenkompetenz: </h2>
<div class="float dreifach last">
<p>Die Studierenden k&ouml;nnen Wissen aus den Bereichen Kerninformatik, Medientechnik, Web-Technologien und angrenzenden Wissenschaften kombinieren und mit Komplexit&auml;t umgehen.<br />Sie erlangen die F&auml;higkeit zu methodischem Vorgehen, zur begr&uuml;ndeten Auswahl und der angemessenen Durchf&uuml;hrung von Arbeits- und Dokumentationstechniken.<br />Die Studierenden haben die F&auml;higkeit zur kritischen Reflexion &uuml;ber und begr&uuml;ndeten Auswahl aus (methodischen, arbeits- und dokumentationstechnischen) Alternativen. </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<div class="clear">&nbsp;<br /></div></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach">&Ouml;konomische Kompetenz: </h2>
<div class="float dreifach last">
<p>Die Studierenden k&ouml;nnen medieninformatische Problem- und Aufgabenstellungen aus betriebswirtschaftlichen Perspektiven betrachten und l&ouml;sen. </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<div class="clear">&nbsp;<br /></div></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach">Fachliche, konstruktive Probleml&ouml;sungskompetenz: </h2>
<div class="float dreifach last">
<p>Die Studierenden erlangen die F&auml;higkeit, algorithmische Entwurfs-, erfolgreiche Benutzungs-, zielf&uuml;hrende Gestaltungs- und angemessene Systemarchitektur-Muster zu erkennen, anzuwenden und kritisch einzuordnen.<br />Die Studierenden beherrschen der Methoden und Arbeitstechniken der Medien-Informatik.<br />Die Studierenden k&ouml;nnen Problem- und Aufgabenstellungen aus informatischer, &ouml;konomischer und sozialer Perspektive betrachten und l&ouml;sen. Die Studierenden k&ouml;nnen sich eigenst&auml;ndig in neue Themenbereiche einarbeiten und das erworbene Wissen effizient in die L&ouml;sung aktueller Frage- und Problemstellungen einbringen und anwenden. </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<div class="clear">&nbsp;<br /></div></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach">Fach&uuml;bergreifende Kompetenzen: </h2>
<div class="float dreifach last">
<p>Die Studierenden erlangen die F&auml;higkeit zur Einarbeitung in informatikfremde Sachverhalte und technologische Probleml&ouml;sungsmethoden. </p></div>
<div class="clear">&nbsp;<br /></div></div>
