<h1>Netzbasierte Anwendungen </h1>
<div class="zeile">
<div class="float dreifach last">
<p>Pflichtfach<br />5 ECTS-P<br />4 SWS<br />j&auml;hrlich angeboten </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Verantwortlich </h2>
<div class="float dreifach last">
<p><a href="../../../../../website/mi_am_campu/prof/prof_chris_5/de/de_prof_chris_univer_1.php">Prof. Christian Noss</a> </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach"><br class="spacer" /> </h2>
<div class="float dreifach last">
<p>Ich google also bin ich. Was die bestbesuchte Suchmaschine der Welt nicht &#64257;ndet, scheint f&uuml;r viele schlichtweg nicht existent, denn das Medium Internet ist elementarer Bestandteil unseres Alltags: es wird gemailt, gechattet, geskyped, gegoogled, gebloggt und gesurft. Wir wickeln Bankgesch&auml;fte &uuml;ber das Web ab, buchen Reisen und Konzertkarten, kaufen B&uuml;cher, schauen wann die n&auml;chste Pr&uuml;fungsphase ist uvm. Doch was steckt dahinter? </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Ziele </h2>
<div class="float dreifach last">
<p>Die Studierenden sollen die technischen Grundlagen des Internet sowie die wesentlichen Meilensteine bei der Entwicklung desselben kennen. Eigene Projekte sollen unter Einsatz von (X)HTML, XML, CSS und Javascript konzipiert und realisiert werden k&ouml;nnen. Grunds&auml;tzliche <br />Begri&#64256;e, Konzepte und Architekturen netzbasierter Anwendungen sollen verstanden werden. </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrinhalte </h2>
<div class="float dreifach last">
<ul><li>technische Grundlagen und Geschichte </li><li>Klassen web-basierter Anwendungen</li><li>(X)HTML, CSS und Javascript</li><li>XML, DTD und XSL</li><li>Web 2.0, Ajax und Co.</li><li>Sicherheit</li><li>Information, Navigation, Transaktion</li><li>Barrierefreiheit</li><li>Serverseitige Technologien </li></ul></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrmethoden </h2>
<div class="float dreifach last">
<p>Vorlesung mit eingebetteten &Uuml;bungselementen. Begleitendes Praktikum. </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Vorraussetzungen </h2>
