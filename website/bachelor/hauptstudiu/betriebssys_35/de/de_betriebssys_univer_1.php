<h1>Betriebssysteme und verteilte Systeme </h1>
<div class="zeile">
<div class="float dreifach last">
<p>Pflichtfach<br />5 ECTS-P<br />4 SWS<br />j&auml;hrlich angeboten </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Verantwortlich </h2>
<div class="float dreifach last">
<p><a href="../../../../../website/mi_am_campu/prof/prof_dr_h_81/de/de_prof_dr_h_univer_1.php">Prof. Dr. Horst Stenzel</a> </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Ziele </h2>
<div class="float dreifach last">
<p>Die Studierenden sollen <li>die Prinzipien und Mechanismen von Betriebssystemen und verteilten Systemen am Beispiel von UNIX verstanden haben, 	</li><li>in der Lage sein, selbstst&auml;ndig Systemprogramme zu schreiben und Betriebssystemstrukturen zu bewerten und 	</li><li>die Mechanismen zur Implementierung verteilter Anwendungen anwenden k&ouml;nnen. </li><br /></p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrinhalte </h2>
<div class="float dreifach last">
<p>Systemprogrammierung am Beispiel von UNIX: Shell-Programmierung, Prozess-Modelle, Prozess-Erzeugung und Synchronisation, UNIX-Prozesse und elementare Synchronisation, Pipes, Shared Memory, Synchronisationsprimitive f&uuml;r den wechselseitigen Ausschluss, Semaphore, Nachrichtenwarteschlangen, Dateisysteme, TCP/IP, Sockets, Remote Procedure Call, Strategien zum Scheduling und zur Speicherverwaltung, Klassische Synchronisationsprobleme. </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrmethoden </h2>
<div class="float dreifach last">
<p>Vorlesung, &Uuml;bung, Praktikum </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Vorraussetzungen </h2>
<div class="float dreifach last">
<p>Kenntnisse und Umnang mit formalen Systemen, sicherer Umgang mit C, Grundkenntnisse in UNIX </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Ort, Ressourcen </h2>
<div class="float dreifach last">
<p>Vorlesungsunterlagen: Foliensammlung, ausformuliertes Skript, Beispiell&ouml;sungen<br /><br />Software: C-Compiler und UNIX </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Literatur </h2>
<div class="float dreifach last">
<p>Tanenbaum, A. S.: Moderne Betriebssysteme<br /><br />Brown, C.: Programmieren verteilter UNIX-Anwendungen<br /><br />Kernighan, B. W., Pike, R.: Der UNIX-Werkzeugkasten<br /><br />Ehses, K&ouml;hler, Stenzel, Victor: Betriebssysteme: Ein Lehrbuch mit &Uuml;bungen zur Systemprogrammierung in UNIX/Linux </p></div><br style="clear:both" /></div>
