<div class="float dreifach last">
<p>Die Studierenden erhalten einen &Uuml;berblick &uuml;ber die Fragestellungen der Allgemeinen Betriebswirtschaftslehre. Sie lernen die zentralen Handlungsfelder von Unternehmen kennen - von konstitutiven, strategischen Entscheidungen bis hin zur Planung und Steuerung der Tagesgesch&auml;fte. Die Studierenden sollen das System betrieblicher Prozesse und Strukturen in den Grundz&uuml;gen verstehen und auch in den gesamtwirtschaftlichen Rahmen einordnen k&ouml;nnen. </p></div><br style="clear:both" /></div>
