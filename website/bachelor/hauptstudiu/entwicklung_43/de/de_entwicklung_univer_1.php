<h1>Entwicklungsprojekt interaktive Medien </h1>
<div class="zeile">
<div class="float dreifach last">
<p>Pflichtfach<br />10 ECTS-P<br />6 SWS<br />halbj&auml;hrlich angeboten </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Verantwortlich </h2>
<div class="float dreifach last">
<p><a href="../../../../../website/mi_am_campu/prof/prof_dr_g_17/de/de_prof_dr_g_univer_1.php">Prof. Dr. Gerhard Pla&szlig;mann</a>, <a href="../../../../../website/mi_am_campu/prof/prof_dr_k_12/de/de_prof_dr_k_univer_1.php">Prof. Dr. Kristian Fischer</a> </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Ziele </h2>
<div class="float dreifach last">
<p>Die Studierenden sollen vertiefende Kenntnisse in die Methoden und Techniken bzgl. Entwicklung und Optimierung <li>Mensch-Computer-basierter Systeme</li><li>Multimedialer Anwendungen </li></ul>erlangen, eigene Erfahrungen in der Projektabwicklung mit Medieninformatik-spezifischen Fragestellungen sammeln und eine reflektierend-kritische Haltung zu methodischen Ans&auml;tzen und Entwicklungsmodellen entwickeln. Ziel ist eine mit eigenen praktischen Erfahrungen fundierte Methodenkompetenz. </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrinhalte </h2>
<div class="float dreifach last">
<p>Die Studierenden bearbeiten ein Entwicklungsprojekt aus den Bereichen: <li>Mensch-Computer Interaktion</li><li>Multimediale Anwendungen </li></ul>Aus dem Methoden- und Technikkatalog (siehe Vorlesungen zu den Lehrbereichen) wird in Absprache mit dem Lehrenden eine Auswahl der einzusetzenden Entwicklungstechniken und -methoden sowie der einzuhaltenden Entwicklungsmodelle getroffen. Qualit&auml;tssicherungsma&szlig;nahmen und Prozessmanagement werden eingesetzt. Bei der Bearbeitung von MCI-Fragestellungen wird ein mehrstufiger Iterationsprozess durchlaufen. </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrmethoden </h2>
