<h1>Computergrafik und Animation </h1>
<div class="zeile">
<div class="float dreifach last">
<p>Pflichtfach<br />5 ECTS-P<br />4 SWS<br />j&auml;hrlich angeboten </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Verantwortlich </h2>
<div class="float dreifach last">
<p><a href="../../../../../website/mi_am_campu/prof/prof_dr_h_81/de/de_prof_dr_h_univer_1.php">Prof. Dr. Horst Stenzel</a> </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Ziele </h2>
<div class="float dreifach last">
<p>Die Studierenden sollen in der Lage sein, 2-und 3-dimensionale Strukturen zu modellieren und zu animieren, und die grundlegenden Algorithmen und Datenstrukturen der Computergrafik beherrschen.<br /><br />Die Interaktion mit Softwaresystemen erfolgt fast ausschlie&szlig;lich &uuml;ber grafische Anwenderschnittstellen, so dass der Umsetzung abstrakter Konzepte in visuelle Strukturen und Abl&auml;ufe gro&szlig;e Bedeutung zukommt. Die allgemeine Verf&uuml;gbarkeit m&auml;chtiger grafischer APIs, Anwendungstools und GPUs macht es nicht mehr erforderlich, dass die Studierenden grundlegende Algorithmen selber umsetzen und programmieren k&ouml;nnen, so dass das Schwergewicht auf die Funktionsweise und das Verst&auml;ndnis der Prinzipien gelegt werden kann. </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Lehrmethoden </h2>
<div class="float dreifach last">
<p>Vorlesung mit eingebetteten &Uuml;bungselementen und Einsatz von interaktiven multimedialen Lernelementen; Praktikum, in dem Stoff aus der Vorlesung mit Hilfe von Java-2D und -3D zur eigenst&auml;ndigen L&ouml;sung von Anwendungsaufgaben eingesetzt werden muss. </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Vorraussetzungen </h2>
<div class="float dreifach last">
<p>Mathematische Grundlagen (lineare Algebra und Analysis) aus dem Grundstudium, algorithmische Grundlagen, Objektorientierte Programmierung, Datenstrukturen, sicherer Umgang mit Java und Eclipse </p></div><br style="clear:both" /></div>
<div class="zeile">
<h2 class="float einfach">Ort, Ressourcen </h2>
<div class="float dreifach last">
<p>Quellensammlung aus dem Internet, Vorlesungsskript, Projekt Grafiti der Uni Oldenburg </p></div><br style="clear:both" /></div>
<div class="zeile">
