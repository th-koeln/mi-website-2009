<div class="float dreifach last">
<p><strong>virtuelles Museum // K&ouml;lner Sport</strong> Auf der Suche nach einer angemessenen und innovativen Vermittlungsform des Themas Sportgeschichte, die gleicherma&szlig;en anschaulich, ansprechend und wissenschaftlich fundiert ist, bietet das virtuelle Museum eine neue M&ouml;glichkeit. Der Begriff des Museums ist dabei bewusst gew&auml;hlt, um die Wertigkeit bei der Vermittlung von Geschichte hervorzuheben. Mit dieser au&szlig;ergew&ouml;hnlichen Sicht auf den Sport einer Stadt wird eine Pr&auml;sentationsplattform geschaffen, bei der eine Verbindung zwischen historischen Fakten, visueller Kommunikation und emotionalem Erleben hergestellt wird. Diese Art der Informationsvermittlung bietet neue Erlebnisr&auml;ume, die Jugendliche und Erwachsene gleicherma&szlig;en ansprechen kann. Die dargestellten Inhalte sollen mit dem aktuellen Sportgeschehen im K&ouml;lner Breiten- und Leistungssport verkn&uuml;pft werden. Auf diese Weise wird ein Interaktionsraum geschaffen, der einerseits zur aktiven Teilnahme an der Gestaltung des virtuellen Museums auffordert, andererseits durch die Einbindung des realen Sportangebots sowie spielerischer Elemente zur k&ouml;rperlichen Bet&auml;tigung animiert. <br /><br />Das interdisziplin&auml;re Projekt wird &uuml;ber den Zeitraum von April 2008 - M&auml;rz 2010 von der RheinEnergie - Stiftung finanziell gef&ouml;rdert. <br /><br /><a href="http://mims03.gm.fh-koeln.de/wpmu/virtuelles_museum/uber-dieses-projekt/">Website virtuelles Museum</a> </p></div>
