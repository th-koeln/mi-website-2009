<p><strong>Ein Tag an dem man vorbeischauen sollte.</strong> Am Anfang der Vorlesungszeit wird das neue Studienjahr mit dem Showcase eingeleitet. <br /><br />Der Showcase beginnt mit einer Einf&uuml;hrungsrede von Herrn Noss. Anschlie&szlig;end werden zwei Vortr&auml;ge von externen Sprechern gef&uuml;hrt, die selbst einmal am Campus Gummersbach studiert haben. Dabei handelt es sich einerseits um Frau Hofmann, welche das Thema FrontEnd-Development behandelt und zum Anderen um Herrn Gehle, welcher &uuml;ber das Leben als IT-Leiter berichtet. Im Anschluss daran werden ausgew&auml;hlte Filme vorgef&uuml;hrt, die im letzten Jahr in der Veranstaltung <a href="../../../../../website/bachelor/studienseme/audiovisuel_395/de/de_audiovisuel_univer_1.php" rel="pkm395">Audiovisuelles Medienprojekt</a> und in WPF's entstanden sind. Dazu werden zwei Slots angeboten, damit w&auml;hrenddessen die besten Projekte des letzten Studienjahres in einer Ausstellung angesehen werden k&ouml;nnen. Durch Abstimmung wird das Team mit der besten Pr&auml;sentation bzw. dem besten Poster ausgew&auml;hlt und um 17 Uhr zum Sieger gek&uuml;rt.<br /><br />F&uuml;r die Studierenden der Medieninformatik ist der Showcase ein Pflichttermin. F&uuml;r Studieninteressierte und Kooperationspartner ist diese Veranstaltung eine gute Gelegenheit einen Blick hinter die Kulissen des Lehr- und Forschungsgebiets zu werfen.<br /><br />&Uuml;brigens: F&uuml;r alle, die noch auf der Suche nach m&ouml;glichen Themen f&uuml;r ein Projekt oder eine Abschlussarbeit sind, sei der Showcase als Inspirationsquelle empfohlen. Hier bekommt man auf unterhaltsame Weise einen &Uuml;berblick, was Andere schon gemacht haben und woran man selbst ankn&uuml;pfen k&ouml;nnte. </p></div>
