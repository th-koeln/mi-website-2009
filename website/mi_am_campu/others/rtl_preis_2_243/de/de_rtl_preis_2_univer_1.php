<h1>RTL Preis 2007 </h1>
<div class="zeile">
<h2 class="float einfach"><br class="spacer" /> </h2>
<div class="float dreifach last">
<p>Zum dritten Mal verlieh der K&ouml;lner Fernsehsender RTL Television GmbH drei F&ouml;rderpreise f&uuml;r herausragende Abschlussarbeiten in der Medieninformatik. Der Bereichsleiter Broadcasting", Bernd H&ouml;ller, &uuml;bergab die Schecks im Gesamtwert von 1500 Euro. RTL arbeitet schon seit mehreren Jahren mit dem Studienbereich Medieninformatik des Campus Gummersbach der FH K&ouml;ln zusammen, meist &uuml;ber Praxissemester und Diplomarbeiten von Studierenden. Aus dieser f&uuml;r beide Seiten fruchtbaren Zusammenarbeit entstand die Idee, herausragende Abschlussarbeiten mit F&ouml;rderpreisen zu pr&auml;mieren. Inhalt des Studiums Medieninformatik sind vor allem Informatik-Anwendungen f&uuml;r Medienunternehmen und damit geh&ouml;ren Fernsehsender zu den bevorzugten Abnehmern von Absolventinnen und Absolventen. </p></div>
<div class="clear">&nbsp;<br /></div></div>
