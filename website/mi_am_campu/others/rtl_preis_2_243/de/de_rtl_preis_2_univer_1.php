<div class="zeile">
<div class="subzeile">
<h2 class="float einfach"><br class="spacer" /> </h2>
<div class="float dreifach last">
<p>Der erste Preis in H&ouml;he von 750 Euro ging an Felix M&uuml;ller aus Borken. In seiner Bachelorarbeit<br />"Ein barrierefreier, webbasierter E-Mail- Dienst f&uuml;r visuell beeintr&auml;chtigte Menschen" erstellte er ein Konzept und einen Prototyp f&uuml;r ein Programm, mit dem Blinde und Sehbehin- derte ohne technische Hilfsmittel &uuml;ber einen Webbrowser ihre E-mails schreiben und abrufen k&ouml;nnen. Die Anregung f&uuml;r das Thema erhielt M&uuml;ller in seinem Praxissemester, das sich mit der Gestaltung von Websites f&uuml;r Sehbehinderte besch&auml;ftigte. Betreuer der Arbeit waren Prof. Dr. Kristian Fischer und Prof. Dr. Gerhard Hartmann vom Studienbereich<br />Medieninformatik der FH.<br /><br />Stephan Welchering, ebenfalls aus Borken, erhielt den zweiten Preis &uuml;ber 500 Euro f&uuml;r seine Bachelorarbeit "Ein Webservice zur Implementierung des Interaktionsparadigmas: Diagonales Lesen f&uuml;r Websites mit besonderer Ber&uuml;cksichtigung struktur- und formatierungs- bezogener Information". Auch diese Abschlussarbeit befasst sich mit dem Thema "Sehbehinderte Menschen und Internet". Welchering er- stellte ein Konzept, mit dem das Prinzip des "diagonalen Lesens", also der schnelle Gesamt&uuml;berblick &uuml;ber einen Text, bei Internetseiten auch f&uuml;r Sehbehinderte m&ouml;glich wird. Betreuer waren Prof. Dr. Gerhard Hartmann und Prof. Dr. Mario Winter. Wie sein Studienkollege M&uuml;ller studiert Welchering derzeit f&uuml;r seinen Masterabschluss in Medieninformatik.<br /><br />Der dritte Preis in H&ouml;he von 250 Euro wurde an Sascha Quillet aus Trier verliehen. Seine Masterarbeit mit dem Titel "Ip-basierte Nachrichtenverteilung f&uuml;r Sendeanstalten" besch&auml;ftigt sich mit der Bild- und Tonnachrichten-&Uuml;bertragung per Internet, sie entstand in Kooperation mit dem Broadcasting Center Europa (BCE) in Luxemburg, einer Tochtergesellschaft von RTL. W&auml;hrend bisher Videofilme oder Tondokumente kostspielig per Satellit in andere Weltregionen &uuml;bermittelt wurden, erm&ouml;glicht dieses System der Sendeanstalt die &Uuml;bertragung der Daten per Internet . Betreuer der Arbeit waren Prof. Dr. Holger G&uuml;nther und Prof. Dr. Fank Victor vom Studienbereich Informatik. Quillet arbeitet derzeit an seiner Promotion an der Universit&auml;t Luxemburg, neben seiner Anstellung bei BCE. </p></div>
