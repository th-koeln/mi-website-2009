<h1>Interview mit Prof. Christian Kohls </h1>
<div class="zeile">
<div class="clear">&nbsp;<br /></div></div>
<div class="zeile">
<div class="subzeile">
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<dl class="einfach bild"><dt><img src="images/interview_m_univer_12_1671_.jpg" alt="Interview mit Prof. Christian Kohls Universalthema " width="172" class="" /></dt></dl>
<div class="float dreifach">
<p><strong>Christian Kohls hat Medieninformatik studiert und war wahrscheinlich einer der ersten Absolventen in Deutschland. Seit dem Wintersemsester 2014 ist er nun Dozent der Medieninformatik am Campus Gummersbach und h&auml;lt dort Seminare. Im Gespr&auml;ch mit Daniela Kucharczyk erz&auml;hlte er von seinem beruflichen Werdegang, was ihn an seinem Fachbereich fasziniert und gab Tipps f&uuml;r angehende Medieninformatiker.</strong> </p></div>
<div class="clear">&nbsp;<br /></div></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach">Was haben Sie gemacht bevor Sie am Campus Gummersbach als Dozent angefangen haben? </h2>
<div class="float dreifach last">
<p>Die letzten 10 Jahre habe ich mich mit verschiedenen Formen des E-Learning besch&auml;ftigt. Angefangen habe ich bei e-teaching.org, einem Projekt am Institut f&uuml;r Wissensmedien in T&uuml;bingen. Das ist ein Info-Portal f&uuml;r Hochschullehrende, die digitale Medien in der Lehre einsetzen m&ouml;chten. Anschlie&szlig;end war ich bei der Firma SMART Technologies - das sind die mit den SMART Boards, also den interaktiven Whiteboards - und habe dort vor allem mit Schulbuchverlagen und Softwareherstellern zusammen gearbeitet, um digitale Lerninhalte zu entwickeln. </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<div class="clear">&nbsp;<br /></div></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach">Und wie kommt es, dass Sie nun hier am Campus Gummersbach Dozent sind? </h2>
<div class="float dreifach last">
<p>Ich hatte schon immer im Hinterkopf, dass ich Lehre machen m&ouml;chte, weshalb ich mich auch mit Didaktik und E-Learning besch&auml;ftigt habe und die Stelle hier am Campus war dann eine sehr gute Gelegenheit, da sie genau meinem Interessengebiet entspricht. Denn der Schwerpunkt soziotechnische Systeme ist eigentlich die ideale Kombination aus einerseits der soliden technischen Sichtweise und andererseits einem Fokus auf den Menschen, wobei alles was man in der Informatik macht, dem Menschen helfen sollte und kein Selbstzweck ist. </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<div class="clear">&nbsp;<br /></div></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach">Was haben Sie studiert? </h2>
<div class="float dreifach last">
<p>Ich habe Medieninformatik studiert und war wahrscheinlich einer der ersten Absolventen in Deutschland. An meiner Fachhochschule in Wedel war ich damals der zweite Durchgang und zu der Zeit gab es diesen Studiengang nur an f&uuml;nf Hochschulen Deutschlands. Damals war es f&uuml;r mich eine sehr bewusste Entscheidung Informatik und Medien miteinander zu kombinieren. </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<div class="clear">&nbsp;<br /></div></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach">Und warum haben Sie sich damals f&uuml;r den Studiengang entschieden? </h2>
<div class="float dreifach last">
<p>Ich habe zum Einen schon immer gerne programmiert und entwickelt, also wirklich Software erstellt, und zum Anderen gab es eine hohe Medienaffinit&auml;t. Denn die Kernaufgabe von Medien ist es ja - zumindest von der Wortbedeutung her - etwas zu vermitteln, ob es jetzt Wissen oder eine Botschaft ist. Und alles womit dies geschehen kann, ob mit Texten, Filmen, Bildern oder visueller Gestaltung, finde ich sehr spannend. </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<div class="clear">&nbsp;<br /></div></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach">Auf welchen Fachbereich haben Sie sich letztendlich spezialisiert? </h2>
<div class="float dreifach last">
<p>Das ist ein bisschen historisch bei mir, da ich mich sehr viel damit auseinandergesetzt habe, wie man mit Medien oder mit dem Computer lernen und damit die pers&ouml;nlichen Lernprozesse verbessern kann. Dabei spielen naturgem&auml;&szlig; psychologische und soziologische Prozesse eine wichtige Rolle - in Kombination mit technischen Komponenten haben wir dann soziotechnische Systeme, also mein Fachgebiet.<br />Darunter versteht man z.B. Social Media, Systeme f&uuml;r kooperatives Arbeiten, E-Learning, Community-Plattformen, die Menschen zusammenbringen und vielleicht auch politische Prozesse unterst&uuml;tzen k&ouml;nnen. Also &uuml;berall wo soziale oder gesellschaftliche Fragestellungen durch die Informatik positiv beeinflusst werden k&ouml;nnen. Es ist aber auch wichtig zu erkennen, wo sich vielleicht negative Auswirkungen ergeben und man dagegen steuern sollte. </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<div class="clear">&nbsp;<br /></div></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach">F&uuml;r welche Veranstaltungen sind Sie in Zukunft am Campus Gummersbach verantwortlich? </h2>
<div class="float dreifach last">
<p>Ich halte einerseits die Grundlagenveranstaltungen Algorithmen und Programmiersprachen II, sowie Paradigmen der Programmierung, was ich sehr sch&ouml;n finde, da beides ausschlaggebend ist, um Software zu entwickeln, die f&uuml;r den Menschen gut nutzbar ist. Denn so anwenderfreundlicher ich etwas gestalten m&ouml;chte und den Menschen in den Mittelpunkt stelle, umso mehr muss ich daf&uuml;r Sorge tragen, dass die Software, die darunter liegt, gut und robust umgesetzt ist. Und andererseits bediene ich mit Wahlpflichtf&auml;chern z.B. &uuml;ber E-Learning Patterns oder Communitydesign die "weiche" Ebene der Informatik, also dass man auch &uuml;berlegt, welche sozialen Konsequenzen man mit Informatik in Bewegung setzt und welche Verantwortung wir tragen, wenn man Software entwickelt und diese Systeme in die Welt setzt. </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<div class="clear">&nbsp;<br /></div></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach">Wie kam es dazu, dass sie sich f&uuml;r diesen Fachbereich entschieden haben? </h2>
<div class="float dreifach last">
<p>Es fing schon im Studium an, als ich mir Autorensysteme f&uuml;r Multimediaanwendungen angeschaut und mit diesen nicht so zufrieden war und etwas naiv gedacht habe, das k&ouml;nnte ich selber besser basteln. Das hat auch ein bisschen funktioniert und ich habe mich damit eine Zeit lang selbstst&auml;ndig gemacht. Allerdings merkt man dann irgendwann, dass man doch ein gr&ouml;&szlig;eres Team braucht, um eine umfangreiche Software zu schreiben. Ich hatte dann das gro&szlig;e Gl&uuml;ck bei e-teaching.org anfangen zu k&ouml;nnen, wo ich bei einem sehr heterogenen Team mit vielen verschiedenen Ideen gelandet war. Diese interdisziplin&auml;re Zusammenarbeit hat mich sicherlich gepr&auml;gt, nicht nur die Perspektive des Programmierers einzunehmen sondern sich mit anderen Menschen auszutauschen, wie Software sein soll, wie sie genutzt werden soll, wie sie gestaltet werden soll, welche Erwartungen damit einhergehen k&ouml;nnen und wie sich Arbeitsprozesse dabei ver&auml;ndern. </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<div class="clear">&nbsp;<br /></div></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach">Welche Tipps hinsichtlich des Studiums und ggf. hinsichtlich der sp&auml;teren Berufswahl w&uuml;rden Sie den Medieninformatik Erstsemestern und Studieninteressierten geben? </h2>
<div class="float dreifach last">
<p>Hmm...eine ganze Reihe von Tipps. Das Wichtigste ist meiner Meinung nach, dass man das Studium sehr ernst nimmt, was hei&szlig;t, dass man viel Zeit investieren muss. Allerdings sollte es einem trotzdem immer Spa&szlig; machen. Es kann ruhig mal anstrengend und schwierig sein, aber irgendwo sollte einem das, was man macht, immer Freude bereiten. Sowohl im Studium, als auch sp&auml;ter im Arbeitsleben. Grade wenn man in die Wahlpflichtf&auml;cher reinkommt oder in die Praxisprojekte, hat man genau diese Freiheiten, sich zu &uuml;berlegen, wo habe ich selber am meisten Spa&szlig; und wo sind meine Interessen. Genau das sollte auch genutzt werden. Dabei sollte man darauf achten, dass es eine Balance zwischen dem gibt, was gemacht werden muss, also den Sachen, die man sp&auml;ter ben&ouml;tigt, um seine bzw. die Ideen anderer zu verwirklichen, und dem was man selber machen m&ouml;chte. Ein weiterer wichtiger Punkt ist, dass man die Partys nat&uuml;rlich nicht auslassen sollte, denn das geh&ouml;rt eben auch zum Studentenleben. Nat&uuml;rlich sollte darunter das Studium nicht leiden - denn ein guter Abschluss bedeutet ja meist auch, einen guten Job zu finden und sich langfristig mehr Freiheiten im Leben zu sichern. </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<div class="clear">&nbsp;<br /></div></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach">Was macht den Medieninformatik Studiengang ihrer Meinung nach so interessant? </h2>
<div class="float dreifach last">
<p>Das Faszinierende f&uuml;r mich ist, dass durch den Einsatz von Medien Ergebnisse sichtbar werden. Ich programmiere nicht versteckte Systeme, bei denen ich dann fast gar nicht mehr sehe, was da passiert, sondern habe meist einen visuellen Output, den ich erzeuge. Durch diese „Ber&uuml;hrung" mit den Nutzern von Informationssystemen wird es erforderlich, viele verschiedene Kompetenzen, z.B. Usability, Computergrafik oder Kommunikationstechnologien zu beherrschen und verschiedene Perspektiven einzunehmen. Durch dieses breite Spektrum er&ouml;ffnen sich dann viele M&ouml;glichkeiten im sp&auml;teren Berufsleben: das Entwickeln von mobilen Anwendungen, Webseiten, Spielen und multimedialen Diensten ebenso wie die Konzeption und Produktion der Inhalte f&uuml;r verschiedenste Medien. Durch diese Vielseitigkeit wird es nie langweilig. </p></div>
