<h2 class="float einfach">Datenvernetzung, Internetvertrauen und Gestenerkennung </h2>
<div class="float dreifach last">
<p>Den ersten Preis und 1000 Euro erhielt David Bellingroth. Seine Bachelor-Arbeit hat den Titel: „Semantic Web User Interfaces - Gestaltung einer Benutzungsschnittstelle zum Zugriff auf Informationen aus dem Semantic Web". Bellingroth besch&auml;ftigte sich mit der Frage, wie vernetzte Daten aus dem Web &uuml;ber Nutzungsschnittstellen ad&auml;quat zug&auml;nglich gemacht werden k&ouml;nnen. Die Studie entstand in Zusammenarbeit mit dem K&ouml;lner Unternehmen Railslove GmbH, Betreuer waren Prof. Dr. Kristian Fischer und Stephan Pavlovic von Railslove. Bellingroth wandte sein System auf die Internet-Plattform „Offenes K&ouml;ln" an, die Informationen aus der K&ouml;lner Stadtverwaltung mit Daten anreichert und sie so &uuml;ber verschiedene Kriterien f&uuml;r B&uuml;rger zug&auml;nglich macht.<br /><br />Auf dem zweiten Platz landete Martin Hesseler mit seiner Masterarbeit „Einfluss von Trust Assurances in Mobile Commerce Applications auf die Bildung von Online Trust". Er bekam 750 Euro Preisgeld. Es ging in der Arbeit um das Vertrauen der Nutzer in online-Anwendungen, insbesondere wenn sie &uuml;ber mobile Ger&auml;te wie Smartphones oder Tablets Produkte oder Dienstleistungen bestellen. Mit einer Vorstudie und einer anschlie&szlig;enden umfangreichen Hauptstudie entwickelte Hesseler ein Konzept, wie „Online Trust", also das Vertrauen in eine sichere Abwicklung, gemessen werden kann und welchen Einfluss sogenannte Trust-Siegel auf die Formierung von Online Trust haben. Wie der Betreuer Prof. Dr. Gerhardt Hartmann Herrn Hesseler bescheinigte, sind seine Ergebnisse stringent und besitzen nach aktuellem Stand der Wissenschaft einen hohen Neuigkeitswert und Innovationsgrad. Zweiter Pr&uuml;fer bei dieser Arbeit war Prof. Dr. Stefan Karsch vom Labor f&uuml;r Kommunikation und Datensicherheit der FH K&ouml;ln.<br /><br />Den dritten Preis und 500 Euro erhielt Daniel Bertram mit seiner Masterarbeit „Untersuchungen zur Varianzreduktion beschleunigungsbasierter 3D-Gestendaten". Die Arbeit entstand im Zusammenhang mit dem Forschungsprojekt SOMA ("Systematische Optimierung von Modellen f&uuml;r IT & Automation"), das von Prof. Dr. Wolfgang Konen geleitet wird. Grundlage der Studie war ein Gestenerkennungsverfahren, das Herr Bertram erstmalig auf ein Smartphone &uuml;bertragen hat. Somit kann man mit dem von Herrn Bertram entwickelten Programm ein Handy zur Gestenerkennung nutzen. Der Masterabsolvent untersuchte, wie diese neue Methodik, die sogenannte Slow Feature Analysis, besser die Gesten von verschiedenen Personen miteinander vergleichen kann und lieferte, so Prof. Konen, wichtige Ans&auml;tze, die die Gestenerkennung robuster machen und den aktuellen Stand der Technik in diesem Bereich weiterentwickeln.<br /></p></div>
