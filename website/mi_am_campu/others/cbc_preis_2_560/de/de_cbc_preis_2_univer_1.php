<h1>CBC Preis 2013 </h1>
<div class="zeile">
<div class="clear">&nbsp;<br /></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach"><br class="spacer" /> </h2>
<div class="float dreifach last">
<p>Auch 2013 k&uuml;hrte das Cologne Broadcasting Center (CBC) in Zusammenarbeit mit der Fachhochschule K&ouml;ln wieder die besten Abschlussarbeiten des Studiengangs Medieninformatik. Das CBC ist f&uuml;r die Betreuung der Technik f&uuml;r die K&ouml;lner „RTL Group Deutschland" zust&auml;ndig, sodass die Siegerehrung im Geb&auml;ude der RTL-Group am K&ouml;lner Rheinufer stattfand. Die Urkunden und Schecks wurden w&auml;hrend einer kleinen Feier mit Freude von dem Bereichsleiter f&uuml;r Systeme und Technologie des CBC, R&uuml;diger Kupke, &uuml;berreicht. Wobei er betonte, dass sich die Studien auf „hohem technischen und theoretischen Niveau" bewegen und er sie mit viel Freude und Interesse gelesen hat. </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<div class="float einfach marginalie">
<p>R&uuml;diger Kupke, Bereichsleiter f&uuml;r Systeme und Technologie des CBC mit den drei Gewinnern<br /><br />1. Platz Bellingroth<br />2. Platz Hesseler<br />3. Platz Bertram </p></div>
