<h1>Analyse sozialer Beziehungen anhand nonverbaler Signale im textbasierten Chat </h1>
<div class="zeile">
<h2 class="float einfach"><strong>Le&oacute;n Rojas, Sarah</strong><br /><a href="../../../../../website/master/wpf_ma/masterarbei_67/de/de_masterarbei_univer_1.php" rel="pkm67">Masterarbeit</a> 2011<br /><br />betreut von:<br /><a href="../../../../../website/mi_am_campu/prof/prof_dr_k_12/de/de_prof_dr_k_univer_1.php" rel="pkm12">Prof. Dr. Kristian Fischer</a><br />Dipl. Psych, Dipl. Wirt-inform.(BA), M.A. Uwe Kirschenmann, Fraunhofer FIT </h2>
<div class="float dreifach last">
<p>Die Kommunikation via Chat konnte sich in den letzten Jahren neben Kommunikationsmedien wie E-Mail und Telefon in vielen Bereichen des allt&auml;glichen Lebens etablieren. Textbasierter Chat weist jedoch, im Vergleich zu Face-to-Face-Kommunikation, gewisse mediale Einschr&auml;nkungen auf. Nonverbale Signale wie Gestik und Mimik k&ouml;nnen, in Ermangelung eines visuellen Kanals, nicht direkt &uuml;bertragen werden. Trotzdem findet auch im Chat ein Austausch nonverbaler Informationen statt, wie beispielsweise der Einsatz von Emoticons oder Pausen in Gespr&auml;chen.<br /><br />Verhaltensweisen bei nonverbaler Kommunikation enthalten Hinweise bez&uuml;glich der sozialen Beziehung der Gespr&auml;chspartner. Derartige nonverbale Signale k&ouml;nnen aus Chat-Protokollen extrahiert und analysiert werden. In dieser Arbeit besch&auml;ftigen wir uns mit der Analyse nonverbaler Daten aus Chat-Protokollen. Ziel ist die Einsch&auml;tzung der St&auml;rke einer sozialen Beziehung anhand eines multidimensionalen Analyse-Modells.<br /><br />In the past few years chat has become one of the primary means of remote communicati- on. It is used in many areas alongside well-established media like e-mail and telephone. But, in comparison to face-to-face-communication, text-based chat presents certain restrictions: Nonverbal signals, like body language and facial expressions, cannot be transmitted, due to the lack of a visual communication channel. Nonetheless chat is not deprived of nonverbal communication: the use of emoticons and pauses during a chat-session represent some of the nonverbal cues characteristic to chat-communication.<br /><br />Nonverbal behavior during communication characterizes the social relationship of the peers. In the case of chat communication all exchanged information is often protocoled and can be analyzed. In this thesis we present an approach for analyzing nonverbal information extracted from chat-logs. The goal is to determine the strength of the relationship between chat-partners using a multidimensional analysis-model.<br /><br /><a href="http://opus.bibl.fh-koeln.de/volltexte/2012/336/pdf/Masterarbeit.pdf">Arbeit als PDF-Dokument</a> </p></div>
