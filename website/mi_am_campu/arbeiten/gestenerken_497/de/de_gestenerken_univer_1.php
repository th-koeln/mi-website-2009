<h1>Gestenerkennung mit der SFA : Klassifizierung von beschleunigungsbasierten 3D-Gesten des Wii-Controllers </h1>
<div class="zeile">
<h2 class="float einfach"><strong>Kristine Hein</strong><br /><a href="../../../../../website/master/wpf_ma/masterarbei_67/de/de_masterarbei_univer_1.php" rel="pkm67">Masterarbeit</a> 2010<br /><br />betreut von:<br /><a href="../../../../../website/mi_am_campu/prof/prof_dr_w_16/de/de_prof_dr_w_univer_1.php" rel="pkm16">Prof. Dr. Wolfgang Konen</a><br /><a href="../../../../../website/mi_am_campu/prof/prof_dr_h_81/de/de_prof_dr_h_univer_1.php" rel="pkm81">Prof. Dr. Horst Stenzel</a> </h2>
<div class="float dreifach last">
<p>In dieser Arbeit wurde die Slow Feature Analysis (SFA) auf ihre Einsatzm&ouml;glichkeiten zur Gestenerkennung zu untersuchen. SFA ist ein Lernalgorithmus, der aus den Neurowissenschaften stammt und aus sich schnell ver&auml;ndernden Signalen, die langsamsten Merkmale herausfiltert. Als Gesten werden in diesem Fall beschleunigungsbasierte dreidimensionale Signale des Wii-Controllers von Nintendo betrachtet. F&uuml;r die Untersuchungen wurden f&uuml;nf unterschiedliche Gesten von zehn Probanden verwendet. Diese Gesten liegen in Form von mehrdimensionalen Zeitreihen vor, die die Messwerte der Beschleunigungssensoren des Wii-Controllers enthalten. Die Gestendaten wurden mit einer Java-Anwendung aufgezeichnet, mit der die Wiimote &uuml;ber Bluetooth verbunden ist. F&uuml;r die Untersuchungen wurde eine Implementierung des SFA-Toolkits (MATLAB, SFA-TK) verwendet und die Ergebnisse mit denen anderer g&auml;ngiger Verfahren verglichen. Untersucht wurde hierzu sowohl die Klassifizierung von Gesten, als auch Ans&auml;tze zur Segmentierung, dem Erkennen von Anfang und Ende einer Geste.<br /><br /><a href="http://opus.bibl.fh-koeln.de/volltexte/2011/304/pdf/Masterthesis_final.pdf">Arbeit als PDF-Dokument</a> </p></div>
