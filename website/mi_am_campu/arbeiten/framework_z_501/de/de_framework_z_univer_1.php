<h1>Framework zur Integration von Usability Engineering in agile Softwareentwicklungsprozesse </h1>
<div class="zeile">
<h2 class="float einfach"><strong>Ren&eacute; Rappenh&ouml;ner</strong><br /><a href="../../../../../website/bachelor/studienseme/bachelorarb_49/de/de_bachelorarb_univer_1.php" rel="pkm49">Bachelorarbeit</a> 2009<br /><br />betreut von: <br /><a href="../../../../../website/mi_am_campu/prof/prof_dr_g_17/de/de_prof_dr_g_univer_1.php" rel="pkm17">Prof. Dr. Gerhard Hartmann</a><br /><a href="../../../../../website/mi_am_campu/prof/prof_dr_m_18/de/de_prof_dr_m_univer_1.php" rel="pkm18">Prof. Dr. Mario Winter</a> </h2>
<div class="float dreifach last">
<p>Bei der Entwicklung interaktiver Software werden die Belange der Benutzer oft au&szlig;er<br />Acht gelassen oder nur unzureichend ber&uuml;cksichtigt. Dies f&uuml;hrt dazu, dass das System<br />von den Benutzern nicht akzeptiert wird, es die Benutzer nicht im ausreichenden Ma&szlig;e unterst&uuml;tzt oder, im Ernstfall, die Benutzer ihre Aufgaben nicht vollst&auml;ndig l&ouml;sen<br />k&ouml;nnen. Um einen N&auml;hrboden f&uuml;r die Entwicklungsprozesse von gebrauchstauglicher<br />Software zu schaffen, muss eine grundlegende Entscheidung getroffen werden. Der Usability Engineering Prozess wird selbst geplant und durchgef&uuml;hrt, oder extern an eine<br />Firma weitergegeben. Diese Arbeit besch&auml;ftigt sich mit ersterem und integriert den Usability Engineering- und Softwareentwicklungsprozess in einem Framework mit agilem<br />Vorgehensmodell, welches sich an erfahrene Projektleiter, Projektmanager und Softwareentwickler ohne gefestigtes Wissen im Bereich des Usability Engineerings richtet.<br />Es werden Ziele definiert und m&ouml;gliche Methoden und Techniken vorgestellt, mit denen diese Ziele erreicht werden k&ouml;nnen. Da sich das Framework an Softwareentwickler richtet, die einen Usability Enginigeering Prozess integrieren wollen, werden zwar notwendige Ziele der Softwareentwicklung, aber nicht die Methoden und Techniken diese zu erreichen, vorgegeben, um einen etablierten Softwareentwicklungsprozess nicht zu sehr zu beeinflussen.<br /><br /><a href="http://opus.bibl.fh-koeln.de/volltexte/2011/314/pdf/Bachelorarbeit.pdf">Arbeit als PDF-Dokument</a> </p></div>
