<h1>Integration von Usability Engineering und Software Engineering: Evaluation und Optimierung eines ganzheitlichen Modells anhand von Konformit&auml;ts- und Rahmenanforderungen </h1>
<div class="zeile">
<h2 class="float einfach"><strong>Holger Fischer</strong><br /><a href="../../../../../website/master/wpf_ma/masterarbei_67/de/de_masterarbei_univer_1.php" rel="pkm67">Masterarbeit</a> 2010<br /><br />betreut von:<br /><a href="../../../../../website/mi_am_campu/prof/prof_dr_g_17/de/de_prof_dr_g_univer_1.php" rel="pkm17">Prof. Dr. Gerhard Hartmann</a><br />Dr. Karsten Nebe, C-LAB, Universit&auml;t Paderborn </h2>
<div class="float dreifach last">
<p>Die Gebrauchstauglichkeit eines Produktes hat sich zu einem entscheidenden Qualit&auml;tskriterium in der Softwareentwicklung entwickelt. Neben den Nutzern erkennen auch immer mehr Organisationen den Mehrwert von gebrauchstauglichen Produkten, insbesondere die steigende Produktivit&auml;t, die verbesserte Qualit&auml;t und den damit verbunden Profit. In Praxis und Wissenschaft wird daher derzeit dem Wunsch nachgegangen, Usability Engineering und Software Engineering zu integrieren. In dieser Arbeit werden Standards der beiden Disziplinen in Bezug auf Aktivit&auml;ten und Artefakte analysiert, welche in einem ganzheitlichen Modell zur Integration von Usability Engineering und Software Engineering abgebildet werden sollen. So sollen m&ouml;gliche Ankn&uuml;pfungspunkte zwischen Aktivit&auml;ten und Artefakten aufgezeigt werden. Ziel ist die Evaluation dieses Modells anhand von Konformit&auml;ts- und Rahmenanforderungen, sowie durch eine Expertenbefragung.<br /><br />The usability of a product has become a significant quality criterion in the software enginnering process. Not only the user, but also the organizations become aware of the benefit created by usable products, especially the rising productivity, the improved quality and therewith the higher profit. Therefore experts in practice and science regard the integration of usability engineering and software engineering. In this thesis standards of both disciplines are analysed to identify activities and artifacts to map them in a holistic model integrating usability engineering and software engineering. In this way points of contact between activities and artifacts should be demonstrated. The aim is the evaluation of this model in reference to conformity and frame requirements as well as by a questioning with experts.<br /><br /><a href="http://opus.bibl.fh-koeln.de/volltexte/2011/297/pdf/Masterthesis_Fischer.pdf">Arbeit als PDF-Dokument</a> </p></div>
