<h1>Auswirkungen des Architekturstils REST auf die Anwendungsentwicklung </h1>
<div class="zeile">
<h2 class="float einfach"><strong>Thomas He&szlig;</strong><br />Masterarbeit 2011<br /><br />betreut von:<br /><a href="../../../../../website/mi_am_campu/prof/prof_dr_k_12/de/de_prof_dr_k_univer_1.php" rel="pkm12">Prof. Dr. Kristian Fischer</a><br /><a href="../../../../../website/mi_am_campu/prof/prof_dr_m_18/de/de_prof_dr_m_univer_1.php" rel="pkm18">Prof. Dr. Mario Winter</a> </h2>
<div class="float dreifach last">
<p>Der Architekturstil REST erlangt in den letzten Jahren immer gr&ouml;&szlig;ere Verbreitung. RPC-orientierte Architekturstile scheinen besonders bei Software-Neuentwicklungen obsolet zu werden. Es existieren viele Gr&uuml;nde, die f&uuml;r eine Verwendung von REST-Architekturen sprechen. Den- noch zeigen sich auch Probleme bei der Entwicklung von REST-konformen Anwendungen. Diese Probleme und jeweils existierende L&ouml;sungsans&auml;tze werden in dieser Arbeit aufgezeigt. Dabei geht es zum einen um verf&uuml;gbare Frameworks und technische Spezifikationen und zum anderen um konzeptionelle L&uuml;cken im Bereich der Automatisierung von Gesch&auml;ftsprozessen.<br /><br /><a href="http://opus.bibl.fh-koeln.de/volltexte/2011/324/pdf/Thomas_Hess.pdf">Arbeit als PDF-Dokument</a> </p></div>
