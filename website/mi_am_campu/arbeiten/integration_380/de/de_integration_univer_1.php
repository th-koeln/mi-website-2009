<h1>Integration von Ontologien in objektorientierte Programmiersprachen als verteilte und dynamische Datenmodelle </h1>
<div class="zeile">
<h2 class="float einfach"><strong>Lars Brillert</strong><br /><a href="../../../../../website/master/wpf_ma/masterarbei_67/de/de_masterarbei_univer_1.php" rel="pkm67">Masterarbeit</a> 2010<br /><br />betreut von<br /><a href="../../../../../website/mi_am_campu/prof/prof_dr_k_12/de/de_prof_dr_k_univer_1.php" rel="pkm12">Prof. Dr. Kristian Fischer</a><br /><a href="../../../../../website/mi_am_campu/prof/prof_chris_5/de/de_prof_chris_univer_1.php" rel="pkm5">Prof. Christian Noss</a> </h2>
<div class="float dreifach last">
<p>Immer mehr Teilbereiche des Semantic Web sind in den letzten Jahren erfolgreich umgesetzt geworden. Ebenso wird bei der Bearbeitung von komplexen Problemr&auml;umen mittlerweile oft auf semantische Modelle zur&uuml;ckgegriffen, um eine flexible Beschreibung der Dom&auml;ne zu erstellen. Werkzeuge, welche die Entwicklung von Anwendungen, die auf semantischen Modellen basieren, unterst&uuml;tzen, sind bislang jedoch nur in begrenztem Ma&szlig;e verf&uuml;gbar. Insbesondere die Verarbeitung von verteilten und dynamischen Modellen ist mit keinem der derzeit verf&uuml;gbaren Produkte vollst&auml;ndig zu realisieren. Diese Arbeit untersucht die M&ouml;glichkeiten zur Integration von semantischen Modellen in objektorientierte Programmiersprachen. Es werden bestehende Ans&auml;tze analysiert und ein formales Modell der Integration erstellt. Das formale Modell wird in Form eines prototypischen Rahmenwerks in der Programmiersprache Ruby implementiert und validiert.<br /><br />Many aspects, that were part of Tim Berners-Lee's vision of the semantic web, are now in use. As well in the context of complex semistructured problem-spaces, semantic models were used to gather a flexible description of the domain of interest. But tools for supporting the development based on semantic models are incomplete and too complex for pushing semantic applications beyond their limits. Especially the processing of distributed and dynamic models in an object-oriented manner is not covered by any of the products presently available. This thesis examines the possibilities of integrating ontologies into object-oriented programming languages. Based on the analysis of existing approaches a formal model will be established. In order to validate the model, a reference implementation for the programming language Ruby will be developed.<br /><br /><a href="http://opus.bibl.fh-koeln.de/frontdoor.php?source_opus=260">Arbeit als PDF-Document</a> </p></div>
