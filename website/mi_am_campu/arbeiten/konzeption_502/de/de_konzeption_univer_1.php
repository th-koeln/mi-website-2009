<h1>Konzeption, prototypische Realisierung und szenariobasierte Validierung einer dienstorientierten Multimediaarchitektur </h1>
<div class="zeile">
<h2 class="float einfach"><strong>Carina Oberm&uuml;ller</strong><br /><a href="../../../../../website/bachelor/studienseme/bachelorarb_49/de/de_bachelorarb_univer_1.php" rel="pkm49">Bachelorarbeit</a> 2009<br /><br />betreut von:<br /><a href="../../../../../website/mi_am_campu/prof/prof_dr_g_17/de/de_prof_dr_g_univer_1.php" rel="pkm17">Prof. Dr. Gerhard Hartmann</a><br />Dipl.-Ing. Thomas Geis, ProContext GmbH </h2>
<div class="float dreifach last">
<p>Aufgrund des Mangels an Softwareprodukten, die Projektmitarbeiter beim &ouml;rtlich und<br />zeitlich verteilten Arbeiten in Projekten effizient und zufriedenstellend unterst&uuml;tzen,<br />soll ein ToDo-Manager entwickelt werden, der den Bed&uuml;rfnissen der Benutzer gerecht<br />wird und deren Anforderungen erf&uuml;llt. Valide Prototypen basieren auf Anforderungen<br />der Benutzer und spiegeln deren Interaktion mit dem System wider. Die konstruierten Interaktionsentw&uuml;rfe sollen in Prototypen abgebildet, evaluiert und &uuml;berarbeitet werden, bis die Aufgaben der Benutzer durch die Dialogschritte abgebildet werden, sodass eine effiziente und zufriedenstellende Bearbeitung erm&ouml;glicht wird. Auf der Basis von ermittelten Nutzungsobjekten und Werkzeugen wird die Navigationsstruktur ermittelt sowie der Funktionsumfang des Systems definiert. Das User-Interface Design beschreibt die Entwicklung von Prototypen, die durch Benutzer evaluiert werden. Ber&uuml;cksichtigt werden neben der Interaktion auch gestalterische Aspekte. Die evaluierten Prototypen werden iterativ &uuml;berarbeitet, damit ein gebrauchstaugliches Modell des ToDo-Managers entsteht. Ziel der Bachelorarbeit ist das Durchf&uuml;hren einer konzeptionellen und konkreten User Interface Design Phase, in denen die Entwicklung eines auf den Anforderungen<br />der Benutzer basierenden demonstrierbaren und interagierbaren Prototypen f&uuml;r einen ToDo-Manager angestrebt wird.<br /><br /><a href="http://opus.bibl.fh-koeln.de/volltexte/2011/313/pdf/Bachelorarbeit.pdf">Arbeit als PDF-Dokument</a> </p></div>
