<h1>CBC Preis 2011 </h1>
<div class="zeile">
<h2 class="float einfach">CBC (Cologne Broadcasting Center) ist eines der f&uuml;hrenden TV-Produktions-, IT- und Broadcast-Unternehmen Deutschlands. CBC betreut unter anderem Sender wie RTL, VOX, Super RTL, RTL II, n-tv.<br /><a href="http://www.cbc.de/cbc3/index.php">mehr zu CBC</a> </h2>
<div class="float dreifach last">
<p>Auch dieses Jahr wurden von CBC, in Kooperation mit der Fachhochschule K&ouml;ln, die besten Bachelor-, Master-, und Diplomarbeiten des Studiengangs Informatik pr&auml;miert. Im Sendezentrum von CBC erhielten die Jungakademiker ihre Auszeichnungen. Der Erstplatzierte erhielt eine Pr&auml;mie von 750 Euro, der Zweitplatzierte erhielt 500 Euro und 250 Euro erhielt der Drittplatzierte.<br /><br />R&uuml;diger Kupke, CBC-Bereichsleiter Systems & Technology &auml;u&szlig;erte sich folgender Ma&szlig;en:<br />"Auch dieses Jahr waren wieder sehr gute Arbeiten dabei: Modern, praxisnah und wissenschaftlich pr&auml;zise. Die Auswahl der drei Besten sowie die Festlegung der Reihenfolge waren sehr schwierig, da sie sich in der akademischen Qualit&auml;t stellenweise nur in Nuancen unterscheiden. Zu Recht sind alle drei Werke auszeichnungswert." </p></div>
