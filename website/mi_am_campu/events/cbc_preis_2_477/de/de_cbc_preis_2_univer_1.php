<p><strong>Preistr&auml;ger 2011</strong><br />1. Herr Ridwan<br />2. Herr Schneider<br />3. Herr Hoke<br /></p></div>
<dl class="dreifach bild"><dt><img src="images/cbc_preis_2_univer_11_1504_.jpg" alt="CBC Preis 2011 Universalthema " width="526" class="" /></dt>
<dd>Voller Stolz pr&auml;sentieren die drei gk&uuml;cklichen Preistr&auml;ger ihre CBC-Urkunde f&uuml;r die besten Bachelor- Master-, und Diplomarbeiten der FH K&ouml;ln (v.l.n.r.: R&uuml;diger Kupke, Tim Schneider, Egy Rizky Utama Ridwan, Carsten Hoke, Prof. Dr. Kristian Fischer) <br /><br />Die intensive Zusammenarbeit erm&ouml;glicht den Studenten und Studentinnen einen direkten Praxiskontakt. Praxisrelevante, unternehmerische Themen und Trends werden durch den intensiven Austausch auch f&uuml;r Professoren erfahrbar. Doch nicht nur die Fachhochschule profitiert von dieser Zusammenarbeit. CBC sieht sich durch die akademischen Gedanken der Studenten inspiriert, was eine sehr gute M&ouml;glichkeit bietet, Nachwuchs zu f&ouml;rdern. CBC vergibt nicht nur Themen f&uuml;r Bachelor- und Masterarbeiten, es wird Studierenden auch teilweise erm&ouml;glicht diese im Unternehmen zu erstellen. Den H&ouml;hepunkt der Kooperation mit CBC bilden allerdings die Auszeichnung und die Verleihung der Urkunden an die gl&uuml;cklichen Sieger. <br /></dd></dl>
