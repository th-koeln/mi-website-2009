<h1>Tag der Offenen T&uuml;r 2011 </h1>
<div class="zeile">
<div class="float dreifach last">
<p>Am 29.05.2011 fand der Tag der offenen T&uuml;r der FH K&ouml;ln Campus Gummersbach von 11 Uhr bis 16 Uhr statt. Viele Besucher, unterschiedlichster Altersgruppen, informierten sich &uuml;ber den Campus. Die Medieninformatik pr&auml;sentierte zwei Attraktionen.<br /><br />Das Motto der esten Attraktion lautete „make some noise". Hier hatten die Besucher die M&ouml;glichkeit, das <a href="http://www.virtuelles-sportmuseum.de/">virtuelle museum//k&ouml;lner sport</a> zu vertonen. Sie konnten sich an einem iMac frei durch das Museum bewegen und dazu, unter anderem Ger&auml;usche zu Interaktionen erzeugen. Der Desktop wurde mit der Software „iShowU" aufgenommen. So wurde als Endprodukt ein kleines Screenmovie erstellt. </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="zeile">
<div class="subzeile">
<div class="float dreifach last">
<p>Die zweite Attraktion lief unter dem Motto „mit der Greenbox vom Campus in die Welt". Die Besucher konnten hier in die Rolle eines Nachrichtensprechers des ZDF Nachrichtenstudio schl&uuml;pfen. Daf&uuml;r wurde zun&auml;chst ein Intro eingespielt, dann verk&uuml;ndete der Besucher die Nachrichten und zum Abschluss folgte ein Outtro. Der Hintergrund wurde live mittels Videomischer eingespielt. </p></div>
