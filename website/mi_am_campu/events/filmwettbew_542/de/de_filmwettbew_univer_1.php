<h2 class="float einfach">Filmwettbewerb: Different </h2>
<div class="float dreifach last">
<p>Zusammen mit dem Musical-Projekt Oberberg und unterst&uuml;tzt durch das Ministerium f&uuml;r Familie, Kinder, Jugend, Kultur und Sport des Landes Nordrhein-Westfalen haben wir einen kleinen Film-Wettbewerb ausgeschrieben. </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<dl class="dreifach bild"><dt><img src="images/filmwettbew_univer_11_1588_.jpg" alt="Filmwettbewerb: Different Universalthema " width="526" class="" /></dt></dl>
<dl class="einfach bild"><dt><img src="images/filmwettbew_univer_12_1588_.jpg" alt="Filmwettbewerb: Different Universalthema " width="172" class="" /></dt>
<dd>Die gl&uuml;cklichen Gewinner:<br />1. Platz - Diego Vivanco<br />mit <a href="http://www.youtube.com/watch?v=iv7Z9XrwsG4">Somos (Wir sind)</a><br />2. Platz - Nicole Babitsch<br />mit <a href="http://www.youtube.com/watch?v=a4zDSAqZfKM">A ‘different' kind of happiness</a><br />3. Platz - Jennifer Hommens<br />mit <a href="http://www.youtube.com/watch?v=FiEoiNpWAPw">Ver2felt</a> </dd></dl>
<div class="float dreifach">
<p>Im Fr&uuml;hjahr 2012 hat das Musical Projekt Oberberg das Musical HONK auf die B&uuml;hne gebracht. Eine Adaption von Hans-Christian Andersens M&auml;rchen „Die Geschichte vom h&auml;sslichen Entlein" - eine Geschichte &uuml;ber das Erwachsen-werden: Wer bin ich eigentlich? Was ist eigentlich gut an mir? Und vor allem: Warum bin ich anders als die Anderen? Die Themen des St&uuml;ckes vom Anders-Sein, nicht zur Gemeinschaft zu passen, von Ausgrenzung und vom Suchen nach der eigenen Identit&auml;t besch&auml;ftigen vor allem junge Menschen. Dieser Wettbewerb sollte dazu anregen, sich im mit den Fragen des „Anders-Seins" kreativ auseinander zu setzen.<br /><br />Eingereicht werden konnten Film- oder Videobeitr&auml;ge zum Thema 'Anders-Sein', die eine L&auml;nge von maximal 3 Minuten nicht &uuml;berschreiten durften. In den Beitr&auml;gen sollte kein gesprochenes Wort enthalten sein. Detaillierte Bedingungen zum Film finden sich auf der <a href="http://www.medieninformatik.fh-koeln.de/different/">Different Website</a> .<br /><br />Unter den eingereichten Beitr&auml;gen wurden Preise in zwei Kategorien vergeben: Der Jury-Preis und der Publikumspreis, der &uuml;ber ein Internet-Voting ermittelt wurde. Der Jury geh&ouml;rten an: Prof. Hans Kornacher, VFX-Supervisor; Torsten Sch&ouml;rdling, Filmemacher; Gregor Leschig, Regisseur und Kulturmanager; Prof. Christian Noss, Designer; Daniela Kucharzcyk, Studentin.<br /><br />Wir bekamen eine recht gute &uuml;berregionale Resonanz, da zwei Beitr&auml;ge aus Niedersachsen (Dochtersen, Oyten) kamen, f&uuml;nf Beitr&auml;ge aus Berlin, zwei aus Baden-W&uuml;rttemberg (N&uuml;rtingen), ein Beitrag aus Sachsen (Leipzig) und vier Beitr&auml;ge aus NRW (Gummersbach, K&ouml;ln, Marienheide). Auch mit dem Internet-Voting haben wir viele Leute erreicht. Mit knapp 3500 Aufrufen und um die 300 Votes wurde der Publikumsliebling unter den eingereichten Filmen entschieden.<br /><br />Alle Teilnehmer haben das Thema 'Different' sehr unterschiedlich interpretiert und umgesetzt, sodass es f&uuml;r die Juroren keine einfache Aufgabe war, die drei Preistr&auml;ger zu bestimmen. Auch das bunt gemischte Jury-Team war eine interessante Mischung, denn jeder steuerte einen anderen Blickwinkel auf die einzelnen Einsendungen bei. Der gegenseitige Austausch bei einem kleinen Catering f&uuml;hrte zu einer sch&ouml;nen Debatte, wobei schlie&szlig;lich ein zufriedenstellendes Ergebnis herauskam. Dieses ist auf der <a href="http://www.medieninformatik.fh-koeln.de/different/">Different Website</a> zu finden und auch alle Beitr&auml;ge k&ouml;nnen weiterhin auf dem <a href="http://www.youtube.com/user/DifferentVoting">Youtube Kanal</a> angeschaut werden. </p></div>
