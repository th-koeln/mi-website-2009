<h1>Nacht der Technik 2013 </h1>
<div class="zeile">
<div class="float dreifach last">
<p>Am 14. Juni 2013 fand in K&ouml;ln zwischen 18 und 24 Uhr die 4. Nacht der Technik statt und erm&ouml;glichte vielen neugierigen Besuchern das Eintauchen in die faszinierende Welt der Technik. Die Fachhochschule K&ouml;ln Campus Deutz trug ebenfalls ihren Teil dazu bei. Neben zahlreichen Kurzvorlesungen aus den unterschiedlichsten Wissensgebieten und Laborbesuchen, bereiteten die Wissenschaftler der Hochschule viele spannende Experimente vor. </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="zeile">
<div class="subzeile">
<div class="float dreifach last">
<p>Auch der Campus Gummersbach war mit einer Attraktion vertreten. " Bewegung als Musikinstrument" lautete das Motto und sorgte schon nach kurzer Zeit f&uuml;r gro&szlig;es Interesse. Die K&ouml;rperbewegung des Musizierenden wurde dabei durch die Kinect, einem kleinen Sensor, erfasst und in die entsprechenden T&ouml;ne umgesetzt. Mit heben des einen Armes konnten die T&ouml;ne variiert werden und durch schnelles auf und ab bewegen des anderen Armes ein Rhythmus erzeugen werden. Zu Beginn erforderte dies ein wenig Geschick, was die Experimentierfreude der Spieler weckte und die Aufmerksamkeit auf sich zog.<br /></p></div>
