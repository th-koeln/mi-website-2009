<h2 class="float einfach"><br class="spacer" /> </h2>
<div class="float dreifach last">
<p><strong>ERCO - semantische Modellierung im Unternehmen</strong><br />Lars Brillert und Stephan Pavlovic; Masterprojekt </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<dl class="einfach bild"><dt><img src="images/die_besten_univer_12_653_.jpg" alt="Die besten Projekte 08/09 Universalthema " width="172" class="" /></dt></dl>
<div class="float dreifach">
<p>Im Projekt „semantische Modellierung im Unternehmen" wurde die Produktdom&auml;ne rund um das Thema Leuchten als semantisches Modell erfasst, um potentielle Vorteile dieser Technik aufzeigen zu k&ouml;nnen. Der Schwerpunkt der Modellierung lag auf der expliziten Formalisierung von unterschiedlichen Beleuchtungsaufgaben, die eine automatische Klassifikation der Leuchten erm&ouml;glicht. Eine prototypische Anwendung greift auf diese Wissensbasis zur&uuml;ck, um &Auml;hnlichkeiten im Produktbestand der Firma ERCO zu bestimmen und dem Anwender die gefundenen &Auml;hnlichkeiten angemessen zu pr&auml;sentieren. </p></div>
<div class="clear">&nbsp;<br /></div></div></div>
