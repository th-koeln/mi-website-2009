<p><strong>Robo Slammer 2000</strong><br />Hanne Hofmann, Niclas Tocha, Julian Rahe, Eugen D&uuml;ck und Torsten Hafer; WPF Visuelle Effekte<br /><br />
<object width="526" height="320"><param name="movie" value="http://www.youtube.com/v/TB20pnZC9bc&hl=de&fs=1&"></param><param name="allowFullScreen" value="true"></param><param name="allowscriptaccess" value="always"></param><embed src="http://www.youtube.com/v/TB20pnZC9bc&hl=de&fs=1&" type="application/x-shockwave-flash" allowscriptaccess="always" allowfullscreen="true" width="526" height="320"></embed></object>
 </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<dl class="einfach bild"><dt><img src="images/die_besten_univer_12_646_.jpg" alt="Die besten Projekte 08/09 Universalthema " width="172" class="" /></dt>
<dd>Dieses Projekt wurde den Showcase Besuchern mit dem "Best Presentation Award" ausgezeichnet. </dd></dl>
<div class="float dreifach">
<p>Ein Roboter f&auml;hrt verwirrt auf einem Tisch herum, besch&auml;digt dabei ein Glas und macht eine Flasche kaputt. Roboter, Flasche und Glas sind mit Cinema 4D im Computer entstanden und wurden nachtr&auml;glich in die reale Welt eingesetzt. Von der ersten bis zur letzten Sekunde ist der Film mit visuellen Effekten aus After Effects und Cinema 4D geschm&uuml;ckt. </p></div>
