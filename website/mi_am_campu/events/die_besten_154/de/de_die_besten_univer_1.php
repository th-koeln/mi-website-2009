<h2 class="float einfach"><br class="spacer" /> </h2>
<div class="float dreifach last">
<p><strong>Kartonplayer</strong><br />Sebastian Busch, Jasmina Duratovic und Stephan Wagner; Grundlagen der visuellen Kommunikation </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<dl class="einfach bild"><dt><img src="images/die_besten_univer_12_650_.jpg" alt="Die besten Projekte 08/09 Universalthema " width="172" class="" /></dt></dl>
<div class="float dreifach">
<p>Muss eine Programmoberfl&auml;che immer gleich aussehen? Nein! Unter diesem Motto gestalteten wir als Abschlussaufgabe im Fach „Grundlagen der visuellen Kommunikation" eine v&ouml;llig neue Oberfl&auml;che f&uuml;r ein bereits vorhandenes Bilderabspielprogramm. Unter der Verwendung von Papervision3D ist so eine Programmoberfl&auml;che mit explorativem Charakter entstanden, die in Form eines Kartons ein immer wiederkehrendes Symbol der Medieninformatik beinhaltet. </p></div>
<div class="clear">&nbsp;<br /></div></div></div>
