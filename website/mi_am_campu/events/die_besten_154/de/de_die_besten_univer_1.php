<div class="subzeile">
<h2 class="float einfach"><br class="spacer" /> </h2>
<div class="float dreifach last">
<p><strong>Mobile Berichterstattung &uuml;ber UMTS</strong><br />Arne Nocke und Nico Ritter; Praxisprojekt </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<dl class="einfach bild"><dt><img src="images/die_besten_univer_12_651_.jpg" alt="Die besten Projekte 08/09 Universalthema " width="172" class="" /></dt></dl>
<div class="float dreifach">
<p>In Kooperation mit Oberberg-Aktuell soll eine M&ouml;glichkeit zur mobilen Berichterstattung, mit geringen Mitteln und zum Live Streaming lokaler Events auf dem von Oberberg- Aktuell betriebenden Nachrichten Portal geschaffen werden, um so auch kleinerern lokalen Unternehmen die M&ouml;glichkeit zu bieten, live ins Internet zu streamen. F&uuml;r den Live-Stream sollen Reporter die M&ouml;glichkeit haben mit wenig Equipment an den Ort des Geschehens zu fahren und von dort direkt &uuml;ber das Internet zu den Lesern von Oberberg-Aktuell zu senden. Entstanden ist ein entsprechendes System, sowie Empfehlungen zur Technik und der Erweiterbarkeit, sowie die Konzeption der filmischen Aspekte solch einer Berichterstattung. </p></div>
<div class="clear">&nbsp;<br /></div></div></div>
