<h2 class="float einfach"><br class="spacer" /> </h2>
<div class="float dreifach last">
<p><strong>Von der Idee zum Konzept... - ein Usability Engineering Prozess</strong><br />Carina Oberm&uuml;ller, Praxisprojekt </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<dl class="einfach bild"><dt><img src="images/die_besten_univer_12_647_.jpg" alt="Die besten Projekte 08/09 Universalthema " width="172" class="" /></dt></dl>
<div class="float dreifach">
<p>Im DATech Leitfaden Usability werden Methoden und Techniken f&uuml;r einen Usability Engineering Prozess beschrieben, auf denen das im Praxisprojekt angewandte Vorgehensmodell zur Entwicklung eines ToDo-Managers basiert. Bestandteil dieses Vorgehensmodells ist eine auf Kontext-Szenarien basierte Analyse, in der Kontext-Interviews durchgef&uuml;hrt werden, um den Nutzungskontext zu bestimmen, sowie Nutzungsanforderungen zu ermitteln. Zudem wird ein Modell der Aufgaben der Benutzer erstellt, in dem die Kernaufgaben der Benutzer definiert werden, auf Basis derer Nutzungsszenarien erstellt werden k&ouml;nnen. Diese liefern die Basis f&uuml;r erste Prototypen und sind Bestandteil des konzeptionellen UI-Designs. </p></div>
<div class="clear">&nbsp;<br /></div></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach"><br class="spacer" /> </h2>
<div class="float dreifach last">
<p><strong>Ein mobiler Client f&uuml;r den Web Service Channelthing - Konzeption und Realisierung der Benutzungsschnittstelle f&uuml;r eine mobile Gesch&auml;ftsanwendung auf dem iPhone</strong><br />Felix M&uuml;ller, unterst&uuml;tzt von den Channelthing-Entwicklern Jan Kus, Tim Schneider & Michael Bumann, Masterprojekt im Forschungsschwerpunkt Business Transactions in Mobile Environments </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<dl class="einfach bild"><dt><img src="images/die_besten_univer_12_648_.jpg" alt="Die besten Projekte 08/09 Universalthema " width="172" class="" /></dt></dl>
<div class="float dreifach">
<p>Der Web Service Channelthing steht Teams als neuartiges Kommunikationstool zur Verf&uuml;gung und f&uuml;hrt die gesamte asynchrone und &uuml;ber viele Medien verstreute Team- Kommunikation in Online-Kan&auml;len nachvollziehbar und annotierbar zusammen. Eine mobile Applikation, die die Teammitglieder jederzeit auf ihren mobilen Endger&auml;ten mit sich f&uuml;hren k&ouml;nnen, erm&ouml;glicht eine ortsentkoppelte, kontextbezogene Nutzung des Web Service und erweitert dessen Einsatzszenarien dadurch signifikant. Ziel dieses Projekts war die Konzeption und die prototypische Implementierung eines mobilen Clients f&uuml;r den Web Service auf der iPhone-Plattform, wobei intensiv mit den Channelthing-Entwicklern zusammengearbeitet wurde. Der Schwerpunkt des Projekts lag auf der Gestaltung der Benutzungsschnittstelle f&uuml;r die mobile Applikation. </p></div>
<div class="clear">&nbsp;<br /></div></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach"><br class="spacer" /> </h2>
<div class="float dreifach last">
<p><strong>Assessment von Unternehmensprozessen zur Softwareentwicklung hinsichtlich ihres Reifegrades, gebrauchstaugliche Systeme zu generieren.</strong><br />Ren&eacute; Rappenh&ouml;ner, Praxisprojekt </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<dl class="einfach bild"><dt><img src="images/die_besten_univer_12_649_.jpg" alt="Die besten Projekte 08/09 Universalthema " width="172" class="" /></dt></dl>
<div class="float dreifach">
<p>Ziel war es, den Entwicklungsprozess eines Unternehmens hinischtlich der F&auml;higkeit, gebrauchstaugliche Systeme zu entwickeln, zu analysieren. Zur Bewertung der Entwicklungsprozesse wurden internationale Standards wie g&auml;ngige Methoden und Techniken aus dem Usability Engineering verwendet, um ein umfangreiches Ergebnis aus verschiedenen Perspektiven zu erhalten. Neben den Prozessen wurden auch die Ergebnisse, die aus diesen hervorgehen, analysiert und bewertet. </p></div>
<div class="clear">&nbsp;<br /></div></div></div>
<div class="zeile">
