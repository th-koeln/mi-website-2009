<h2 class="float einfach">Wie werde ich als Medieninformatiker reich und ber&uuml;hmt? </h2>
<div class="float dreifach last">
<p>Unter diesem Motto stand der Miniwettbewerb "Die besten Projekte", der dieses Jahr zum ersten Mal in den Medieninformatik Studieng&auml;ngen ausgelobt wurde. Die 10 von der Jury ausgew&auml;hlten Projekte wurden auf dem <a href="../../../../../website/mi_am_campu/others/showcase_105/de/de_showcase_univer_1.php" rel="pkm105">Showcase</a> pr&auml;sentiert. Hier wurde dann von den Besuchern noch der "Best Presentation Award" vergeben. Im Folgenden finden Sie die Preistr&auml;ger und ihre Projekte. </p></div>
