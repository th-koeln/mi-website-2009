<h2 class="float einfach"><br class="spacer" /> </h2>
<div class="float dreifach last">
<p><strong>SpotNews, Geodatenbasiertes Nachrichtendistributionssystem</strong><br />Niclas Tocha und Manuel Krischer; MCI/MMA Praktikum </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<dl class="einfach bild"><dt><img src="images/die_besten_univer_12_652_.jpg" alt="Die besten Projekte 08/09 Universalthema " width="172" class="" /></dt></dl>
<div class="float dreifach">
<p>Spot News ist eine plattformunabh&auml;ngige Schnittstelle zwischen den Informanten, der Nachrichtenagentur und den Journalisten im Nachrichtenbusiness. Es vereinfacht die &Uuml;bermittlungen von Informationen an die Agentur durch die Informanten und verteilt die Auftr&auml;ge aus einem heterogenen System heraus, nur an Journalisten, die sich in der N&auml;he des Geschehens aufhalten. Als mobile Clients werden javaf&auml;hige Mobiltelefone mit IP-basierter Verbindung sowie zus&auml;tzlich GPS-Funktionalit&auml;t auf Journalistenseite vorausgesetzt. </p></div>
<div class="clear">&nbsp;<br /></div></div></div>
