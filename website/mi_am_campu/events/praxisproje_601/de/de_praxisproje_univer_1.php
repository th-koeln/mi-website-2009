<div class="float dreifach last">
<p>Das Abschlussprojekt ist im Medieninformatik Bachelor in zwei Teile gegliedert: das Praxisprojekt und die Bachelorarbeit. Beide Projekte m&uuml;ssen nicht zwingend in einem Themenfeld angesiedelt sein. Jedoch ist dies die &uuml;bliche Konstellation, zumal dadurch eine intensive Auseinandersetzung in einem Themen- beziehungsweise Problemfeld besser m&ouml;glich ist. Um den angehenden Absolventen also ein "Abtauchen" in die Abschlussarbeit zu erleichtern und fr&uuml;hzeitig mit m&ouml;glichen Kooperationspartnern und Themengebieten vertraut zu machen, fand am 20.01.2016 die MI Praxisprojekt Kontaktb&ouml;rse statt, wo Dozenten und externe Sprecher Vortr&auml;ge hielten. </p></div>
