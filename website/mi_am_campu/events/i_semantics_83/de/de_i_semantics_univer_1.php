<h2 class="float einfach">Inhalt der Konferenzreihe </h2>
<div class="float dreifach last">
<p>Durch das breite Themenspektrum von Wissenmanagement, &uuml;ber semantische Technologien bis hin zu Industriebeitr&auml;gen wurden bis zu 6 Tracks gleichzeitig angeboten, so dass man stets eine Vortrag finden konnte, der einen interessierte. <br /><br />Neben den fachlich oft interessanten Vortr&auml;gen sind aber vor allem der pers&ouml;nliche Kontakt Experten aus der ganzen Welt etwas, das jeden Studenten einmal eine solche Konferenz besuchen lassen sollte. So wird in Zukunft im Wiki der Medieninformatik eine neuartige Suche der Uni Karlsruhe verwendet werden, die der &Ouml;ffentlichkeit eigentlich noch gar nicht zur Verf&uuml;gung steht. </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<dl class="einfach bild"><dt><img src="images/i_semantics_univer_12_541_.jpg" alt="I-Semantics 2009 in Graz Universalthema " width="172" class="" /></dt></dl>
<div class="float dreifach">
<p>F&uuml;r genau solche M&ouml;glichkeiten gab es neben Fachvortr&auml;gen und Paneldiskussionen auch zwei "Social Events", in denen man in ungezwungener Atmosph&auml;re Leuten kennenlernen und Projektideen diskutieren konnte. Neben interessanten Gespr&auml;chen konnte man hier auch einfach mit den anderen Teilnehmern gem&uuml;tlich ein Glas Wein trinken und die Livemusik genie&szlig;en.<br /><br />Auf dem Bild links sieht man das Grazer Kunsthaus, in dem am ersten Abend die sogenannte "Welcome Reception" statt fand, bei der man neben der beeindruckenden Architektur auch das Buffet mit lokale Spezialit&auml;ten genie&szlig;en konnte </p></div>
<div class="clear">&nbsp;<br /></div></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach">Fazit </h2>
<div class="float dreifach last">
<p>Durch den studentenfreundlichen Betrag von 80&euro; f&uuml;r Fr&uuml;hbucher ist die I-Semantics eine Konferenz, die man nur jedem Studenten, der sich f&uuml;r das Themengebiet "Semantik" interessiert ans Herz legen kann. Neben vielen fachlichen Argumenten, spricht auch noch die wundersch&ouml;ne Stadt Graz mit ihren zahlreichen Attraktionen (Kunsthaus, Murinsel, Dom im Berg,...) f&uuml;r einen Besuch im Jahr 2010. </p></div>
