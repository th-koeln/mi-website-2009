<h1>I-Semantics 2009 in Graz </h1>
<div class="zeile">
<h2 class="float einfach">International Conference on Semantic Systems </h2>
<div class="float dreifach last">
<p>Die I-Semantics ist eine Konferenzreihe, die sich mit der semantischen Modellierung und dem Semantic Web besch&auml;ftigt. Sie findet in Kooperation mit der Wissenmanagement-Konferenz "I-Know" j&auml;hrlich in Graz statt. F&uuml;r die Medieninformatik haben der Masterstudent Lars Brillert und der wissenschaftliche Mitarbeiter Stephan Pavlovic dieses Jahr teilgenommen. </p></div>
