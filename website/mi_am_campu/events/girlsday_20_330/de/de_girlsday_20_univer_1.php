<h1>Girlsday 2010 </h1>
<div class="zeile">
<h2 class="float einfach">10. M&auml;dchen-Zukunftstag in K&ouml;ln und Gummersbach </h2>
<div class="float dreifach last">
<p>Am 23. April 2010 fanden im Rahmen des M&auml;dchen-Zukunftstages zwei von der Medieninformatik organisierte Veranstaltungen statt, eine am Standort in K&ouml;ln-Deutz und die andere am Campus Gummersbach. Insgesamt konnten &uuml;ber 50 Sch&uuml;lerinnen das Berufsfeld eines Medieninformatikers / einer Medieninformatikerin kennenlernen und anhand praktischer &Uuml;bungen „hautnah" erleben. </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach">Das kauf ich euch ab - vom Werbespot zum Fahrkartenautomat. </h2>
<div class="float dreifach last">
<p><a href="../../../../../website/mi_am_campu/prof/prof_dr_g_17/de/de_prof_dr_g_univer_1.php" rel="pkm17">Gerhard Hartmann</a> f&uuml;hrte am Standort Deutz in die Themenstellung ein und zeigte den Zusammenhang zwischen Film-Figuren und benutzerzentrierter Gestaltung interaktiver Systeme auf. Anhand der in den Werbevideos gezeigten Akteure wurden die Sch&uuml;lerinnen f&uuml;r unterschiedlichste Personengruppen sensibilisiert, so dass sie im Anschluss in Gruppenarbeit entsprechende User Profiles erstellen konnten. Dabei wurden sie von Vertretern aus der Medieninformatik (<a href="../../../../../website/mi_am_campu/wiss_ma/carina_ober_159/de/de_carina_ober_univer_1.php" rel="pkm159">Christiane Gr&uuml;nloh</a>, <a href="../../../../../website/mi_am_campu/wiss_ma/christiane_87/de/de_christiane_univer_1.php" rel="pkm87">Carina Oberm&uuml;ller</a>, Sahrah El ghammaz, Maike Schickendanz) tatkr&auml;ftig unterst&uuml;tzt. Die erstellten User Profiles wurden im Anschluss von der jeweiligen Gruppe pr&auml;sentiert.<br /><br /><a href="../../../../../website/mi_am_campu/wiss_ma/christiane_87/de/de_christiane_univer_1.php" rel="pkm87">Christiane Gr&uuml;nloh</a> gab den Sch&uuml;lerinnen einige ausgew&auml;hlte Kreativit&auml;tstechniken an die Hand, mit deren Unterst&uuml;tzung sie dann selbst kreativ werden konnten. Von jeder Gruppe wurde ein Prototyp f&uuml;r einen Fahrkartenautomaten erstellt, bei dem vor allem die vorher erarbeiteten Use Cases und Problemszenarien ber&uuml;cksichtigt wurden. Die Ergebnisse waren beeindruckend, ebenso wie das Engagement der &uuml;beraus motivierten Sch&uuml;lerinnen.<br /><br />Im Anschluss an die Veranstaltung der Medieninformatik hatten die Sch&uuml;lerinnen noch Gelegenheit, Fragen zum Studium bzw. allgemein zum Abschluss an einer Fachhochschule zu stellen.<br /><br />In der abschliessenden anonymen Befragung gaben 10 Teilnehmerinnen an, dass sie ein Medieninformatik-Studium in die engere Wahl ziehen. </p></div>
