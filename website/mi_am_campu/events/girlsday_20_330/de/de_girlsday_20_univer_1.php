<h2 class="float einfach">How to Weave the Web? </h2>
<div class="float dreifach last">
<p>Wie entsteht eigentlich eine Website? Diese Fragestellung war Ausgangspunkt der Veranstaltung in Gummersbach. Nach einem &Uuml;berblick &uuml;ber die verschiedene Aufgaben von Medieninformatikern und einem exemplarischen Wochenablauf, f&uuml;hrte <a href="../../../../../website/mi_am_campu/prof/prof_chris_5/de/de_prof_chris_univer_1.php" rel="pkm5">Christian Noss</a> in das Themengebiet Konzipieren, Gestalten und Realisieren eines Webangebots ein. Als Beispielanwendung sollte die Website der Band "die Killerpilze" konzipiert werden.<br /><br />Im ersten Workshopteil entwickelten die Sch&uuml;lerinnen, unterst&uuml;tzt von den Medieninformatikstudentinnen Julia Beyer und Alice Bajorek, die inhaltliche Struktur der Website und organisierten die Inhalte mit Hilfe der Card Sorting Methode. Die Ergebnisse wurden dann der Gesamtgruppe pr&auml;sentiert.<br /><br />Im n&auml;chsten Schritt ging es weiter zum Thema Layout. Nach der Erl&auml;uterung einiger grunds&auml;tzlicher Konzepte und Herangehensweisen, wurden in den Gruppen papier-basierte Layout-Scibbles entwickelt, die final ausgestellt und diskutiert wurden und so den Abschluss der Veranstaltung in Gummersbach bildeten. </p></div>
