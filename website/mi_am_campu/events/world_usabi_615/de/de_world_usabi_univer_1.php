<p>Der weltweit stattfindende „World Usability Day" hat dieses Jahr als Fokusthema „Sustainable User Experience (UX)" bzw. „Green UX" und auch an der TH K&ouml;ln wurde ein interessantes Vortragsprogramm zusammen gestellt. <br /><br /><strong>Olga Witt</strong> vom Zero Waste Laden wird uns in das Themengebiet „Leben ohne M&uuml;ll" einf&uuml;hren und einen &Uuml;berblick dar&uuml;ber geben, mit welchen Fragen sich die "Zero Waste Bewegung" besch&auml;ftigt und welche konkreten L&ouml;sungen es gibt.<br /><br /><strong>Felix Dietz</strong> von der Dingfabrik K&ouml;ln e.V. stellt Initiativen wie Farblab, Makerspace und RepairCafe vor und pr&auml;sentiert am Beispiel der Dingfabrik wie die Szene zu Nachhaltigkeit und Usability beitragen kann und berichtet von Menschen, die erstmals mit Technik jenseits des User Interface in Kontakt kommen.<br /><br />Auch im Beitrag von <strong>Liane Kirschner und Tim Schneider</strong> (Railslove GmbH) geht es um Nachhaltigkeit; sowohl hinsichtlich der Produkte, aber auch wie man mit einem nachhaltigen Team in einem nachhaltigen Prozess nachhaltige UX etabliert. <br /><br /><strong>Franz-L. Jaspers, Philipp Schulte, Sheree May Sa&szlig;mannshausen und Robert Gabriel</strong> stellen ihr Projekt „Smart Sustainable Home" aus dem Masterstudiengang Medieninformatik der TH K&ouml;ln vor, welches sie bereits im M&auml;rz 2016 erfolgreich auf der Student Interaction Design Research Conference in Malm&ouml; pr&auml;sentieren konnten. <br /><br />Die Veranstaltung findet am Donnerstag, 10.11.2016 von 17-19.30 Uhr statt.<br /><br />Campus S&uuml;dstadt<br />Mevissensaal<br />Claudiusstra&szlig;e 1<br />50678 K&ouml;ln<br /><br />Eine Anmeldung ist nicht erforderlich. Weitere Informationen zum Programm und den Referenten findet Ihr <a href="http://www.wud-koeln.de/">hier</a> . </p></div>
