<h1>CBC Preis 2012 </h1>
<div class="zeile">
<h2 class="float einfach">CBC (Cologne Broadcasting Center) ist eines der f&uuml;hrenden TV-Produktions-, IT- und Broadcast-Unternehmen Deutschlands. CBC betreut unter anderem Sender wie RTL, VOX, Super RTL, RTL II, n-tv.<br /><a href="http://www.cbc.de/cbc3/index.php">mehr zu CBC</a> </h2>
<div class="float dreifach last">
<p>CBC betreut die Technik f&uuml;r die K&ouml;lner „RTL Group Deutschland" und arbeitet seit Jahren eng mit dem Studienbereich Medieninformatik am Campus Gummersbach der Fachhochschule K&ouml;ln zusammen. In einer Feierstunde im Geb&auml;ude der RTL-Group am K&ouml;lner Rheinufer &uuml;berreichte R&uuml;diger Kupke, Bereichsleiter Systeme und Technologie bei CBC, die Urkunden und die Schecks. &Uuml;bergeben wurden die Preise in „Europas modernster Sendeanstalt", wie Kupke die neuen Geb&auml;ude auf dem Gel&auml;nde der K&ouml;ln-Messe bezeichnete. Er betonte, mit welcher Freude er die Studien „auf hohem technischen und theoretischen Niveau" gelesen habe.<br /></p></div>
