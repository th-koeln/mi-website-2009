<div class="float einfach marginalie">
<p><strong>Preistr&auml;ger 2012</strong><br />1. Rafael Kobinski<br />2. Christopher Messner<br />3. Adrian Rennertz </p></div>
<dl class="dreifach bild"><dt><img src="images/cbc_preis_2_univer_11_1593_.jpg" alt="CBC Preis 2012 Universalthema " width="526" class="" /></dt>
<dd>Voller Stolz pr&auml;sentieren die drei gl&uuml;cklichen Preistr&auml;ger ihre CBC-Urkunde f&uuml;r die besten Arbeiten der FH K&ouml;ln (v.l.n.r.: Prof. Dr. Hans L. Stahl, R&uuml;diger Kupke, Christopher Messner, Rafael Kobinski, Adrian Rennertz, Prof. Dr. Kristian Fischer) <br /><br />Den ersten Platz bei den F&ouml;rderpreisen des „Cologne Broadcasting Center" (CBC) erreichte Rafael Kobinski mit seiner Diplomarbeit und erhielt 1000 Euro. In seiner Studie empfiehlt er, verschiedene Komponenten des mobilen Netzwerks auf ein zentrales, nicht mobiles Computersystem zu verlagern, weil nur solche Systeme derzeit die erforderliche Rechnerleistung bieten k&ouml;nnen. Der Pr&uuml;fer Prof. Dr. Stefan Karsch betonte in seiner Beurteilung der Arbeit deren „hohen Neuigkeitswert in einem hochaktuellen Bereich der mobile security".<br /><br />Den zweiten Platz, dotiert mit 750 Euro, erreichte Christopher Messner aus Gummersbach mit einer Bachelorarbeit f&uuml;r das CBC. Er entwickelte am Beispiel eines Redaktionssystems Kriterien f&uuml;r die &Uuml;bertragung von XML-Strukturen auf relationale Datenbanken. Mit seiner Arbeit habe der Informatiker eindrucksvoll gezeigt, dass er eine anspruchsvolle Problemstellung systematisch analysieren und daraus einen praxistauglichen Prototyp entwickeln kann, lobte der Pr&uuml;fer Prof. Dr. Kristian Fischer, zweiter Pr&uuml;fer war Dipl.-Ing. Karl Jost vom CBC. <br /><br />Adrian Rennertz, Absolvent der Medieninformatik, hatte sich begeistert f&uuml;r die Idee, Musik allein aus K&ouml;rperbewegungen zu erzeugen. Er meinte: „Wenn man zu Musik tanzen kann, sollte man auch durch Tanz Musik erzeugen k&ouml;nnen." F&uuml;r die „Entwicklung eines Prototypen auf Basis von Kinect und Pure Data" erhielt er den dritten Preis und 500 Euro. Er entwickelte mehrere Prototypen, welche zahlreiche Ans&auml;tze f&uuml;r m&ouml;gliche Folgearbeiten bieten und stellt damit eine wichtige Grundlage dar. Der Pr&uuml;fer Prof. Konen hatte ebenso wie der Zweitpr&uuml;fer Prof. Christian Noss die m&uuml;ndliche Pr&uuml;fung mit der Note 1,0 bewertet.<br /></dd></dl>
