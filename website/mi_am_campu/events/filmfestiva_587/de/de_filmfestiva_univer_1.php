<div class="float einfach marginalie">
<p>"23, Student, sucht" von StreetBackBoys (Steven Beckers, Marcel Posala, Christian Franke, Patrick Schmitz) </p></div>
<dl class="dreifach bild"><dt><img src="images/filmfestiva_univer_11_1689_.jpg" alt="Filmfestival 2015 Universalthema " width="526" class="" /></dt>
<dd>Bei einem Discobesuch mit seinem Freund und Mitbewohner Steve versucht Andy die zuvor misslungene Klausur zu vergessen. Als er einen Schl&uuml;sselbund mit einem merkw&uuml;rdigen Anh&auml;nger aufheben will, lernt er Emelie kennen. Die Beiden verstehen sich auf Anhieb und haben einen wundervollen Abend zusammen. Am n&auml;chsten Morgen wacht Andy verkatert bei seinem Freund Steve auf und erinnert sich nur vage an den letzten Abend, woraufhin sie nochmal gemeinsam den Verlauf rekonstruieren. Ver&auml;rgert stellt Andy fest, dass er nur den Namen seiner neun Bekanntschaft hat. Trotzdem macht er sich auf die Suche nach ihr und hat dabei zun&auml;chst wenig Gl&uuml;ck. </dd></dl>
