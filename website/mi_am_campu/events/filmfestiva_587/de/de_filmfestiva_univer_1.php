<div class="float einfach marginalie">
<p>"Die Analyse" von Unknown Productions (Meryem Dural, Aylin Mengi, Turac Melis Ira, Jasmin Schermuly, Baris Kabacali, Sebastian Kind) </p></div>
<dl class="dreifach bild"><dt><img src="images/filmfestiva_univer_11_1690_.jpg" alt="Filmfestival 2015 Universalthema " width="526" class="" /></dt>
<dd>Die Psychologiestudenten Derek und Michelle nehmen an einem Projekt teil, f&uuml;r welches sie Patienten einer Nervenklinik interviewen m&uuml;ssen. Thema des Projekts ist: „psychisches Befinden der Menschen im Alter von 20 bis 30 Jahren, welche ihre Familien auf grausame Weise verloren haben". Derek f&auml;llt besonders eine Patientin in etwa seinem Alter auf, welche bei einem Brand ihre ganze Familie verloren hat. Bei der Analyse dieser Patientin kommen dann immer mehr Einzelheiten an Licht, die ein grausames Geheimnis enth&uuml;llen, was dazu f&uuml;hrt, dass die ganze Situation eskaliert. </dd></dl>
