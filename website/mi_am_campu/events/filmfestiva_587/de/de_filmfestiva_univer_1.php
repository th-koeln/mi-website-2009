<h2 class="float einfach"><br class="spacer" /> </h2>
<div class="float dreifach last">
<p>Hier nochmal kurze Zusammenfassungen zu den einzelnen Teilnehmern: </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<div class="float einfach marginalie">
<p>GEWINNER der Ausz&auml;hlung ist "Hugo" von den Wanna Bros (Yannik Huber, Florian Br&auml;unsbach, David Kurz, Milan Feind, Dominik Schuster). Auf <a href="https://www.youtube.com/watch?v=ceQJJEdj2tE&feature=youtu.be">Youtube</a> auch immer noch zu sehen! </p></div>
<dl class="dreifach bild"><dt><img src="images/filmfestiva_univer_11_1686_.jpg" alt="Filmfestival 2015 Universalthema " width="526" class="" /></dt>
<dd>Der Film beginnt mit Hugo, einem Hund, der ein Gespr&auml;ch seiner Herrchen mit anh&ouml;rt. Dabei geht es um die verstorbene Oma, die der Familie eine Vase hinterlassen hat, welche nun im Arbeitszimmer steht. Hugo ist nat&uuml;rlich neugierig und nutzt, als alle aus dem Haus sind, die Gelegenheit, um sich das geliebte Erbst&uuml;ck genauer anzusehen. Ungeschickter Weise geht die Vase dabei zu Bruch. Zum Gl&uuml;ck wei&szlig; sich Hugo zu helfen und bestellt in einem unbeobachteten Moment &uuml;ber den Laptop des Sohnes eine Neue, plus seine Lieblingshundeleckerlies, da er ja schon einmal dabei ist. F&uuml;r die hohe Rechnung wird dann nat&uuml;rlich der Sohn beschuldigt und auch danach wird es nicht wirklich besser f&uuml;r ihn. </dd></dl>
<div class="clear">&nbsp;<br /></div></div></div>
<div class="zeile">
<div class="subzeile">
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<div class="float einfach marginalie">
<p>Den 2. Platz belegt "Wrong Time. Wrong Place." von Yolo Studios (Maximilian Huhn, Oliver Haarmann, Roman Seibt, Sophie Stelzer, Dajana Jeyaratnam). Auf <a href="https://www.youtube.com/watch?v=Ved-xnFVizw">Youtube</a><br /> auch immer noch zu sehen! </p></div>
<dl class="dreifach bild"><dt><img src="images/filmfestiva_univer_11_1687_.jpg" alt="Filmfestival 2015 Universalthema " width="526" class="" /></dt>
<dd>Die Geschichte handelt von zwei jungen Kiffern, die gro&szlig;e Schulden bei einem Drogenboss haben. Sie werden von der rechten Hand des Bosses reingelegt, indem er sie auf eine Party mitnehmen will. Allerdings hat er zu dieser Zeit seinen Boss schon umgebracht, das Geld zur Seite geschafft und die Leiche mit der Mordwaffe im Kofferraum seines Wagens versteckt. Er verschwindet spurlos und alarmiert die Polizei, um die vermeintlichen M&ouml;rder festzunehmen. Die Beweise scheinen eindeutig. </dd></dl>
<div class="clear">&nbsp;<br /></div></div></div>
