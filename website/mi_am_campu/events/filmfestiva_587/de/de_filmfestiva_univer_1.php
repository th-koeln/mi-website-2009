<p>"Eine einzige Lachnummer" von Trippy Animals (Jessica Lee Schulz, Enrico Gette, Sascha Jan Kuhlmann, Bernhard Kelm) </p></div>
<dl class="dreifach bild"><dt><img src="images/filmfestiva_univer_11_1691_.jpg" alt="Filmfestival 2015 Universalthema " width="526" class="" /></dt>
<dd>Einer der Studenten befindet sich anfangs alleine im Wohnzimmer. Nach und nach sto&szlig;en die anderen Mitglieder der WG hinzu. Hierbei werden die einzelnen Charaktere &uuml;berspitzt eingef&uuml;hrt. Vergleichbar mit einer Sitcom werden hierbei Witze und Handlungen mit Lachern eines fiktiven Publikums untermalt. W&auml;hrend die Vier ihren &uuml;blichen T&auml;tigkeiten nachgehen, spielt sich das Publikum immer weiter auf und ihr Lachen wird immer pr&auml;senter, unpassender und absurder. S&auml;mtliche Handlungen, egal ob witzig oder eben nicht, rufen eine Reaktion hervor. Den Studenten selbst f&auml;llt nun auch das Lachen auf und sie machen sich gegenseitig darauf aufmerksam. Durch Einstellen jeglicher Handlungen versuchen sie Lacher zu vermeiden. Mittlerweile &uuml;berschallt das Publikum sogar das Reden der Studenten und als einer der Studenten eine Fernbedienung findet, wird alles noch verr&uuml;ckter. </dd></dl>
