<div class="subzeile">
<div class="float einfach marginalie">
<p>"Words" von Awesome Films (Meike Manke, Natalie Tork Alinaghipour, Jacqueline Voll, Nikolas Gander, Phuong Thuy Duong Vo) Zum Anschauen immer noch auf <a href="https://www.youtube.com/watch?v=G9rKVRglJKY">Youtube</a><br /></p></div>
<dl class="dreifach bild"><dt><img src="images/filmfestiva_univer_11_1688_.jpg" alt="Filmfestival 2015 Universalthema " width="526" class="" /></dt>
<dd>Die sch&uuml;chterne Studentin Amy wird von ein paar Kommilitonen gemobbt, aber sie schafft es nicht, sich dagegen zur Wehr zu setzen. Eines Tages bekommt sie dann in einem Antiquit&auml;ten-Gesch&auml;ft eine Edelsteinkette von dem Verk&auml;ufer geschenkt. Dieser soll ihr angeblich Gl&uuml;ck bringen. Sp&auml;ter stellt sich heraus, dass es sich bei der Kette um ein magisches Artefakt handelt, das ihr die „Kraft der Worte" verleiht. Was bedeutet, dass alles geschieht, was sie wortw&ouml;rtlich zu einer Person sagt. Leider hat das nicht immer nur gl&uuml;ckliche Folgen. </dd></dl>
