<p>"Stay Awake" von Backstreet Bobs (Anatol Walger, Tobias Szuminski, Tobias Gerstenberg, Manuel Michels) </p></div>
<dl class="dreifach bild"><dt><img src="images/filmfestiva_univer_11_1692_.jpg" alt="Filmfestival 2015 Universalthema " width="526" class="" /></dt>
<dd>Charlie Green wacht in seiner verw&uuml;steten Wohnung auf, f&uuml;hlt sich ausgelaugt und hat Kopfschmerzen. Kann sich allerdings nicht erinnern, wie es dazu gekommen sein k&ouml;nnte. Erst eine fremde DVD, die er in dem Chaos findet, liefert ihm die fehlenden Informationen und zugleich eine erschreckende Nachricht. Denn w&auml;hrend er ohnm&auml;chtig war, hat man ihn vergiftet und das Gegenmittel bekommt er nur, wenn die Forderungen in H&ouml;he von 10 Mio. &euro; erf&uuml;llt werden. Bis dahin h&auml;lt ihn nur Koffein am Leben, wobei die Dosis stetig gesteigert werden muss. Die erste Adresse, an die er sich wendet, ist sein wohlhabender Vater. Doch als er bei ihm vor der Wohnung steht, &ouml;ffnet dieser nur verkatert die T&uuml;r und versetzt Charlie in Entsetzen, als er ihm sagt, dass er sein ganzes Geld letzte Nacht verspielt hat. Ein neuer Ausweg muss her und auf der Suche nach einem Hinweis, sto&szlig;en die beiden tats&auml;chlich auf etwas. Neue Hoffnung keimt auf, denn der Wettlauf gegen die Zeit ist nun doch noch zu gewinnen. </dd></dl>
