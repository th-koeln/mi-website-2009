<h1>Gastvortrag von Claudia Cusumano in 'Mensch-Computer Interaktion' </h1>
<div class="zeile">
<h2 class="float einfach"><strong>Wie sieht der Alltag eines Usability Engineers/Interaction Designers aus? Welche Inhalte des Medieninformatik-Studiums sind f&uuml;r diese Querschnittst&auml;tigkeit besonders relevant?</strong> </h2>
<div class="float dreifach last">
<p>Diese Fragen beantwortete Claudia Cusumano, Interaction Designerin bei der User Interface Design GmbH (UID) in Ludwigsburg, in Ihrem Gastvortrag vor Studierenden des Moduls "Mensch-Computer Interaktion" von Prof. Dr. Gerhard Hartmann am 30.06.2010.<br /><br />Frau Cusumano hat ihren Bachelor- und Masterabschluss im Studiengang Medieninformatik an der Fachhochschule K&ouml;ln erworben und sich schon w&auml;hrend ihres Studiums auf die Bereiche Usability Engineering und Interaction Design spezialisiert. Neben vielen Fragen zur Berufspraxis und den Kompetenzen eines Usability Engineers stellten die H&ouml;rer auch Fragen zu Berufsperspektiven. Auch nach dem Vortrag stand Claudia Cusumano noch f&uuml;r mehr als eine Stunde weiteren interessierten Nachfragen zur Verf&uuml;gung. </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="zeile">
<div class="subzeile">
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
