<div class="zeile">
<div class="subzeile">
<h2 class="float einfach">Wie empfanden Sie den Einstieg/die Einarbeitung in den Job direkt nach dem Studium? </h2>
<div class="float dreifach last">
<p>Eine Herausforderung stellt sicherlich die Projektvielfalt bei UID dar - aufgrund der unterschiedlichen Branchen, der verschiedensten Systemen und der diversen Technologien. Grunds&auml;tzlich ist es so, dass man sofort in den Projekten mitarbeitet. Im Falle von Workshops (z.B. Konzeptvorstellungen beim Kunden) f&auml;hrt man beispielsweise anfangs mit einem erfahreneren Usability Engineer zum Kunden. Nat&uuml;rlich unterscheiden sich die Projekte in ihrer Komplexit&auml;t. </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<div class="clear">&nbsp;<br /></div></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach">F&uuml;hlten Sie durch das Studium gut auf den Job vorbereitet? </h2>
<div class="float dreifach last">
<p>Ein eindeutiges "Ja". Sicher, die Praxis unterscheidet sich immer ein wenig von der Theorie. Aber insgesamt empfinde ich uns Medieninformatik-Absolventen als sehr gut (aus-)ger&uuml;stet (in methodischer, praktischer und technischer Hinsicht). </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<div class="clear">&nbsp;<br /></div></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach">An welchen Projekten waren Sie bisher beteiligt? Sind Sie zeitgleich in mehreren Projekten aktiv? </h2>
<div class="float dreifach last">
<p>Bisher habe ich bei UID in allen der sechs Branchen (Automotive, Consumer, Enterprise, Industry, Medical & Pharma, Web) mitgearbeitet. Abh&auml;ngig von der Projektkomplexit&auml;t, arbeitet man h&auml;ufig an mehreren Projekten gleichzeitig und im Team (bestehend aus weiteren Interaction Designern/Usability Engineers, Designern und Entwicklern). </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<div class="clear">&nbsp;<br /></div></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach">Was sind Ihre genauen T&auml;tigkeiten in den Projekten? </h2>
<div class="float dreifach last">
<p>Die T&auml;tigkeiten k&ouml;nnen von Projekt zu Projekt sehr unterschiedlich sein. Bisher habe ich verschiedenste Methoden der benutzer-zentrierten Gestaltung anwenden k&ouml;nnen: z.B. Nutzungskontextanalyse, Experten Evaluation, Interaktionskonzept, User Interface Patterns/Patterndesign, Personas, Usability Test. Aber auch die enge Zusammenarbeit mit den &uuml;brigen Projektbeteiligten wie Entwicklern und Designern wird bei UID sehr gro&szlig; geschrieben - vor allem unterst&uuml;tzt durch Anwendung von SCRUM, ein Vorgehensmodell der agilen Softwareentwicklung. </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<div class="clear">&nbsp;<br /></div></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach">Welche Vorteile hat ein Informatikstudium f&uuml;r den Bereich Interaction Design? </h2>
<div class="float dreifach last">
<p>Neben den Modulen "Mensch-Computer Interaktion" und "Interaction Design" empfinde ich vor allem die Informatik Skills als gro&szlig;en Vorteil: z.B. theoretische und praktische Kenntnisse in diversen Programmiersprachen (z.B. C, Java) und Software-Vorgehens- und Prozessmodellen (z.B. XP, RUP). Dadurch kann man sich viel besser in die Entwickler und deren Arbeit "eindenken" und wei&szlig;, welche Informationen wichtig f&uuml;r diese sind. H&auml;ufig sind auch UML-Kenntnisse gefragt (z.B. Aktivit&auml;tsdiagramme), um beispielsweise den Ablauf eines Anwendungsfalls zu verdeutlichen. </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<div class="clear">&nbsp;<br /></div></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach">Welche Vorteile sehen Sie speziell im Masterstudium? </h2>
<div class="float dreifach last">
<p>Ich sehe vor allem die geringe Anzahl an Studenten und die intensive Betreuung durch die Professoren als gro&szlig;en Vorteil; aber auch die Einarbeitung in wissenschaftliche Fragestellungen (ob eigenst&auml;ndig oder im Team). Mir pers&ouml;nlich haben au&szlig;erdem die zahlreichen Seminarvortr&auml;ge (inkl. Peer Reviews), die man vor seinen Mitstudenten h&auml;lt, enorm geholfen, meine Soft Skills auf diesem Gebiet weiter auszubauen. Denn h&auml;ufig schl&uuml;pfe ich als Interaction Designer in die Rolle des Consultant, trete vor dem Kunden und muss das Konzept vorstellen oder den Kunden einfach hinsichtlich Usability beraten. </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<div class="clear">&nbsp;<br /></div></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach">Was verdient man als Interaction Designer? </h2>
<div class="float dreifach last">
<p>Das darf ich nat&uuml;rlich nicht verraten ;-) Hier verweise ich aber gerne auf den „Branchenreport Usability 2009" der German UPA, der u. a. einen Gehaltspiegel gemessen an der zunehmenden Berufserfahrung enth&auml;lt: http://germanupa.de/german-upa/branchenreport </p></div>
