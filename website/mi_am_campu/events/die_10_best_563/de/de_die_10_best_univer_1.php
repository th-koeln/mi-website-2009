<h1>Die 10 besten Projekte vom Showcase 2013 </h1>
<div class="zeile">
<h2 class="float einfach">Ein Tag an dem man vorbeischauen sollte. </h2>
<div class="float dreifach last">
<p>Auch dies Jahr startete der Showcase mit zwei Vortr&auml;gen von externen Sprechern. Diese haben ebenfalls mal am Campus Gummersbach studiert und freuten sich diesmal selber einen Vortrag halten zu d&uuml;rfen. Im Anschluss daran wurden die besten Projekte des letzten Studienjahres in einer Ausstellung gezeigt. Anschlie&szlig;end bestand f&uuml;r die h&ouml;heren Semester im Rahmen einer Feedbackrunde die Gelegenheit Gutes zu loben, Schlechtes zu kritisieren und eigene Ideen in die Medieninformatik Studieng&auml;nge einzubringen. Zum Ausklang wurden ausgew&auml;hlte Filme vorgef&uuml;hrt, die innerhalb der Veranstaltung <a href="../../../../../website/bachelor/studienseme/audiovisuel_395/de/de_audiovisuel_univer_1.php" rel="pkm395">Audiovisuelles Medienprojekt 2. Semester</a><a href="../../../../../website/bachelor/studienseme/audiovisuel_396/de/de_audiovisuel_univer_1.php" rel="pkm396">Audiovisuelles Medienprojekt 3. Semester</a> entstanden sind<br /><br />Es folgen einige visuelle Eindr&uuml;cke, sowie Einblicke in die pr&auml;mierten Projekte. </p></div>
