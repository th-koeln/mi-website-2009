<h1>RTL Preis/CBC 2010 </h1>
<div class="zeile">
<h2 class="float einfach">CBC (Cologne Broadcasting Center) ist eines der f&uuml;hrenden TV-Produktions-, IT- und Broadcast-Unternehmen Deutschlands. CBC betreut unter anderem Sender wie RTL, VOX, Super RTL, RTL II, n-tv.<br /><a href="http://www.cbc.de/cbc3/index.php">mehr zu CBC</a> </h2>
<div class="float dreifach last">
<p>In Kooperation mit der Fachhochschule K&ouml;ln hat CBC die besten Bachelor- und Masterarbeiten des Studiengangs Informatik aus dem Jahr 2010 gek&uuml;rt. Die Sieger erhalten eine Pr&auml;mie von 750 Euro f&uuml;r den ersten Platz, 500 Euro f&uuml;r den zweiten Platz und 250 Euro f&uuml;r den dritten Platz.<br /><br />Lars Brillert belegte mit seiner Masterarbeit (Titel: „Integration von Ontologien in objektorientierte Programmiersprachen als verteilte und dynmaische Datenmodelle") den ersten Platz. Platz zwei erhielt Theresa Linke mit ihrer Masterarbeit (Titel: „Modellierung und Generierung multimedialer Anwendungen f&uuml;r die 'Cologne Service Oriented Integrative Multimedia Architecture‘"). Carina Oberm&uuml;ller belegte den dritten Platz mit ihrer Bachelorarbeit (Titel: „Konzeptionelles und konkretes UI-Design f&uuml;r verteiltes To-Do-Management auf Basis von Requirements Elicitation"). </p></div>
