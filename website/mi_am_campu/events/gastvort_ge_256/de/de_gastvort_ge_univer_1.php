<h2 class="float einfach"><strong>Rocky roads and short cuts in user experience design - Field report of a design-developer.</strong> </h2>
<div class="float dreifach last">
<p>So der Titel des Gastvortrags von Herrn Julian Masuhr, den er am 22. Januar vor Masterstudierenden der Medieninformatik und Design-Studenten der KISD hielt.<br /><br />Julian Masuhr ist Absolvent des Bachelor- und Masterstudiengangs Medieninformatik und seit mehreren Jahren als Interaction Designer berufst&auml;tig. Er berichtete aus seiner Erfahrungsperspektive &uuml;ber Vorgehensweisen in Industrieprojekten, Best-Practices und die Bedeutung der Interdisziplinarit&auml;t f&uuml;r den Bereich Interaction Design am Beispiel des user-experience designs. Herr Masuhr stellte heraus, dass im Bereich des Interaction Designs &uuml;berproportional viele Medieninformatiker t&auml;tig sind, weil sie sowohl die Informatik- als auch die Design-Perspektive bearbeiten. </p></div>
