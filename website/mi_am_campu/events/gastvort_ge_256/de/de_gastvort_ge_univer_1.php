<h1>Gastvort&auml;ge im Rahmen der Masterveranstaltung interaction design </h1>
<div class="zeile">
<h2 class="float einfach"><br class="spacer" /> </h2>
<div class="float dreifach last">
<p>Im Januar gab es f&uuml;r die Studierenden des Medieninformatik Masterstudiengangs im Rahmen der Veranstaltung interaction design von <a href="../../../../../website/mi_am_campu/prof/prof_dr_g_17/de/de_prof_dr_g_univer_1.php" rel="pkm17">Prof. Dr. Gerhard Hartmann</a>, die M&ouml;glichkeit direkt mit Industrievertretern ins Gespr&auml;ch zu kommen, die dankend angenommen wurde.<br /></p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach"><strong>Sich selbst und andere &uuml;berzeugen - kontroverse Diskussionen zum Interaction Design professionell moderieren.</strong> </h2>
<div class="float dreifach last">
<p>So lautete der Titel des Gastvortrags von Herrn Thomas Geis, den er am 29. Januar vor Masterstudierenden der Medieninformatik und Design-Studenten der KISD hielt. <br /><br />Thomas Geis ist Gesch&auml;ftsf&uuml;hrer der K&ouml;lner ProContext GmbH und seit vielen Jahren als Usability Professional t&auml;tig. ProContext weist eine lange Liste nationaler wie internationaler Kunden auf und genie&szlig;t in der Branche ein hohes Ansehen. In seinem Vortrag ging Herr Geis auf sehr praktische Fragestellungen bei Industrieprojekten ein und konnte den H&ouml;rern so tiefe Einblicke in kontroverse Diskussionen zum Thema interaction design und usability von Produkten geben. </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<div class="float einfach marginalie">
<p>Thomas Geis, Gesch&auml;ftsf&uuml;hrer von <a href="http://www.procontext.com/de/">ProKontext</a> in K&ouml;ln. </p></div>
