<h2 class="float einfach"><br class="spacer" /> </h2>
<div class="float dreifach last">
<p>Am 26. M&auml;rz fand am Campus Gummersbach das Medieninformatik Filmfestival 2014 statt. Dabei betrachteten 110 Zuschauer insgesamt elf Filme aus dem Audiovisuellen Medienprojekt des Wintersemesters 2013/14. Im Anschluss w&auml;hlten diese dann die besten drei Filme aus, wobei sich folgende Nominierung ergeben hat.<br /><br />Den ersten Platz belegte die Gruppe Misunderstood Prophets mit Florian Schmiedeberg, Lana Kloodt, Peter Wagner, Marcel G&ouml;bel und Manuel Sorg. Der Titel des Films ist Programm, denn bei "Kochen mit Funky Fresh" sind die Platten hei&szlig;. Die Besonderheit dabei ist, dass der Benutzer den Verlauf der Story bestimmt, also interaktiv beteiligt ist. Zur Vorf&uuml;hrung wurden die einzelnen Szenen allerdings hintereinander geschnitten. <br /><br />Marija Kandic, Carina Kessel, Maik G&uuml;ldenmeister, Dennis Wilk und Dominique Esser als Gruppe Power Puff Girls & Myggi belegten mit ihrem Film "Worum Geht's?" den zweiten Platz. Der Film beginnt damit, dass Kai dringend mit seinem besten Kumpel Markus reden muss. Er ruft ihn in der Fr&uuml;h an und verabredet sich mit ihm f&uuml;r abends. Worauf Markus w&auml;hrend des Tages st&auml;ndig dar&uuml;ber nachdenkt, was er ihm sagen k&ouml;nnte und sich fragt "Worum geht's?". <br /><br />Als Gruppe Placeholder belegen Dieter Derksen, Thomas Friesen, Ricardo Burtzki, Christian Grevenstein und Paul Gleisberg den dritten Platz. Ihr Film mit dem Titel "Der Alanya Express" handelt von einem gem&uuml;tlichen Abend bei Kush, der dadurch zerst&ouml;rt wird, dass der Fahrer von der Pizzaria Alanya krank geworden ist. Somit auch nicht geliefert werden kann. Aus diesem Grund macht sich die Runde zu Fu&szlig; auf den Weg und dieser birgt unerwartete Gefahren.<br /> <br />Alle Gewinnerfilme sind nat&uuml;rlich weiterhin online verf&uuml;gbar unter den folgenden Links: <br />Kochen mit Funky Fresh, <a href="https://www.youtube.com/watch?v=D_9nTNOZxkQ"> Worum Geht's? </a>, <a href="https://www.youtube.com/watch?v=x0HNiCPEOvU&feature=youtu.be"> Der Alanya Express </a>.<br /><br />Die Organisatoren des Abends sind zufrieden mit Ihrem Schaffensraum und bezeichnen das Festival als gelungene Veranstaltung. Wir hoffen und freuen uns darauf, das Festival etablieren zu k&ouml;nnen, um auch weiterhin einem breiten Publikum verschiedenste Augenschm&auml;use zu bieten. </p></div>
