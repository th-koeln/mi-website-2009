<h1>Konferenz: Mensch und Computer 2009 in Berlin </h1>
<div class="zeile">
<h2 class="float einfach">Grenzenlos frei!? </h2>
<div class="float dreifach last">
<p>An der Humboldt-Universit&auml;t zu Berlin fand vom 06.09. bis 09.09.2009 die diesj&auml;hrige „Mensch & Computer" unter dem Motto „Grenzenlos frei?!" statt. Sie integrierte zudem die Praxistagung Usability Professionals 2009 der German Usability Professionals Association (UPA) und bot damit Usability Professionals aus Wirtschaft und Forschung (und solche, die es noch werden wollen) ihre Ideen, Erfahrungen und Erkenntnisse untereinander auszutauschen. Ausgerichtet wurde die Tagung von der Humboldt-Universit&auml;t zu Berlin, sowie durch den Fachbereich Mensch-Computer-Interaktion die Gesellschaft f&uuml;r Informatik e.V. (GI) und dem German Chapter der Association for Computing Machinery (ACM). </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="zeile">
<div class="subzeile">
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<div class="float einfach marginalie">
<p>Keynote von Paul Hekkert: "Aesthetics is in the usability of the beholder." </p></div>
<dl class="dreifach bild"><dt><img src="images/konferenz_univer_11_551_.jpg" alt="Konferenz: Mensch und Computer 2009 in Berlin Universalthema " width="526" class="" /></dt></dl>
<dl class="einfach bild"><dt><img src="images/konferenz_univer_12_551_.jpg" alt="Konferenz: Mensch und Computer 2009 in Berlin Universalthema " width="172" class="" /></dt></dl>
<div class="float dreifach">
<p>Vier Tage lang konnte zum Thema Usability in Workshops, Tutorien und Vortr&auml;gen das Wissen vertieft und weitergegeben werden. Wir, Christiane Gr&uuml;nloh und Holger Fischer, waren als wissenschaftliche Mitarbeiter der Medieninformatik vor Ort in Berlin und haben an der Konferenz teilgenommen. Nach entspannten Workshops am Sonntag zum Thema „Gebrauchstauglichkeit von Texten" und „Versuchsplanung - vom Auftrag zum Abschlussbericht in 90 Minuten", begann der Montag mit der offiziellen Begr&uuml;&szlig;ung und einer motivierenden Keynote von Paul Hekkert (Delft University of Technology, NL) mit dem Titel „Aesthetics is in the usability of the beholder".<br /><br />Der Montag bis Mittwoch bestand aus Vortr&auml;gen, die sowohl Einblicke in den Alltag eines Usability Engineers gaben, also eher praktischer Natur waren, als auch wissenschaftliches Neuland betraten. Zu den Highlights z&auml;hlten f&uuml;r uns u. a. Vortr&auml;ge zum Thema „Psychophysiologische und bildgebende Erhebungsverfahren f&uuml;r User Experience", „Benutzerprofile von Menschen mit Beeintr&auml;chtigungen/F&auml;higkeiten", „Usability-Anforderungen zur Prozessgestaltung" und „Interaktionsm&ouml;glichkeiten mit der Wii Remote und dem Wii Balance Board". Einige Sponsoren waren auch direkt vor Ort vertreten und gaben den Teilnehmern die M&ouml;glichkeit, beispielsweise Hardware direkt auszuprobieren, wie beispielsweise einen Eye-Tracker oder den iPoint 3D des Fraunhofer Instituts. </p></div>
<div class="clear">&nbsp;<br /></div></div></div>
<div class="zeile">
<div class="subzeile">
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<dl class="einfach bild"><dt><img src="images/konferenz_univer_12_552_.jpg" alt="Konferenz: Mensch und Computer 2009 in Berlin Universalthema " width="172" class="" /></dt></dl>
<div class="float dreifach">
<p>Auf der Abendveranstaltung in der Kalkscheune Berlin am Montag bestand, neben dem leckeren Essen, die M&ouml;glichkeit Kontakte zu kn&uuml;pfen und interessante Gespr&auml;che zu f&uuml;hren. Die Tagung endete am Mittwochmittag mit einer spektakul&auml;ren Keynote von Patrick Baudisch (Hasso-Plattner-Institut, Potsdam) mit dem Titel „My new PC is a phone - user interfaces for the new smallness".<br /><br />Alles in allem hat uns die Tagung bereichert und wir haben viel Lese- und Denkstoff mit nach Hause genommen. Die „Mensch und Computer" im n&auml;chsten Jahr findet zusammen mit der e-Learning Fachtagung Information (DeLFI) vom 12. bis 15.09.2010 in Duisburg statt. Die Fachtagung der German UPA ist dann allerdings kein Bestandteil, da die Internationale Hauptkonferenz der UPA 2010 in M&uuml;nchen statt findet (erstmalig au&szlig;erhalb Nordamerikas). </p></div>
