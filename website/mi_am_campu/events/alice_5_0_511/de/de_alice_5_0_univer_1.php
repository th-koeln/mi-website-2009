<div class="float dreifach">
<p>&Uuml;berraschend wird der mitteilsame Blogger vor den Augen seiner Fangemeinde von einer verh&auml;ngnisvollen Fehlermeldung ersch&uuml;ttert - und mit ihm seine gesamte virtuelle Existenz. Denn der Verlust seines Host-Profils w&uuml;rde f&uuml;r den Aussteiger neben dem digitalen, auch seinen sozialen Tod bedeuten. <br /><br />Der Film verfolgt die sich abzeichnende L&ouml;schung eines Users, der nicht in das Raster der Konzerne passt. Protagonisten aus Wirtschaft und Forschung begleiten mit ihren Aussagen Prometheus' digitales Sterben und machen es zu einer aufregenden Recherche nach den Ursachen daf&uuml;r. Handelt es sich bei dem Fehlercode etwa um ein Virus? Oder steckt das ber&uuml;chtigte User-Scoring dahinter, ein Programm, das von Host selbst eingesetzt wird? Wie erwirtschaftet der Konzern eigentlich seine astronomischen Ums&auml;tze und vor allem - mit welchen Usern? </p></div>
