<h2 class="float einfach"><strong>Die Autoren</strong> </h2>
<div class="float dreifach last">
<p><strong>Patrick Doberenz</strong> wurde 1980 in Neubrandenburg geboren. Vor seinem Filmstudium war er unter anderem Referent f&uuml;r Medienwerkst&auml;tten in der freien Kinder- und Jugendhilfe Bonn. Von 2003 bis 2008 studierte er an der Kunsthochschule f&uuml;r Medien K&ouml;ln, 2005/2006 ging er mit einem Stipendium an das Cit&eacute; Internationale des Arts Paris. Patrick Doberenz lebt und arbeitet als freier Filmemacher in K&ouml;ln. </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<dl class="einfach bild"><dt><img src="images/alice_5_0_univer_12_1520_.jpg" alt="Alice 5.0 Universalthema " width="172" class="" /></dt></dl>
<div class="float dreifach">
<p><strong>Philipp Enders</strong> wurde 1980 in Stuttgart geboren. Vor seinem Filmstudium absolvierte er unter anderem eine Ausbildung zum Mediengestalter in Freiburg. Von 2003 bis 2009 studierte er an der Kunsthochschule f&uuml;r Medien K&ouml;ln, 2006/2007 ging er mit einem Stipendium an die internationale Filmhochschule Kuba. Philipp Enders lebt und arbeitet als freier Filmemacher in K&ouml;ln.<br />Die beiden Autoren lernten sich w&auml;hrend ihrer Studienzeit kennen. Ihr gemeinsamer Abschlussfilm ist die mehrfach preisgekr&ouml;nte Dokumentation „man stirbt.". 2010 gr&uuml;ndeten sie die Gegenschuss Filmproduktion. Derzeit sind sie Stipendiaten des AV-Gr&uuml;nderzentrum NRW und produzieren f&uuml;r ZDF/arte einen weiteren Dokumentarfilm. </p></div>
