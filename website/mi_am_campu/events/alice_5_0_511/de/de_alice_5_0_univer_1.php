<h1>Alice 5.0 </h1>
<div class="zeile">
<h2 class="float einfach">Dokufiktion, D 2011, 45 Minuten<br />Full HD 16:9<br />Gegenschuss Filmproduktion/ZDF </h2>
<div class="float dreifach last">
<p>Am <strong>31. Mai 2012</strong> zeigen wir von <strong>16:00-18:00 Uhr</strong> im <strong>MI Studio, 3.216</strong> die Dokufiktion <strong>ALICE 5.0</strong> von <strong>Patrick Doberenz</strong> und <strong>Philipp Enders</strong>. Die Autoren sind mit von der Partie und stehen im Anschluss an die Vorf&uuml;hrung f&uuml;r Fragen zur Verf&uuml;gung. Wir haben im Studio leider nur 90 Pl&auml;tze, also keinesfalls zu sp&auml;t kommen :) </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="zeile">
<div class="subzeile">
<h2 class="float einfach"><strong>Synopsis</strong> </h2>
<div class="float dreifach last">
<p>Ein bescheidenes Baumhaus in der v&ouml;lligen Ein&ouml;de dient dem autarken Lebensk&uuml;nstler Prometheus als Wohnung. Seine Heimat aber ist ein m&auml;chtiges soziales Netzwerk namens Host, das ihm trotz der widrigen Lebensbedingungen den Kontakt zu anderen Menschen erm&ouml;glicht. </p></div>
<div class="clear">&nbsp;<br /></div></div>
<div class="subzeile">
<div class="float einfach marginalie">
<p>Bild 1: Vernetzt in der Ein&ouml;de: der Lebensk&uuml;nstler Prometheus </p></div>
