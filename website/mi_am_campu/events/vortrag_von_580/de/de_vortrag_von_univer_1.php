<p>"Grundlagen von Netzwerk-IO in Bezug auf moderne Webanwendungen"<br /><br />&Uuml;blicherweise wird Node.js als "serverseitiges JavaScript" bezeichnet. Die Tatsache, dass es Node erlaubt, Webanwendung sowohl im Front- als auch im Backend mit JS zu programmieren ist zwar reizvoll, um das Wesen von Node zu verstehen allerdings sekund&auml;r. Node erlaubt es durch Einsatz von event-driven IO effizient mit Netzwerkressourcen umzugehen. Dieser Vortrag soll die Grundlagen von Netzwerk-IO in Bezug auf moderne Webanwendungen erl&auml;utern und Unterschiede im Umgang mit Netzwerk-Resourcen zwischen Node und klassischen Webframeworks aufzeigen.<br /><br />Tim Becker ist Gesch&auml;ftsf&uuml;hrer der Press Any Key UG in K&ouml;ln und besch&auml;ftigt sich mit ganzheitlichen Anwendungen, die Internet und die physikalische Welt miteinander verbinden. </p></div>
